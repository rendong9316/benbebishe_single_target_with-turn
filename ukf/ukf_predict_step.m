% =========================================================================
% ukf_predict_step.m
% =========================================================================
% 功能说明：
%   本函数执行UKF算法的预测步骤（时间更新/先验估计）。
%   整个预测步骤等价于从后验分布 p(x_{k-1}|z_{1:k-1}) 计算
%   先验分布 p(x_k|z_{1:k-1})。具体分为三个子步骤：
%     1) 生成Sigma点（UT变换）
%     2) 将每个Sigma点通过状态转移函数传播
%     3) 从传播后的Sigma点统计计算预测均值和预测协方差
%
%   UKF算法中的角色：
%     预测步骤是UKF滤波循环的第一个阶段。在卡尔曼滤波中，预测步负责
%     根据运动模型将状态估计向前推一个时间步。本函数实现了完整的
%     UT预测流程，输出预测状态、预测协方差和传播后的Sigma点集。
%
%   为何用Sigma点统计采样而非Jacobian（与EKF对比）：
%     EKF（扩展卡尔曼滤波）使用一阶泰勒展开（Jacobian矩阵）对非线性函数
%     线性化，其缺点包括：
%       (a) 对于强非线性函数，一阶近似精度不足，会产生显著截断误差
%       (b) 需要手动推导解析Jacobian，对复杂函数不现实
%       (c) 忽略了高阶矩信息（偏度、峰度等）
%
%     UKF通过Sigma点方法避免上述问题：
%       (a) Sigma点经过真实非线性函数变换后，再统计矩信息，这是
%           "先采样→变换→再统计"的方式，无截断误差
%       (b) 无需计算任何导数，对任意复杂的非线性函数均适用
%       (c) Sigma点精确匹配前两阶矩（均值和协方差），并近似保留
%           更高阶矩（通过alpha、beta参数调节）
%       (d) 理论上对于任意非线性函数，UT变换的均值精度为三阶
%           （对高斯分布），协方差精度至少为二阶
%
%   输入参数：
%     ukf    : UKF滤波器结构体（struct），必须包含以下字段：
%              - ukf.x        : 上一时刻后验状态估计，4×1向量
%              - ukf.P        : 上一时刻后验协方差，4×4矩阵
%              - ukf.n        : 状态维度（4）
%              - ukf.lam      : lambda参数
%              - ukf.dt       : 采样时间间隔（秒）
%              - ukf.Wm       : 均值权重向量，(2n+1)×1
%              - ukf.Wc       : 协方差权重向量，(2n+1)×1
%              - ukf.Q        : 过程噪声协方差，4×4矩阵
%
%   输出参数：
%     x_pred  : 先验（预测）状态均值，4×1向量，即 x_{k|k-1}
%     P_pred  : 先验（预测）协方差矩阵，4×4矩阵，即 P_{k|k-1}
%     X_pred  : 传播后的Sigma点矩阵，4×(2n+1)
%               在更新步骤中会再次使用这些点通过量测模型
%     ukf     : 更新后的UKF结构体（本函数不修改ukf，保持原样返回）
%
%   注意事项：
%     - 本函数假设状态转移函数 cv_model_state_transition 是线性的，
%       因为CV模型本身就是线性的。即便如此仍用Sigma点传播，以保持
%       与非线性量测更新的框架一致性
%     - 对于CV模型，预测均值 x_pred 与直接用 F*x 结果一致（线性变换下
%       UT不增加信息），但协方差传播公式通过Sigma点重新计算
%     - 过程噪声 Q 在每个预测步累加到 P_pred 中，反映了模型不确定性
%     - Wm 和 Wc 是预计算好的权重，保证Sigma点统计量无偏
% =========================================================================

function [x_pred, P_pred, X_pred, ukf] = ukf_predict_step(ukf)
    % ======== 子步骤1：生成Sigma点 ========
    % 根据上一时刻的后验均值和协方差，调用 ukf_sigma_points 生成 2n+1 个Sigma点
    % 输入：ukf.x（均值）、ukf.P（协方差）、ukf.n（维度）、ukf.lam（lambda）
    % 输出：X 矩阵 4×9（因为n=4，所以2*4+1=9个Sigma点）
    X = ukf_sigma_points(ukf.x, ukf.P, ukf.n, ukf.lam);

    % ======== 子步骤2：传播每个Sigma点通过状态转移函数 ========
    % 预分配传播后的Sigma点矩阵，尺寸与输入Sigma点矩阵相同
    X_pred = zeros(ukf.n, 2 * ukf.n + 1);

    % 对每一个Sigma点（共2n+1=9个），应用状态转移函数进行时间传播
    for i = 1:(2 * ukf.n + 1)
        % 将第i个Sigma点 X(:,i) 通过CV模型状态转移函数传播 dt 秒
        % 结果存储在 X_pred(:,i) 中，作为该Sigma点的一步预测
        X_pred(:, i) = ukf_state_transition(X(:, i), ukf.dt);
    end

    % ======== 子步骤3：计算预测状态均值 x_pred ========
    % 预测状态均值是传播后Sigma点的加权和
    % x_pred = sum_{i=1}^{2n+1} Wm_i * X_pred_i
    % 这里的乘法 X_pred * ukf.Wm 即矩阵-向量乘法，
    % X_pred 为 4×9，ukf.Wm 为 9×1，结果为 4×1 向量
    % Wm 权重满足 sum(Wm_i) = 1，保证均值估计无偏
    x_pred = X_pred * ukf.Wm;

    % ======== 子步骤4：计算预测协方差矩阵 P_pred ========
    % 预测协方差 = 过程噪声 Q + Sigma点散布导致的协方差
    % P_pred = Q + sum_{i=1}^{2n+1} Wc_i * (X_pred_i - x_pred) * (X_pred_i - x_pred)'
    %
    % 公式含义：
    %   Q: 过程噪声，反映模型不确定性（CV模型不是完美的）
    %   后半部分：Sigma点相对于预测均值的散布程度，加权求和得到
    %           由于状态转移引入的协方差变化

    % 首先将预测协方差初始化为过程噪声协方差 Q
    P_pred = ukf.Q;

    % 然后逐个累加每个Sigma点的散布贡献
    for i = 1:(2 * ukf.n + 1)
        % dx: 第i个传播后的Sigma点与预测均值的偏差向量（4×1）
        dx = X_pred(:, i) - x_pred;

        % ukf.Wc(i) 是第i个Sigma点的协方差权重（标量）
        % dx * dx' 是该Sigma点的外积矩阵（4×4），描述了该点对协方差的贡献
        % 加权累加到 P_pred，反映所有Sigma点的散布程度
        P_pred = P_pred + ukf.Wc(i) * (dx * dx');
    end
    % 循环结束后，P_pred 即为完整的预测协方差矩阵

    % ======== 子步骤5：协方差正则化（数值稳定性保障）========
    P_pred = regularize_cov(P_pred);
end
