% =========================================================================
% aircraft_trajectory_create_turn.m
% 拐弯航迹生成器 - 在中间形成约120°拐角
% =========================================================================
% 复用 aircraft_trajectory_create 的航段架构，通过3个航路点定义拐弯航迹。
% 航路点经覆盖校验确保全部位于双雷达威力范围内。
% =========================================================================

function [traj, waypoints] = aircraft_trajectory_create_turn(params)
    % 拐弯航路点: 中间点形成约120°拐角
    % 全部位于双雷达威力范围内 (1000-2000km, 15°波束)
    waypoints = [126.0, 32.5, 0;   % 起点 (西南)
                 128.5, 33.5, 0;   % 拐点 (东北) — 入向~62°, 出向~182°
                 128.6, 31.7, 0];  % 终点 (南偏东) — 拐角约120°

    % 拐弯场景降低航速以保持帧数
    speed_ms = 140.0;

    traj = aircraft_trajectory_create(waypoints, speed_ms, params.dt_sec);
end
