% =========================================================================
% ukf_sigma_points.m
% =========================================================================
% 功能说明：
%   本函数是UKF算法中核心的Sigma点生成（UT变换的第一步：确定性采样）。
%   它接收状态均值向量 x 和协方差矩阵 P，通过Cholesky分解（平方根分解）
%   生成 2n+1 个Sigma点。这些Sigma点构成了对原始多元高斯分布 N(x,P) 的
%   确定性近似，围绕均值对称分布，精确匹配原始分布的前两阶矩（均值和协方差）。
%
%   UKF算法中的角色：
%     Sigma点生成是无迹变换（Unscented Transform, UT）的基础步骤。
%     在UKF中，Sigma点会在预测步中被状态转移函数传播（代替传统的Jacobian
%     线性化），在更新步中又被量测函数传播并用于计算量测协方差和互协方差。
%     Sigma点方法本质上是通过少量精心选择的采样点来近似非线性变换后的分布。
%
%   数学原理（详细）：
%     对于一个n维状态向量 x ~ N(mean=x, cov=P)，UT变换生成2n+1个Sigma点：
%
%       X_0 = x                           （中心点，索引1）
%       X_i = x + sqrt((n+lambda)*P)_i    （第i个正方向点，索引i+1, i=1..n）
%       X_{i+n} = x - sqrt((n+lambda)*P)_i（第i个负方向点，索引i+n+1, i=1..n）
%
%     其中 sqrt((n+lambda)*P)_i 表示矩阵 (n+lambda)*P 的Cholesky下三角分解的
%     第i列。Cholesky分解要求矩阵对称正定：
%
%       对于对称正定矩阵 A，Cholesky分解为 A = L * L'，其中L是下三角矩阵
%       那么 sqrt(A) = L（这里"sqrt"指的不是逐元素开方，而是平方根矩阵）
%       因此 L的第i列就是协方差椭球在第i个主轴方向上的"半径"
%
%     lambda参数的含义：
%       lambda = alpha^2 * (n + kappa) - n
%       - alpha (0<alpha<=1) 控制Sigma点的散布程度，通常取1e-3~1
%       - kappa (>=0) 是辅助缩放参数，通常取0或3-n
%       - beta (>=0) 用于引入先验分布的高阶矩信息，对高斯分布beta=2最优
%       - 当lambda>0时Sigma点距均值更远，更"分散"；lambda<0时更"集中"
%
%   Cholesky分解的作用：
%     如果直接用 P 的列向量去生成Sigma点，那么点的散布方向将平行于坐标轴，
%     无法反映变量之间的相关性。Cholesky分解将 P "旋转"到其自然的主轴方向，
%     使Sigma点散布方向与协方差椭球的主轴对齐，精确反映随机变量的散布形态。
%
%     形象地说：Cholesky分解相当于把标准正态分布的点"拉伸"和"旋转"成
%     原分布的协方差椭球形状。
%
%   输入参数：
%     x   : 状态均值向量，n×1 列向量，n为状态维度（本系统中n=4）
%     P   : 状态协方差矩阵，n×n 对称半正定矩阵
%     n   : 状态维度，标量
%     lam : lambda参数，标量，控制Sigma点散布的缩放因子
%
%   输出参数：
%     X   : Sigma点矩阵，尺寸为 n × (2n+1)
%           共2n+1列，每列是一个n维Sigma点
%           第1列为中心点 X_0
%           第2~n+1列：正方向点 X_i (i=1..n)
%           第n+2~2n+1列：负方向点 X_{i+n} (i=1..n)
%
%   注意事项：
%     - Cholesky分解要求矩阵严格正定，若P接近奇异可能导致分解失败
%     - 为应对数值稳定性问题，代码加入了try-catch：若Cholesky失败，
%       则对矩阵加上微小正则化项1e-8*eye(n)后再尝试分解
%     - (n+lambda)项若为负（lambda < -n），也会导致Cholesky失败；
%       正确的alpha、kappa参数选取可避免此情况
% =========================================================================

function X = ukf_sigma_points(x, P, n, lam)
    % ======== 步骤1：Cholesky分解——计算协方差的平方根矩阵 ========
    % 对 (n+lambda)*P 进行Cholesky下三角分解，用于生成Sigma点的散布半径
    % chol(A, 'lower') 返回下三角矩阵L，满足 L*L' = A
    % 这是Sigma点生成中最关键的数值计算步骤
    try
        % 首选路径：直接对 (n+lambda)*P 做Cholesky分解
        % sqrtP 是 n×n 下三角矩阵，其第i列表示第i维的散布方向及大小
        sqrtP = chol((n + lam) * P, 'lower');
    catch
        % 备选路径（数值稳定性保障）：如果上面失败（P不是严格正定或lambda不合适），
        % 则在矩阵对角线上加上微小正值 1e-8，强制使其正定
        % 这是UKF中的标准处理手段，称为"协方差膨胀"或"正则化"
        % 1e-8 * eye(n) 是一个 n×n 对角矩阵，对角元均为 1e-8
        sqrtP = chol((n + lam) * P + 1e-8 * eye(n), 'lower');
    end

    % ======== 步骤2：初始化Sigma点矩阵（预分配内存） ========
    % 共 2n+1 列，每一列存储一个 n 维 Sigma 点
    X = zeros(n, 2 * n + 1);

    % ======== 步骤3：设置中心Sigma点 X_0 ========
    % 第一个Sigma点即状态均值本身，权重最高（由Wm(1)和Wc(1)决定）
    X(:, 1) = x;

    % ======== 步骤4：生成正负方向的Sigma点 ========
    % 沿协方差矩阵的n个主轴方向，分别在正、负两侧各取一个点
    % sqrtP(:, i) 是第i个主轴方向的"散布向量"，其模长反映了该方向的
    % 标准差大小，方向反映了该轴与其他变量的相关性
    for i = 1:n
        % 正方向Sigma点：均值 + Cholesky第i列（沿第i主轴正方延伸）
        X(:, i + 1) = x + sqrtP(:, i);

        % 负方向Sigma点：均值 - Cholesky第i列（沿第i主轴负方延伸）
        % 注意索引偏移：正方向点在第2~n+1列，负方向点在第n+2~2n+1列
        X(:, i + 1 + n) = x - sqrtP(:, i);
    end
    % 循环结束后，X矩阵包含2n+1个Sigma点，对称地围绕均值x分布
end
