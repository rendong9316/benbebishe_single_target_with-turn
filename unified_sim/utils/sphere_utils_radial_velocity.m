% =========================================================================
% sphere_utils_radial_velocity.m
% =========================================================================
% 功能概要：
%   径向速度计算函数。
%   将目标在经纬度方向上的运动速度（度/秒）转换为沿指定方位角的
%   径向速度（米/秒）。所谓"径向速度"是指目标相对于观察点（雷达站）
%   在视线方向上的速度分量。
%
% 数学原理：
%   设目标当前的经纬度变化率为 (lon_rate, lat_rate)，单位为"度/秒"。
%   目标当前纬度为 lat，雷达观测目标的方位角为 az（从北顺时针）。
%
%   第1步：将经纬度变化率转换为本地切平面上的"东-北"速度分量：
%
%      v_east  = lon_rate * (π/180) * R * cos(lat)
%      v_north = lat_rate * (π/180) * R
%
%     其中：
%     - lon_rate * π/180 将经度变化率从 度/秒 转为 弧度/秒
%     - lat_rate * π/180 将纬度变化率从 度/秒 转为 弧度/秒
%     - R = 6371000 米 为地球半径
%     - v_east  是"东向速度"：沿纬度线的线速度（米/秒）
%     - cos(lat) 修正因子：因为纬度越高，经线之间的距离越窄，
%       所以在高纬度处，相同的经度变化对应更小的东向距离
%     - v_north 是"北向速度"：沿经线的线速度（米/秒）
%       纬度变化不需要 cos 修正，因为经线在任何纬度处的间距都相同
%
%   推导（微积分视角）：
%     在纬度 lat 处
%       - 纬度方向上的微弧长 = R * d(lat_rad) = R * (π/180) * d(lat)
%       - 经度方向上的微弧长 = R * cos(lat) * d(lon_rad)
%                            = R * cos(lat) * (π/180) * d(lon)
%     两边除以 dt 即得速度公式。
%
%   第2步：将"东-北"速度投影到雷达视线（LOS, Line of Sight）方向上：
%
%      rv = v_east * sin(az) + v_north * cos(az)
%
%     其中 az 是方位角（从北顺时针计量）：
%     - 当 az = 0°（正北）：cos(0)=1, sin(0)=0,
%       rv = v_north，即只有北向速度贡献径向速度
%     - 当 az = 90°（正东）：cos(90)=0, sin(90)=1,
%       rv = v_east，即只有东向速度贡献径向速度
%     - 当 az = 45°（东北）：cos(45)=sin(45)=√2/2,
%       rv = (v_east + v_north) * √2/2，东和北各贡献一半
%
%   符号约定：
%   - 径向速度 > 0：目标远离雷达（距离在增大）
%   - 径向速度 < 0：目标接近雷达（距离在减小）
%   - 这取决于目标速度与雷达视线方向的夹角：
%     同向（远离）为正，反向（接近）为负
%
% 在项目中的角色：
%   本函数是"经纬度速度"与"雷达测量径向速度"之间的桥梁。
%   雷达通过多普勒效应可以测量目标的径向速度（单位：米/秒），
%   但目标的状态向量通常包含的是经纬度方向上的速度（单位：度/秒）。
%   本函数将状态速度转换为量测域的速度，用于：
%   1. 量测生成：计算雷达应观测到的"理想"径向速度
%   2. UKF 量测模型 h(x)：将状态预测映射到量测空间（径向速度部分）
%
% 输入：
%   lon_rate - 经度方向的速度（单位：度/秒，正值=向东移动）
%   lat_rate - 纬度方向的速度（单位：度/秒，正值=向北移动）
%   lat      - 目标当前的纬度（单位：度，北纬为正）
%   az       - 从观察点（雷达）到目标的方位角（单位：度，从北顺时针 0-360）
% 输出：
%   rv - 径向速度（单位：米/秒，正值=远离雷达，负值=接近雷达）
% =========================================================================

function rv = sphere_utils_radial_velocity(lon_rate, lat_rate, lat, az)
    % -----------------------------------------------------------------
    % 第1步：将纬度和方位角从"度"转换为"弧度"
    % 后续三角运算需要弧度输入
    % -----------------------------------------------------------------
    lat_rad = deg2rad(lat);  % 目标纬度（弧度）
    az_rad  = deg2rad(az);   % 雷达观测方位角（弧度）

    % -----------------------------------------------------------------
    % 第2步：获取地球半径（常数）
    % -----------------------------------------------------------------
    R = 6371000.0;

    % -----------------------------------------------------------------
    % 第3步：将经度变化率（度/秒）转换为东向线速度（米/秒）
    % -----------------------------------------------------------------
    % 转换步骤：
    %   (1) lon_rate * π/180  →  将经度变化率从"度/秒"转为"弧度/秒"
    %   (2) × R               →  将弧度/秒转为弧度对应的弧长/秒 = 米/秒
    %   (3) × cos(lat_rad)    →  纬度修正：在高纬度处，经线密集，
    %                             等经度差对应的实际距离更短
    %
    % 举例：在赤道（lat=0, cos(0)=1）：
    %   v_east = 1°/s * π/180 * 6371000 * 1 ≈ 111195 m/s ≈ 111 km/s
    % 在纬度60°（cos(60°)=0.5）：
    %   v_east = 1°/s * π/180 * 6371000 * 0.5 ≈ 55598 m/s ≈ 55.6 km/s
    % -----------------------------------------------------------------
    v_east = lon_rate * pi / 180.0 * R * cos(lat_rad);

    % -----------------------------------------------------------------
    % 第4步：将纬度变化率（度/秒）转换为北向线速度（米/秒）
    % -----------------------------------------------------------------
    % 转换步骤：
    %   (1) lat_rate * π/180  →  将纬度变化率从"度/秒"转为"弧度/秒"
    %   (2) × R               →  将弧度/秒转为米/秒
    %
    % 注意：北向速度不需要 cos(lat) 修正，
    % 因为经线（南北方向的大圆）在任何纬度处的弧长都是 R×弧度
    % -----------------------------------------------------------------
    v_north = lat_rate * pi / 180.0 * R;

    % -----------------------------------------------------------------
    % 第5步：将东向和北向速度投影到雷达视线方向，合成径向速度
    % -----------------------------------------------------------------
    % 投影公式：
    %   rv = v_east * sin(az_rad) + v_north * cos(az_rad)
    %
    % 原理：这是二维速度向量 [v_east, v_north] 在单位方向向量
    % [sin(az), cos(az)] 上的点积（内积/scalar projection）。
    %
    % 雷达视线方向的单位向量在"东-北"坐标系中的表示为：
    %   u = [sin(az), cos(az)]
    % 因为：
    %   - 方位角从北开始顺时针计量
    %   - 当 az=0°（正北），方向为 [0, 1]（纯北方向）
    %   - 当 az=90°（正东），方向为 [1, 0]（纯东方向）
    %
    % 速度向量在方向向量 u 上的投影 = v · u = v_east*sin(az)+v_north*cos(az)
    % -----------------------------------------------------------------
    rv = v_east * sin(az_rad) + v_north * cos(az_rad);
end