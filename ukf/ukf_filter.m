% =========================================================================
% ukf_filter.m
% =========================================================================
% 功能概要：
%   UKF（无迹卡尔曼滤波）滤波器构造函数。
%   创建并返回一个 UKF 滤波器结构体，包含所有滤波参数和三个公共 API 函数句柄。
%   本函数是整个 UKF 模块的入口，调用后得到一个"滤波器对象"供后续逐帧使用。
%
% =========================================================================
% 【UKF 算法通俗入门 — 写给完全不懂卡尔曼滤波的读者】
% =========================================================================
%
% 0. 卡尔曼滤波解决什么问题？
%    想象你在一间黑暗的房间里跟踪一只猫。你有两个信息来源：
%      (a) 你对猫的运动规律有所了解（"猫通常沿一个方向匀速走"）
%      (b) 你每隔30秒用手电筒照一下，看到猫的大致位置（但有误差）
%    问题是：手电筒照到的位置有误差（噪声），而猫也不会永远匀速走，
%    你如何融合这两种信息得到猫的"最佳估计位置"？
%
%    卡尔曼滤波就是解决这个问题的数学框架。它的核心思想是：
%      先用运动模型"猜"猫下一步在哪，再用测量结果"修正"猜测。
%      信任程度由"不确定性"（协方差）来量化——哪个信息源更可靠就多信哪个。
%
% 1. 为什么需要 UKF（无迹卡尔曼滤波）？
%    标准卡尔曼滤波 (KF) 要求系统是线性的，即：
%      状态转移：x_next = F * x        （线性）
%      量测方程：z = H * x            （线性）
%    但雷达的量测方程是高度非线性的——从经纬度到雷达斜距/方位角要用球面
%    三角函数，写成 H*x 根本做不到。
%
%    EKF（扩展卡尔曼滤波）的解法：对非线性函数做一阶泰勒展开（求 Jacobian），
%    把曲线近似成切线。缺点：
%      - Jacobian 矩阵推导复杂且容易出错
%      - 一阶近似丢掉了高阶信息，强非线性时精度差
%      - 每次迭代都要重新计算 Jacobian，计算量大
%
%    UKF 的解法（本函数的实现）：不需要 Jacobian！核心思想是——
%      "与其把非线性函数近似成直线，不如选几个代表性的采样点，
%       让这些点通过真实的非线性函数，然后从变换后的点统计均值和方差。"
%
%    这叫做 UT（Unscented Transform, 无迹变换）。效果上，UT 对任意非线性
%    函数都能精确到三阶（泰勒展开意义上），而 EKF 只到一阶。
%
% 2. 本系统中的状态空间建模
%    UKF 工作在"状态空间"框架下，需要定义三个要素：
%
%    (a) 状态向量 x（我们要估计的东西）：
%        x = [lon; lon_dot; lat; lat_dot]
%        共4维：经度、经度变化率（度/秒）、纬度、纬度变化率（度/秒）
%        注意这里的"速度"是经纬度变化率（度/秒），不是米/秒。
%
%    (b) 运动模型 f(x)（状态如何随时间演化）：
%        假设目标做匀速直线运动（Constant Velocity, CV 模型）：
%          lon_{k} = lon_{k-1} + lon_dot_{k-1} * dt
%          lon_dot_{k} = lon_dot_{k-1}（速度不变）
%        写成矩阵：x_k = F(dt) * x_{k-1}，其中 F 是 4x4 矩阵。
%        由于 CV 模型本身就是线性的，UKF 在预测步的信号传播很简单。
%        过程噪声 Q 吸收"模型不完美"导致的误差（目标不可能真的绝对匀速）。
%
%    (c) 量测模型 h(x)（状态如何映射到雷达读数）：
%        这是非线性最强的部分。给定目标经纬度和速度 [lon, lat, v_lon, v_lat]，
%        计算雷达看到的三个量：
%          range = Haversine_distance(radar, target)     ← 球面大圆距离
%          azimuth = 球面方位角(radar → target)           ← 三角函数组合
%          radial_vel = 速度向量在雷达-目标连线上的投影     ← 点积运算
%        这三个函数都不是线性的，这正是 UKF 发挥作用的地方。
%
% 3. UKF 的工作流程（每个采样周期执行一次）
%    ┌─────────────┐     ┌────────────────┐     ┌─────────────┐
%    │  预测步       │ ──→ │   Sigma点生成   │ ──→ │  更新步      │
%    │  基于运动模型  │     │  2n+1个采样点   │     │  基于雷达量测  │
%    │  外推状态     │     │  通过量测模型    │     │  修正状态     │
%    └─────────────┘     └────────────────┘     └─────────────┘
%         ↑                                             │
%         └─────────── 下一时刻（递归）──────────────────┘
%
%    详细子步骤：
%      (1) Sigma点生成：从当前状态分布 N(x, P) 生成 2n+1=9 个采样点
%      (2) 状态传播：每个Sigma点通过运动模型 f(x) 传播 dt 秒
%      (3) 预测统计：从传播后的Sigma点计算预测均值 x_pred 和协方差 P_pred
%      (4) 量测传播：每个传播后的Sigma点通过量测模型 h(x)
%      (5) 量测统计：计算预测量测均值 z_pred、量测协方差 P_zz、互协方差 P_xz
%      (6) 卡尔曼增益：K = P_xz / P_zz（决定"量测信息对状态修正的权重"）
%      (7) 状态更新：x_new = x_pred + K*(z_actual - z_pred)
%      (8) 协方差更新：P_new = P_pred - K*P_zz*K'（不确定性因量测信息而降低）
%
% 4. Sigma点参数（alpha, beta, kappa）的直观含义
%
%    UKF 使用三个参数来控制 Sigma 点的分布。它们通过公式
%      lambda = alpha^2 * (n + kappa) - n
%    影响 Sigma 点围绕均值的"散布半径"。
%
%    alpha（通常 1e-4 ~ 1）：
%      控制 Sigma 点离均值多远。alpha 越小，Sigma 点越聚集在均值附近，
%      意味着"估计比较准，不需要探索太远"。alpha 越大，点越分散，
%      意味着"不确定性大，需要探索更广的范围"。
%      本系统设 alpha=1e-3，属于较保守的选择（点较集中）。
%
%    beta（通常 = 2）：
%      引入先验分布的高阶矩（峰度）信息。对于高斯分布，beta=2 是最优的，
%      它让协方差权重 Wc(1) 比均值权重 Wm(1) 多出 (1-alpha^2+beta) 的修正量。
%
%    kappa（通常 = 0 或 3-n）：
%      辅助缩放参数。设为 0 是常见的保守选择；设为 3-n 可以保证
%      对于高斯分布，Sigma 点的峰度与原始分布一致。
%
% 5. 协方差矩阵的物理含义
%
%    P（状态协方差，4x4）：
%      代表滤波器对当前状态估计的"不确定程度"。
%      对角线元素是各分量自身的方差，非对角线是分量间的协方差。
%      P 大 → 滤波器很"不确定"，会更多信任量测而非模型预测。
%      P 小 → 滤波器很"自信"，会比较保守地修正。
%      每次预测步 P 增大（不确定性随时间增加），每次更新步 P 减小（量测消除不确定性）。
%
%    Q（过程噪声协方差，4x4）：
%      代表运动模型的"不完美"程度。CV 模型假设匀速，但真实目标可能有加速度。
%      Q 吸收了模型误差：目标加速/转弯时，UKF 不会因为模型失配而发散。
%      Q 越大 → 滤波器越"不信任"运动模型，会更依赖量测来修正。
%
%    R（量测噪声协方差，3x3）：
%      代表雷达传感器本身的测量误差水平。
%      对角元素分别是距离噪声方差（σ_range²）、方位噪声方差（σ_az²）、
%      径向速度噪声方差（σ_rv²）。这些值来自给定的传感器规格。
%      R 越大 → 滤波器越"不信任"量测，更依赖运动模型。
%
%    Q 和 R 的相对大小决定了滤波器的"信任偏好"：
%      Q/R 比值大 → 信任量测多于模型 → 响应快但更"抖"
%      Q/R 比值小 → 信任模型多于量测 → 轨迹平滑但可能有滞后
%
% 6. 调用方式（纯面向过程、无函数句柄）
%    滤波器创建后，通过直接函数调用进行三种操作：
%      ukf = ukf_filter_init(ukf, meas)                  — 第一帧初始化
%      [lon, lat, ukf] = ukf_filter_update(ukf, meas)    — 有量测帧正常滤波
%      [lon, lat, ukf] = ukf_filter_predict_only(ukf)    — 漏检帧仅预测
%
%    为什么采用纯函数调用而非函数句柄？
%      - 调用关系清晰明确，不存在"结构体上挂函数"的模糊性
%      - 所有 ukf 函数都是独立存在的 .m 文件，调用时显式传入 ukf 结构体
%      - 排除了函数句柄可能带来的复杂性（如闭包捕获、生命周期管理）
%
% 输入：
%   params    — simulation_params 结构体，包含所有 UKF 可调参数
%   radar_lon — 雷达站经度（度），用于量测模型中的坐标转换
%   radar_lat — 雷达站纬度（度）
%   dt        — 采样时间间隔（秒），即相邻两帧量测的时间步长
%
% 输出：
%   ukf — UKF 滤波器结构体，包含所有滤波状态（x, P, Q, R 等），不含函数句柄
% =========================================================================

function ukf = ukf_filter(params, radar_lon, radar_lat, tx_lon, tx_lat, dt)
    % =====================================================================
    % 第1部分：保存基础参数到结构体
    % =====================================================================
    % 这些参数在整个滤波过程中保持不变，供各个子函数访问
    ukf.params = params;           % 完整的仿真参数结构体（含所有 UKF 可调参数）
    ukf.radar_lon = radar_lon;     % 雷达站经度（度），量测模型中的坐标原点
    ukf.radar_lat = radar_lat;     % 雷达站纬度（度），量测模型中的坐标原点
    ukf.tx_lon = tx_lon;           % 照射站（发射站）经度（度），用于双基地量测模型
    ukf.tx_lat = tx_lat;           % 照射站（发射站）纬度（度），用于双基地量测模型
    ukf.dt = dt;                   % 采样时间间隔（秒），状态转移的步长

    % =====================================================================
    % 第2部分：UKF 核心参数计算
    % =====================================================================
    % n = 状态维度 = 4：[lon, lon_dot, lat, lat_dot]
    % m = 量测维度 = 3：[range, azimuth, radial_vel]
    n = 4;                         % 状态空间维度
    alpha = params.ukf_alpha;      % Sigma 点散布因子（控制点离均值的远近）
    beta  = params.ukf_beta;       % 先验分布知识参数（高斯分布取2）
    kappa = params.ukf_kappa;      % 额外缩放参数（通常取0）

    % lambda 是 Sigma 点生成的核心合成参数：
    %   lam = alpha^2 * (n + kappa) - n
    %
    % lambda 影响 Sigma 点与均值之间的距离：
    %   lam > 0 → 点散布更远，探索范围更大（更"保险"）
    %   lam < 0 → 点散布更近，探索范围更小（更"集中"）
    %   lam = 0 → 刚好使 (n+lam) = alpha^2*(n+kappa)
    %
    % 保守设计中常通过调整 alpha 确保 lam >= 0，以保证 sqrt(lam*P) 为实数
    lam = alpha^2 * (n + kappa) - n;

    ukf.n   = n;                   % 状态维度（4），存储以备子函数使用
    ukf.m   = 3;                   % 量测维度（3）：斜距、方位角、径向速度
    ukf.lam = lam;                 % lambda 合成参数

    % =====================================================================
    % 第3部分：计算 Sigma 点权重
    % =====================================================================
    % UKF 使用两组权重：Wm 用于均值估计，Wc 用于协方差估计。
    % 共计算 2n+1 = 9 个权重值，分别对应 9 个 Sigma 点。
    %
    % 权重公式（经典 UKF）：
    %   中心点 (i=1):
    %     Wm(1) = lambda / (n + lambda)
    %     Wc(1) = lambda / (n + lambda) + (1 - alpha^2 + beta)
    %   其余点 (i=2..2n+1):
    %     Wm(i) = Wc(i) = 1 / [2*(n + lambda)]
    %
    % 权重满足归一化条件：sum(Wm) = 1，确保均值估计无偏。
    %
    % Wc(1) 比 Wm(1) 多出的 (1-alpha^2+beta) 项是为了修正协方差估计中
    % 的高阶矩偏差。对于高斯分布且 beta=2，alpha=1 的特殊情况，
    % Wc(1) = Wm(1) + 2。

    % 初始化所有权重为 1 / [2*(n+lambda)]
    ukf.Wm = ones(2 * n + 1, 1) / (2.0 * (n + lam));
    ukf.Wc = ones(2 * n + 1, 1) / (2.0 * (n + lam));

    % 中心点的权重具有不同的公式（其余点共享同一个公式）
    ukf.Wm(1) = lam / (n + lam);                              % 中心点均值权重
    ukf.Wc(1) = lam / (n + lam) + (1.0 - alpha^2 + beta);     % 中心点协方差权重

    % =====================================================================
    % 第4部分：构建量测噪声协方差矩阵 R (3×3)
    % =====================================================================
    % R 是量测噪声协方差矩阵，描述雷达传感器本身的测量误差水平。
    % 这里假设三个量测分量（距离、方位、径向速度）的噪声互不相关，
    % 所以 R 是一个对角矩阵：
    %
    %   R = [σ_range²      0             0         ]
    %       [   0        σ_azimuth²      0         ]
    %       [   0           0        σ_radialVel² ]
    %
    % 每个对角元素 = 对应量测噪声标准差的平方（即方差）。
    % 这些标准差来自 config/simulation_params.m 中的配置。
    ukf.R = diag([params.ukf_range_std_m^2, ...                % 距离噪声方差（m²）
                  params.ukf_azimuth_std_deg^2, ...            % 方位噪声方差（deg²）
                  params.ukf_rv_std_ms^2]);                    % 径向速度噪声方差（(m/s)²）

    % =====================================================================
    % 第5部分：构建过程噪声协方差矩阵 Q (4×4)
    % =====================================================================
    Q_base = diag([1e-9, 1e-13, 1e-9, 1e-13]);
    ukf.Q = Q_base * params.ukf_Q_scale;
    ukf.Q_base = ukf.Q;  % 保存基准Q供模糊自适应调节参考
    ukf.Q_ema = 1.0;      % 模糊自适应EMA状态初始化

    % =====================================================================
    % 第6部分：构建初始状态协方差矩阵 P (4×4)
    % =====================================================================
    % P 表示滤波器对当前状态估计的"不确定程度"。
    % 初始时刻，滤波器对目标的位置和速度都没有信息，
    % 需要给一个较大的 P 来表示"我很不确定"。
    %
    % 对角线元素：
    %   pp² = 初始位置方差（度²），pp 来自参数 ukf_P_pos_std（默认 0.1°）
    %   pv² = 初始速度方差（(度/秒)²），pv 来自参数 ukf_P_vel_std（默认 0.01°/s）
    %
    % 采用对角矩阵意味着假设各状态分量之间不相关（简化假设）。
    pp = params.ukf_P_pos_std;    % 位置标准差（度）
    pv = params.ukf_P_vel_std;    % 速度标准差（度/秒）
    ukf.P = diag([pp^2, pv^2, pp^2, pv^2]);

    % =====================================================================
    % 第7部分：初始化状态向量 x (4×1) 和初始化标志
    % =====================================================================
    % x 初始化为零向量，真正的初始化在收到第一帧量测后由 ukf_filter_init 完成。
    ukf.x = zeros(4, 1);             % 状态向量占位（将在 init 时赋值）
    ukf.initialized = false;         % 初始化标志位（false = 尚未收到第一帧量测）

    % =====================================================================
    % 第8部分：常量
    % =====================================================================
    ukf.R_EARTH = 6371000.0;         % 地球半径（米），用于 Haversine 距离和速度转换
end
