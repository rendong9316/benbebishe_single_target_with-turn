% =========================================================================
% bc_fuse.m — Bar-Shalom-Campo (BC) 单帧融合算法
% =========================================================================
%
% 【功能概述】
%   实现 Bar-Shalom-Campo 提出的考虑互协方差的线性最小方差融合。
%   与 SCC/CI 等算法不同，BC 融合显式地维护两个传感器估计之间的互协
%   方差矩阵 P12，从而实现理论上更精确的融合结果。适用于两个传感器对
%   同一目标的估计存在相关性的场景（如同一过程噪声驱动、公共先验等）。
%
% 【数学原理】
%   BC 融合公式（假设两个估计 x1, x2，协方差 P1, P2，互协方差 P12）：
%
%     新息协方差: S = P1 + P2 - P12 - P12'
%
%     融合增益:   K_bc = (P1 - P12) * inv(S)
%
%     融合状态:   x_fused = x1 + K_bc * (x2 - x1)
%
%     融合协方差: P_fused = P1 - K_bc * (P1 - P12')
%
%   互协方差 P12 的递推维护（在融合循环中由调用方维护）：
%     预测步: P12_pred = F * P12 * F' + Q
%     更新步: P12_new = (I - K1*H1) * P12_pred * (I - K2*H2)'
%             ≈ P1_new * inv(P1_pred) * P12_pred * inv(P2_pred)' * P2_new'
%
%   其中 F 为 CV 模型状态转移矩阵，Q 为过程噪声协方差。
%   两个传感器共享同一过程噪声源，因此互协方差中仅计入 Q 的 1/2。
%
% 【输入参数】
%   x1  - [Nx1] 传感器1（参考源）的状态估计向量
%   P1  - [NxN] 传感器1 的误差协方差矩阵
%   x2  - [Nx1] 传感器2 的状态估计向量
%   P2  - [NxN] 传感器2 的误差协方差矩阵
%   P12 - [NxN] 互协方差矩阵（可选参数，默认为零矩阵）
%         首次融合或无互协方差信息时传入空矩阵 []
%
% 【输出】
%   x_fused - [Nx1] 融合后的状态估计向量
%   P_fused - [NxN] 融合后的误差协方差矩阵
%
% 【调用关系】
%   被 run_track_fusion.m 调用（method='BC' 时）
%   内部调用 regularize_cov() 对协方差矩阵进行正则化
%
% 【参考文献】
%   Bar-Shalom, Y. and Campo, L., "The Effect of the Common Process Noise
%   on the Two-Sensor Fused-Track Covariance", IEEE T-AES, 1986.
% =========================================================================

function [x_fused, P_fused] = bc_fuse(x1, P1, x2, P2, P12)
    % -----------------------------------------------------------------
    % 步骤1: 对两个输入协方差进行正则化，确保数值稳定性
    %        regularize_cov 将强制矩阵对称、正定、条件数可控
    % -----------------------------------------------------------------
    P1 = regularize_cov(P1);
    P2 = regularize_cov(P2);

    % -----------------------------------------------------------------
    % 步骤2: 处理互协方差矩阵 P12
    %        若未传入或为空，则设为零矩阵（退化为独立假设）
    % -----------------------------------------------------------------
    if nargin < 5 || isempty(P12)
        P12 = zeros(size(P1));
    end

    % -----------------------------------------------------------------
    % 步骤3: 计算新息协方差 S
    %        S = P1 + P2 - P12 - P12'
    %        注意：P12 + P12' 是互协方差对总协方差的修正项
    %        若两估计完全独立 (P12=0)，S 退化为 P1+P2（即 SCC 形式）
    % -----------------------------------------------------------------
    S = P1 + P2 - P12 - P12';
    S = regularize_cov(S);  % 正则化确保可逆

    % -----------------------------------------------------------------
    % 步骤4: 计算 BC 融合增益 K_bc = (P1 - P12) / S
    %        使用 MATLAB 的 / 运算符（等价于右乘 inv(S)）
    % -----------------------------------------------------------------
    K_bc = (P1 - P12) / S;

    % -----------------------------------------------------------------
    % 步骤5: 融合状态估计
    %        x_fused = x1 + K_bc * (x2 - x1)
    %        本质是对新息 (x2 - x1) 的加权修正
    % -----------------------------------------------------------------
    x_fused = x1 + K_bc * (x2 - x1);

    % -----------------------------------------------------------------
    % 步骤6: 融合协方差矩阵
    %        P_fused = P1 - K_bc * (P1 - P12')
    %        注意：更新项是 (P1 - P12') 而非 (P1 - P12)
    % -----------------------------------------------------------------
    P_fused = P1 - K_bc * (P1 - P12');
    P_fused = regularize_cov(P_fused);  % 最终正则化
end
