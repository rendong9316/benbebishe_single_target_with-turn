% =========================================================================
% sphere_utils_get_earth_radius.m
% =========================================================================
% 功能概要：
%   地球半径获取函数。
%   返回 WGS84 球面模型的平均地球半径（即体积等效半径）。
%   这是整个球面几何计算模块的基准常数，所有涉及实际物理距离的
%   计算（如 Haversine 距离、径向速度等）都依赖此半径值。
%
% 数学原理：
%   地球并非完美的球体，而是两极稍扁、赤道略鼓的椭球体。
%   WGS84 椭球模型的长半轴 A = 6378137.0 米，扁率 f = 1/298.257223563。
%   在实际工程中，为了简化计算，常采用球面近似模型。
%
%   本函数使用的 6371000.0 米是 WGS84 的体积等效球半径（也称作
%   平均地球半径或 authalic 半径的近似值），它在全球范围内的
%   距离计算误差不超过 0.5%。
%   该值的计算公式为 R = (2*A + B) / 3（三轴半径加权平均），
%   其中 A = 6378137.0 米, B = A*(1-f) ≈ 6356752.3 米。
%
% 在项目中的角色：
%   本函数是球面工具层（sphere_utils_*）的基础依赖项。
%   Haversine 距离、方位角目的地计算、径向速度转换等函数
%   均调用本函数来获取地球半径。
%   集中定义在一个函数中，便于未来若需更换地球模型（如改用
%   更高精度的模型），只需修改此一处即可。
%
% 输入：无（本函数不接受任何输入参数）
% 输出：
%   R - 地球半径（单位：米），数值为 6371000.0
% =========================================================================

function R = sphere_utils_get_earth_radius()
    % 返回 WGS84 球面模型的平均地球半径
    % 6371000.0 米 = 6371 公里
    R = 6371000.0;
end