% =========================================================================
% sphere_utils_haversine_distance.m
% =========================================================================
% 功能概要：
%   球面 Haversine 距离计算函数。
%   根据 WGS84 球面模型计算地球表面两点之间的大圆距离（最短路径）。
%   这是球面导航中最基础、最常用的距离公式，适用于任意两点间的
%   球面距离计算。
%
% 数学原理 —— Haversine 公式：
%   Haversine 公式是球面三角学中用于计算球面上两点间大圆距离的公式。
%   它得名于 "half versed sine"（半正矢）函数：
%       hav(θ) = sin^2(θ/2) = (1 - cos(θ)) / 2
%
%   对于球面上两点 P1(lon1, lat1) 和 P2(lon2, lat2)：
%       hav(Δσ) = hav(Δlat) + cos(lat1) * cos(lat2) * hav(Δlon)
%
%   其中 Δσ 为两点间的球心角（弧度），Δlat = lat2 - lat1，Δlon = lon2 - lon1。
%
%   展开后得到：
%       a = sin^2(Δlat/2) + cos(lat1) * cos(lat2) * sin^2(Δlon/2)
%       c = 2 * atan2(√a, √(1-a))
%       dist = R * c
%
%   其中 R 为地球半径，dist 为最终的大圆距离（米）。
%
%   与余弦定理公式比较：
%   余弦定理公式 d = acos(sin(lat1)*sin(lat2) + cos(lat1)*cos(lat2)*cos(Δlon))
%   在两点距离很近时（a 接近 0），acos 在参数接近 1 时会导致严重的
%   精度损失。Haversine 公式使用 atan2 规避了此问题，对任意距离都
%   保持较高的数值精度。
%
% 在项目中的角色：
%   被航迹生成、量测生成、RMSE 计算、关联距离判定等多个模块调用，
%   是项目中使用频率最高的工具函数之一。
%
% 输入：
%   lon1 - 第一点的经度（单位：度，东经为正）
%   lat1 - 第一点的纬度（单位：度，北纬为正）
%   lon2 - 第二点的经度（单位：度，东经为正）
%   lat2 - 第二点的纬度（单位：度，北纬为正）
% 输出：
%   dist - 两点间的大圆距离（单位：米）
% =========================================================================

function dist = sphere_utils_haversine_distance(lon1, lat1, lon2, lat2)
    % -----------------------------------------------------------------
    % 第1步：将经纬度差值从"度"转换为"弧度"
    % 因为 MATLAB 的三角函数（sin, cos, atan2 等）都要求输入弧度
    % deg2rad 是 MATLAB 自带的函数，换算关系：弧度 = 度 × π / 180
    % -----------------------------------------------------------------
    dlon = deg2rad(lon2 - lon1);   % 经度差（弧度）
    dlat = deg2rad(lat2 - lat1);   % 纬度差（弧度）

    % -----------------------------------------------------------------
    % 第2步：将各点的纬度从"度"转换为"弧度"
    % 后续计算中需要使用纬度的余弦值
    % -----------------------------------------------------------------
    lat1_rad = deg2rad(lat1);      % 第1点纬度（弧度）
    lat2_rad = deg2rad(lat2);      % 第2点纬度（弧度）

    % -----------------------------------------------------------------
    % 第3步：计算 Haversine 公式的中间变量 a
    % -----------------------------------------------------------------
    % a = hav(Δσ) 是球心角 Δσ 的 haversine 值，取值范围 [0, 1]
    %
    % 公式拆解：
    %   sin(dlat/2)^2            ← 纬度差的 haversine 项
    %   cos(lat1_rad)            ← 第1点纬度的余弦（考虑球面纬度收缩）
    %   cos(lat2_rad)            ← 第2点纬度的余弦（考虑球面纬度收缩）
    %   sin(dlon/2)^2            ← 经度差的 haversine 项
    %
    % cos(lat1_rad)*cos(lat2_rad) 这一乘积反映了在不同纬度处，
    % 相同经度差对应的实际弧长不同（纬度越高，经线越密集）
    % -----------------------------------------------------------------
    a = sin(dlat / 2.0)^2 + cos(lat1_rad) * cos(lat2_rad) * sin(dlon / 2.0)^2;

    % -----------------------------------------------------------------
    % 第4步：从 haversine 值反求球心角 c
    % -----------------------------------------------------------------
    % c = 2 * atan2(√a, √(1-a))
    %
    % 此公式的推导：
    %   hav(θ) = sin^2(θ/2) = a
    %   => sin(θ/2) = ±√a  （取正，因为 θ ∈ [0, π]）
    %   => cos(θ/2) = √(1 - a)  （由 sin^2 + cos^2 = 1）
    %   => tan(θ/2) = √a / √(1-a)
    %   => θ = c = 2 * atan2(√a, √(1-a))
    %
    % 使用 atan2(y, x) 而非 atan(y/x) 的优势：
    %   atan2 使用 y 和 x 的符号确定正确象限，自动处理 x=0 的情况
    % -----------------------------------------------------------------
    c = 2.0 * atan2(sqrt(a), sqrt(1.0 - a));

    % -----------------------------------------------------------------
    % 第5步：将球心角乘以地球半径，得到实际物理距离
    % -----------------------------------------------------------------
    % 弧长公式：距离 = 半径 × 球心角(弧度)
    % sphere_utils_get_earth_radius() 返回 6371000.0 米
    % -----------------------------------------------------------------
    dist = sphere_utils_get_earth_radius() * c;
end