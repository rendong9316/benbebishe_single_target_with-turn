% =========================================================================
% regularize_cov.m — 协方差矩阵正则化（强制对称正定）
% =========================================================================
%
% 【功能概述】
%   对任意协方差矩阵 P 执行正则化操作，确保输出矩阵严格对称正定。
%   在 UKF 滤波和航迹融合链路中，由于浮点舍入误差的长期累积，协方差
%   矩阵可能出现以下退化现象：
%     - 不对称：P ≈ P' 但不完全相等（加减法舍入误差）
%     - 半正定/不定：某些特征值变为负值或精确零（减法消去、除法截断）
%     - 病态：条件数过大（最大特征值/最小特征值 >> 1e6）
%
%   一旦协方差非正定，UKF 的 Cholesky 分解（Sigma 点生成）会直接报错，
%   且融合算法的信息矩阵求逆也会数值崩溃。本函数通过特征值裁剪法
%   修复这些问题，同时尽可能保留原始协方差的结构信息。
%
% 【算法步骤】
%   1. 强制对称化：P_sym = (P + P')/2  —— 消除不对称的舍入误差分量
%   2. 特征值分解：P_sym = V * D * V'  —— 旋转到主轴坐标系
%   3. 双阈值裁剪：d_clip = max(d, max(min_eig, 1e-6*max(d)))
%      - 绝对阈值 min_eig（默认 1e-12）：处理精确为零的特征值
%      - 相对阈值 1e-6*max(d)：防止条件数过大（特征值跨度超 6 个数量级）
%   4. 重构矩阵：P_reg = V * diag(d_clip) * V'  —— 回到原始坐标系
%
% 【为什么用特征值裁剪而不是直接加噪？】
%   另一种常见做法是 P_reg = P + εI（Jittering），即在矩阵对角线上
%   加一个小常数。这种方法简单但粗暴：它会无差别地改变所有特征值，
%   包括本来健康的、较大的特征值。而特征值裁剪法只修改"坏"的特征值
%   （负值或过小值），保留了协方差矩阵的主体结构和主轴方向。
%
% 【输入参数】
%   P       — [NxN] 待正则化的协方差矩阵，可能不对称或非正定
%   min_eig — [可选] 绝对特征值最小阈值，默认 1e-12
%
% 【输出】
%   P_reg   — [NxN] 正则化后的协方差矩阵，保证严格对称正定
%
% 【调用位置】
%   - track_fusion_algorithms.m 中各融合算法的内部正则化
%   - time_align_tracks.m 中协方差传播后的正则化
%   - 其他需要对协方差做数值安全的模块
% =========================================================================

function P_reg = regularize_cov(P, min_eig)
    % ---------------------------------------------------------------
    % 参数校验：如果调用者未传入 min_eig，使用默认值 1e-12
    % 1e-12 是一个极小的正数，足以防止特征值为精确零导致的奇异问题，
    % 但又小到不会显著改变原本健康的协方差矩阵
    % ---------------------------------------------------------------
    if nargin < 2
        min_eig = 1e-12;
    end

    % ---------------------------------------------------------------
    % NaN/Inf 守卫：如果协方差矩阵中出现了 NaN 或 Inf，说明上游
    % 计算已经彻底崩溃（如除以零、overflow）。此时继续正则化没有意义，
    % 直接返回单位矩阵作为最保守的降级策略——相当于"放弃所有先验信息"，
    % 让后续滤波从零先验重新开始
    % ---------------------------------------------------------------
    if any(isnan(P(:))) || any(isinf(P(:)))
        P_reg = eye(size(P, 1));
        return;
    end

    % ===================================================================
    % 步骤1：强制对称化
    % ===================================================================
    % 理论上的协方差矩阵是对称的（Cov(X,Y) = Cov(Y,X)），但浮点运算
    % 中 P(i,j) 和 P(j,i) 可能相差 1e-15 量级的舍入误差。
    % 这里取 (P + P')/2 将矩阵强制变为对称矩阵：
    %   - 如果 P 本身已对称，此操作是恒等变换（结果不变）
    %   - 如果 P 有微小不对称，此操作将对称分量和保留，反对称分量消除
    % 这等价于在 Frobenius 范数意义下找到离 P 最近的对称矩阵
    % ===================================================================
    P_sym = (P + P') / 2.0;

    % ===================================================================
    % 步骤2：特征值分解 (Eigenvalue Decomposition)
    % ===================================================================
    % [V, D] = eig(P_sym) 将 P_sym 分解为：
    %   V — [NxN] 特征向量矩阵，每一列 V(:,i) 是一个特征向量（主轴方向）
    %   D — [NxN] 对角矩阵，对角线元素 D(i,i) 是对应的特征值 λ_i
    %
    % 几何直观：协方差矩阵描述了一个 N 维的"不确定椭球"：
    %   - 特征向量 V 的每一列 = 椭球的一个主轴方向
    %   - 特征值 D 的对角元 = 对应主轴方向的方差（半轴长度的平方）
    %   - 特征值越大 → 该方向的不确定性越高 → 椭球在该方向越"扁长"
    %   - 特征值为零 → 该方向完全确定（方差为零），这通常不可靠
    %   - 特征值为负 → 物理上无意义（"负的方差"不存在），必定是数值误差
    %
    % 特征值分解将矩阵从"原始坐标系"旋转到"主轴坐标系"，在主轴坐标系
    % 中协方差矩阵是对角的，各维度相互独立，便于单独处理每个特征值
    % ===================================================================
    [V, D] = eig(P_sym);

    % 从对角矩阵 D 中提取对角元素为列向量 d（N×1）
    % d(i) 就是第 i 个主轴方向的方差
    d = diag(D);

    % ===================================================================
    % 步骤3：计算裁剪阈值（双阈值策略）
    % ===================================================================
    % 为什么要用双阈值？因为协方差矩阵的量级在不同场景下差异巨大：
    %   - 滤波初期：P 可能很小（特征值 ~1e-3），此时 1e-12 足够
    %   - 长时间漏检：P 可能膨胀到 1e6 量级，此时 1e-12 远小于 P，
    %     不起任何作用，需要用相对阈值
    %
    % max_d = 当前协方差矩阵的最大特征值，代表"最不确定的方向"的方差
    % 用它作为尺度参考，计算相对阈值
    % ===================================================================
    max_d = max(d);

    % min_allowed = max(绝对阈值, 相对阈值)
    %   绝对阈值 min_eig = 1e-12：兜底线，任何特征值都不能低于此值
    %   相对阈值 1e-6 * max_d：防止条件数过大
    %     条件数 = max_d / min_allowed，此处条件数上限 = 1e6
    %     条件数过大意味着矩阵接近奇异，求逆时数值不稳定
    %     1e6 的条件数上限是工程实践中常用的安全阈值
    % ===================================================================
    min_allowed = max(min_eig, 1e-6 * max_d);

    % ===================================================================
    % 步骤4：裁剪特征值
    % ===================================================================
    % max(d, min_allowed) 是逐元素的逐点最大值操作：
    %   对于每个特征值 d(i)：
    %     - 如果 d(i) >= min_allowed → 保持原值不动（这个特征值是健康的）
    %     - 如果 d(i) <  min_allowed → 提升到 min_allowed（修复坏特征值）
    %
    % 这包括了三种需要修复的情况：
    %   (a) 负特征值：d(i) < 0，提升到 min_allowed > 0（修复物理矛盾）
    %   (b) 零特征值：d(i) = 0，提升到 min_allowed（防止奇异）
    %   (c) 过小特征值：0 < d(i) < min_allowed，提升到 min_allowed
    %       （防止条件数过大）
    %
    % 注意：这个操作只修改了"坏"的特征值，保留了健康特征值不变，
    % 因此正则化后的矩阵与原始矩阵的差异最小
    % ===================================================================
    d_clip = max(d, min_allowed);

    % ===================================================================
    % 步骤5：用裁剪后的特征值重构协方差矩阵
    % ===================================================================
    % P_reg = V * diag(d_clip) * V'
    %
    % 这一步将修正后的特征值"旋转"回原始坐标系：
    %   diag(d_clip)：将一维特征值向量恢复为对角矩阵形式
    %   V * (...) * V'：利用正交变换 V' 将主轴坐标系中的对角矩阵
    %                   旋转回原始坐标系
    %
    % 重构后的 P_reg 保证满足：
    %   - 对称性：(V*D*V')' = V*D'*V' = V*D*V'（D 是对角阵，D'=D）
    %   - 正定性：所有特征值 >= min_allowed > 0，因此对任意非零向量 x，
    %             x' * P_reg * x = x' * V * D_clip * V' * x > 0
    %   - 最小扰动：只对坏特征值做了修改，保留了原始矩阵的主轴方向
    %               和健康方差分量
    %
    % 至此，P_reg 是一个严格对称正定的协方差矩阵，可以安全地用于
    %   - UKF 的 Cholesky 分解（Sigma 点生成）
    %   - 融合算法的信息矩阵求逆
    %   - 任何需要正定矩阵的后续计算
    % ===================================================================
    P_reg = V * diag(d_clip) * V';
end
