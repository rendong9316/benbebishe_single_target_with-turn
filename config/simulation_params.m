% =========================================================================
% simulation_params.m — 双基地OTH-SWR单目标跟踪仿真参数配置
% =========================================================================
%
% 【功能概述】
%   本文件是双基地高频天波超视距雷达（OTH-SWR）单目标跟踪仿真的唯一
%   参数配置入口。所有仿真模块（雷达量测生成、UKF滤波、航迹管理、融合
%   评估等）的参数均在此集中定义，由主程序 run_simulation.m 调用后分发
%   至各子模块。
%
%   双基地雷达体制说明：
%     每个雷达站由分置的发射站（Tx）和接收站（Rx）组成。发射站向电离层
%     发射高频信号，经电离层折射后照射目标；目标散射回波经电离层再次折射
%     后到达接收站。这种"一跳"传播模式可实现数千公里的超视距探测。
%     由于 Tx 和 Rx 分置，距离量测对应的是"发射站→目标→接收站"的总路径
%     长度（双基地距离），而非目标到雷达的直线距离（斜距）。
%
% 【参数设计原则】
%   - 异质噪声：R1 为精度站（低噪声），R2 为标准站（约 2 倍噪声），用于
%     验证融合算法对不同精度等级传感器的适应能力
%   - 系统偏差（bias）：R1 和 R2 的偏差符号相反，模拟雷达标定误差，同时
%     为融合层面的偏差互校正提供条件
%   - 时间异步：R2 采样时刻比 R1 晚 13 秒，需在融合前进行时间对齐，模拟
%     实际多传感器网络中常见的非同步采样场景
%
% 【输出】
%   params - MATLAB 结构体，包含以下模块的全部参数：
%     ├── 模块1:  时间设定（采样周期、时长、偏移）
%     ├── 模块2:  站点几何（Tx/Rx 经纬度）
%     ├── 模块3:  威力覆盖（波束指向、宽度、距离范围）
%     ├── 模块4:  目标航迹（航路点、速度、运动模式）
%     ├── 模块5:  量测噪声（异质传感器的距离/方位/多普勒噪声）
%     ├── 模块6:  系统偏差（各雷达的距离/方位固定偏置）
%     ├── 模块7:  UKF 参数（UT 参数、过程噪声、初始协方差）
%     ├── 模块8:  航迹管理（M/N 起始逻辑、K_loss 终止逻辑、门限）
%     ├── 模块9:  检测/虚警模型（P_d、P_fa、分辨单元数）
%     ├── 模块10: PDA 加权（门内检测概率、杂波密度）
%     ├── 模块11: 模糊自适应 Q（滑动窗口、缩放因子范围）
%     └── 模块12: 随机种子（保证仿真可重复性）
%
% 【调用关系】
%   被调用方: run_simulation.m（仿真主程序，唯一调用者）
%   参数使用者: radar_tracker, time_align_tracks, run_track_fusion,
%               stitch_tracks, radar_coverage_check 等全部子模块
%
% 【坐标系约定】
%   地理位置:  经纬度（度），WGS84 坐标系
%   局部平面:  以雷达站为原点的 ENU（东-北-天）坐标系，单位米
%   角度:      度（deg），0° = 正北，顺时针增加
%   距离:      米（m）或千米（km），视具体参数而定
% =========================================================================

function params = simulation_params()
    % =====================================================================
    % 模块1: 时间设定
    % =====================================================================
    % 说明：
    %   仿真时间轴以 ref_start_time 为参考零点（t=0）。雷达按各自的采样
    %   偏移量（time_offset）从不同时刻开始周期性采样，采样间隔均为
    %   dt_sec。R1 和 R2 的采样时刻不同步，R2 比 R1 晚 13 秒，因此每帧
    %   的量测时间标签不同，后续融合需做时间对齐（外推或内插到统一时刻）。

    % 采样周期（秒）
    % 物理含义：雷达天线旋转一周或电子扫描完成一个完整探测帧的时间间隔。
    % OTH-SWR 的典型重访周期为 10~60 秒，此处取 30 秒对应中速扫描雷达。
    params.dt_sec = 30.0;

    % 仿真总时长（秒）
    % 3600 秒 = 1 小时，共产生 3600/30 = 120 帧量测。足够覆盖目标从进入
    % 探测区域到离开的完整过程，同时计算量可控。
    params.duration_sec = 3600.0;

    % 仿真参考起始时刻（UTC 时间，datetime 对象）
    % 该时间仅用于标注仿真数据的时间戳，不影响滤波或融合的数学计算。
    % 与 ADS-B 标定数据文件的时间范围对应，便于事后对比真实轨迹。
    params.ref_start_time = datetime(2026, 4, 27, 9, 30, 0);

    % 雷达1 采样起始偏移（秒）
    % R1 从 t=0 开始采样，采样时刻序列: 0s, 30s, 60s, 90s, ...
    params.time_offset_radar1_sec = 0.0;

    % 雷达2 采样起始偏移（秒）
    % R2 从 t=13 开始采样，采样时刻序列: 13s, 43s, 73s, 103s, ...
    % 选择 13 秒而非整数倍的半周期（15 秒），是为了避免与 R1 的采样时刻
    % 出现简单的倍数关系，使得时间对齐更接近真实场景中的随机异步情况。
    params.time_offset_radar2_sec = 13.0;

    % =====================================================================
    % 模块2: 站点几何位置（经纬度，单位：度，WGS84）
    % =====================================================================
    % 说明：
    %   双基地雷达的 Tx（发射站）和 Rx（接收站）分置。一般 Tx 位于内陆
    %   （远离电磁干扰），Rx 位于前沿（靠近探测区域以获得更好的接收信噪比）。
    %   本仿真中两个雷达的 Rx 相距约 2° 经度（~185 km），形成空间分集。
    %   每个雷达的 Tx-Rx 基线约 4° 经度（~370 km），属于典型的中等基线
    %   双基地配置。

    % --- 雷达1 接收站 ---
    % 经度 113.0°E，纬度 33.5°N（大致位于中国中部地区，如河南南部）
    params.radar1_lon = 113.0;   params.radar1_lat = 33.5;

    % --- 雷达2 接收站 ---
    % 经度 115.0°E，纬度 33.0°N（R1 东侧约 185 km，纬度略低 0.5°）
    params.radar2_lon = 115.0;   params.radar2_lat = 33.0;

    % --- 雷达1 发射站 ---
    % 经度 109.0°E，纬度 33.5°N（R1 Tx 位于 Rx 西侧约 4° ≈ 370 km）
    % Tx 与 Rx 同纬度，基线沿东西方向，便于双基地距离与地理距离之间的
    % 几何换算
    params.radar1_tx_lon = 109.0;  params.radar1_tx_lat = 33.5;

    % --- 雷达2 发射站 ---
    % 经度 111.0°E，纬度 33.0°N（R2 Tx 位于 Rx 西侧约 4° ≈ 370 km）
    params.radar2_tx_lon = 111.0;  params.radar2_tx_lat = 33.0;

    % =====================================================================
    % 模块3: 雷达威力覆盖参数
    % =====================================================================
    % 说明：
    %   OTH-SWR 的覆盖区域由三个因素共同决定：
    %   1. 距离范围 — 由电离层反射的传播模式决定
    %   2. 方位范围 — 由天线波束指向和波束宽度决定
    %   3. 仰角范围 — 由电离层高度和发射仰角决定（本仿真简化，不显式建模）
    %
    %   目标只有在同时满足距离和方位约束时才被视为"被雷达探测到"。

    % 波束中心方位角（度，0° = 正北，顺时针为正）
    % R1 波束指向 92°（正东略偏南），R2 波束指向 91°（正东略偏南）
    % 两雷达波束指向几乎相同，指向东部海域，形成重叠覆盖区
    params.radar1_beam_center_deg = 92.0;
    params.radar2_beam_center_deg = 91.0;

    % 波束宽度（度，3dB 半功率全宽度，即波束两侧增益下降到 -3dB 的角度间隔）
    % 15° 对应典型 OTH-SWR 接收阵列的波束宽度（约 16 元均匀线阵的自然波束宽度）
    params.beam_width_deg = 15.0;

    % 最小探测距离（km 和 m）
    % OTH-SWR 存在最小探测距离（"静区"），原因：
    %   - 近距离回波以地波/直达波为主，天波传播尚未形成
    %   - 电离层 F 层高度约 250~400 km，一跳模式的最小地面距离约 800~1200 km
    % 此处取 1000 km 为保守估计
    params.range_min_km = 1000.0;

    % 最大探测距离（km 和 m）
    % OTH-SWR 单跳最大传播距离受地球曲率和电离层高度限制，通常不超过
    % 3000~4000 km。取 2000 km 保证信号衰减在可接受范围内（信噪比足够）
    params.range_max_km = 2000.0;

    % 转换为米（内部计算统一使用 SI 单位）
    params.range_min_m = params.range_min_km * 1000;   % 1,000,000 m
    params.range_max_m = params.range_max_km * 1000;   % 2,000,000 m

    % =====================================================================
    % 模块4: 目标航迹参数
    % =====================================================================
    % 说明：
    %   仿真一个民航客机级别的空中目标，从西向东飞越雷达覆盖区。目标以
    %   恒定速度沿大圆航线（或近似直线）从航路点1飞往航路点2。本仿真
    %   仅支持 "straight" 模式（匀速直线），运动模型为近常速（NCV）。
    %
    %   运动方程（地理坐标下的直线插值）：
    %     lon(t) = lon1 + (lon2-lon1) * (v*t / total_distance)
    %     lat(t) = lat1 + (lat2-lat1) * (v*t / total_distance)
    %   其中 v = aircraft_speed_ms，total_distance 由两航路点的大地线
    %   距离确定。

    % 航路点数组: 每行 [经度(°), 纬度(°), 高度(m)，未使用]
    % 航路点1: (127.5°E, 31.0°N) — 东海海域，靠近日本冲绳以西
    % 航路点2: (130.5°E, 33.0°N) — 日本海/对马海峡方向
    % 目标总体向东北方向飞行，穿越两个雷达的共同覆盖区
    params.aircraft_waypoints = [127.5, 31.0, 0.0; 130.5, 33.0, 0.0];

    % 巡航速度（m/s）
    % 230 m/s = 828 km/h ≈ 447 kn（节），对应典型民航客机（B737/A320）
    % 在 10000m 巡航高度的经济巡航速度（Ma ≈ 0.78）
    params.aircraft_speed_ms = 230.0;

    % 航迹模式
    % "straight": 两点间匀速直线运动，无机动（无转弯、无加速）
    % 匀速直线是跟踪算法性能评估的基准场景，便于分离滤波误差和机动误差
    params.trajectory_mode = "straight";

    % =====================================================================
    % 模块5: 量测噪声参数（异质传感器）
    % =====================================================================
    % 说明：
    %   两个雷达的量测噪声水平不同（异质），模拟实际中不同精度等级的
    %   传感器网络。R1 为高精度站（参考站），R2 为标准站，其噪声标准差
    %   约为 R1 的 2 倍。
    %
    %   量测模型（极坐标）：
    %     z = [r, θ, ṙ]^T + w
    %     其中 r = 双基地距离（m），θ = 方位角（°），ṙ = 径向速度（m/s）
    %     噪声 w ~ N(0, R)，R = diag(σ_r², σ_θ², σ_ṙ²) 为对角协方差
    %
    %   量测噪声来源（物理机制）：
    %   - 距离噪声: 主要由电离层传播路径的时变延迟引起（多模传播、色散）
    %   - 方位噪声: 主要由接收阵列的波束指向误差和电离层侧向散射引起
    %   - 多普勒噪声: 主要由电离层运动（层高起伏）引入的附加多普勒频移

    % --- 雷达1（精度站）---
    % 距离噪声标准差 7 km: OTH-SWR 距离精度典型值 5~15 km，7 km 为较优水平
    params.radar1_range_noise_std_m = 7000.0;
    % 方位噪声标准差 0.35°: 对应约 16 元阵列的超分辨测角精度
    params.radar1_azimuth_noise_std_deg = 0.35;

    % --- 雷达2（标准站）---
    % 距离噪声标准差 14 km: 约为 R1 的 2 倍，代表常规性能的 OTH-SWR
    params.radar2_range_noise_std_m = 14000.0;
    % 方位噪声标准差 0.6°: 约为 R1 的 1.7 倍，对应较小孔径天线阵列
    params.radar2_azimuth_noise_std_deg = 0.6;

    % 径向速度量测噪声（m/s，两雷达共用）
    % 多普勒测量精度主要由相干处理间隔（CPI）和发射频率稳定性决定，
    % 两雷达采用相同的波形参数，因此多普勒噪声相近，统一取 0.5 m/s
    params.radial_vel_noise_std_ms = 0.5;

    % =====================================================================
    % 模块6: 系统偏差参数（模拟雷达标定误差）
    % =====================================================================
    % 说明：
    %   系统偏差（固定偏置）不同于随机噪声——它在每次量测中都是一个
    %   固定的加法常数，来源于雷达的系统性标定误差：
    %   - 距离偏置: 由电离层虚高估计误差、时基偏差等引起（km 级）
    %   - 方位偏置: 由天线阵列指向标定误差、电离层倾斜等引起（度级）
    %
    %   两雷达的偏差符号相反：
    %     R1: 距离 +20 km（测远），方位 -3.0°（偏西）
    %     R2: 距离 -15 km（测近），方位 +3.5°（偏东）
    %   这种配置使得在融合层面可以通过交叉校验检测并部分校正系统偏差，
    %   是评估分布式融合算法对系统误差鲁棒性的典型测试场景。
    %
    %   注意：如果不希望引入系统偏差（理想标定情况），将这些值设为 0 即可。

    % 雷达1 系统偏差
    params.radar1_range_bias_m = 20000.0;      % 距离正偏 +20 km
    params.radar1_azimuth_bias_deg = -3.0;     % 方位负偏 -3.0°（向西偏）

    % 雷达2 系统偏差
    params.radar2_range_bias_m = -15000.0;     % 距离负偏 -15 km
    params.radar2_azimuth_bias_deg = 3.5;      % 方位正偏 +3.5°（向东偏）

    % UKF 滤波器内部使用的量测噪声参数（默认值）
    % 实际运行时，各雷达的 UKF 会根据自身隶属的雷达站选用对应的噪声参数
    % （在 run_simulation.m 中根据雷达编号覆盖），此处仅提供初始默认值
    params.ukf_range_std_m = params.radar1_range_noise_std_m;
    params.ukf_azimuth_std_deg = params.radar1_azimuth_noise_std_deg;
    params.ukf_rv_std_ms = params.radial_vel_noise_std_ms;

    % =====================================================================
    % 模块7: UKF（无迹卡尔曼滤波）滤波器参数
    % =====================================================================
    % 说明：
    %   UKF 是处理非线性量测方程的标准方法，其核心思想是用一组确定性采样
    %   点（Sigma 点）近似状态分布，而非对非线性函数做一阶泰勒展开（EKF）。
    %   UKF 对 OTH-SWR 这种极坐标→直角坐标的非线性转换特别有效，精度
    %   可达二阶（EKF 仅一阶），且无需计算 Jacobian 矩阵。
    %
    %   UT（Unscented Transform）三参数：
    %     alpha: 控制 Sigma 点在均值附近的散布程度，小值使点更集中
    %     beta:  融入状态的先验分布知识（对高斯分布，beta=2 最优）
    %     kappa: 次级尺度参数，通常取 0 或 3-n（n 为状态维数）
    %
    %   复合尺度因子: lambda = alpha² * (n + kappa) - n
    %    本仿真 n=4（位置+速度，2D），lambda = 1e-6 * (4+0) - 4 ≈ -4
    %    负 lambda 可能导致协方差矩阵非正定，需在 Sigma 点生成时通过
    %    平方根分解和正则化处理。

    % UT 散布度参数 alpha
    % 取 1e-3 使 Sigma 点非常靠近均值，适用于状态维数较低（n=4）的情况。
    % 较小的 alpha 可避免 Sigma 点散布过远导致的"非局部效应"（Sigma 点
    % 落在非线性区域的另一端，引入错误的高阶信息）。
    params.ukf_alpha = 1e-3;

    % UT 先验参数 beta
    % beta = 2.0 对于高斯分布是最优选（可融入峰度信息），有助于提高协方差
    % 估计精度。该值来源于 Julier & Uhlmann 的原始论文推导。
    params.ukf_beta = 2.0;

    % UT 次级尺度参数 kappa
    % kappa = 0 为常用默认值，使 lambda = alpha² * n - n，Sigma 点分布
    % 仅由 alpha 控制。
    params.ukf_kappa = 0.0;

    % 过程噪声协方差缩放因子
    % 实际 Q 矩阵通过 process_noise_cv() 函数生成，形式为：
    %   Q = Q_scale * G * G^T
    % 其中 G 是近常速（NCV）模型的过程噪声增益矩阵：
    %   G = [dt²/2, 0; dt, 0; 0, dt²/2; 0, dt]
    % Q_scale 本质上是加速度过程噪声的功率谱密度（PSD），单位为 m²/s³。
    % 取值 2e4 对应 RMS 加速度约 sqrt(2e4/dt) ≈ 25.8 m/s²，对民航目标
    % 略显保守（偏大），用于覆盖未建模的轻微机动和电离层传播扰动。
    params.ukf_Q_scale = 2e4;

    % 初始状态协方差参数（地理坐标，度）
    % UKF 初始化时，位置和速度的初始不确定度：
    %   位置标准差 0.2° ≈ 22 km（经纬度方向等效），大于单次量测的噪声
    %     水平，以覆盖航迹起始阶段位置估计的不确定性
    %   速度标准差 0.003°/s ≈ 0.33 m/s（纬度方向），保守的速度不确定性，
    %     用于覆盖目标速度先验未知的情况
    params.ukf_P_pos_std = 0.2;     % 初始位置标准差（°）
    params.ukf_P_vel_std = 0.003;   % 初始速度标准差（°/s）

    % UKF 工作模式
    % "standard": 标准 UKF，使用对称 Sigma 点集（2n+1 个点）
    % 备选模式（扩展用）: "square_root" 平方根 UKF、"spherical" 球面 UKF
    params.ukf_mode = "standard";

    % =====================================================================
    % 模块8: 航迹管理参数（M/N 起始逻辑 + K_loss 终止逻辑）
    % =====================================================================
    % 说明：
    %   航迹管理负责新航迹的确认（起始）和旧航迹的删除（终止），是目标
    %   跟踪系统中连接"检测"和"跟踪"的关键环节。
    %
    %   M/N 起始逻辑（序贯检测）：
    %     在长度为 N 的滑动窗口中，若至少 M 次扫描存在关联量测（检测到
    %     目标），则确认航迹起始。这是一种序贯概率比检验（SPRT）的简化
    %     实现——在虚警起始概率和快速起始之间权衡。
    %
    %   K_loss 终止逻辑：
    %     连续 K_loss 次扫描未关联到量测，则终止（删除）航迹。避免在目标
    %     已离开探测区域后继续维持虚假航迹，浪费计算和通信资源。
    %
    %   关联门限：
    %     量测到航迹的关联采用椭球门控（ellipsoidal gating）：
    %       门控距离 d² = ν^T * S^{-1} * ν ≤ gate_sigma²
    %     其中 ν 为新息（量测-预测量测），S 为新息协方差。gate_sigma 决定
    %     椭球的大小：gate_sigma=2 时，在 2D 量测空间中约有 86.5% 的概率
    %     真实量测落在门内（卡方分布 CDF）。

    % M/N 航迹起始参数
    % M=4, N=8: 最近 8 次扫描中至少需要 4 次检测到目标才确认一条新航迹。
    % 等效起始概率阈值 = 4/8 = 50%，在快速起始（小 N）和低虚警（大 M/N 比）
    % 之间取折中。起始延迟约 4×30 = 120 秒（最坏情况）。
    params.tracker_M = 4;
    params.tracker_N = 8;

    % 航迹终止连续丢失帧数
    % K_loss = 15: 连续 15 帧（15×30s = 450s = 7.5分钟）未检测到目标
    % 则终止航迹。该时长足够覆盖因电离层短暂扰动（~1~2 分钟量级）导致的
    % 检测中断，但不会在目标已永久离开覆盖区后无限维持航迹。
    params.tracker_K_loss = 15;

    % 关联门限 Sigma 数
    % gate_sigma = 2.0: 新息的马氏距离（Mahalanobis distance）超过 2
    % 倍标准差的量测被排除在关联之外。对于 2 维量测（距离+方位），门内
    % 包含概率 = chi2cdf(4, 2) ≈ 86.5%。
    params.gate_sigma = 2.0;

    % =====================================================================
    % 模块9: 检测概率与虚警参数
    % =====================================================================
    % 说明：
    %   目标检测是一个二元假设检验问题：H0（仅噪声）vs H1（信号+噪声）。
    %   检测概率 P_d 和虚警率 P_fa 是描述检测性能的两个核心指标，受信噪
    %   比（SNR）和检测门限共同决定（通过 Neyman-Pearson 准则）。
    %
    %   OTH-SWR 的 P_d 通常远低于微波雷达（0.9+），原因：
    %   - 电离层传播损耗大且时变（衰落深度可达 10~20 dB）
    %   - 高频段外部噪声电平高（大气噪声、人为干扰）
    %   - 多模传播引起的信号衰落（不同模式的干涉）
    %
    %   虚警来源：
    %   - 电离层杂波（Es 层、赤道扩展 F 等不规则体回波）
    %   - 流星余迹回波（短暂但大量）
    %   - 同频段其他 OTH-SWR 的互干扰

    % 单帧检测概率 P_d
    % P_d = 0.6: 典型 OTH-SWR 在中等 SNR 下的检测概率。在 120 帧中约有
    % 72 帧产生检测（60%），其余 48 帧漏检。多雷达融合可以利用空间分集
    % 弥补单站的漏检。
    params.detection_probability = 0.6;

    % 单分辨单元虚警率 P_fa
    % P_fa = 0.001: 每千个距离-方位分辨单元平均产生 1 个虚警。在总共
    % n_resolution_cells = 150 个单元中，每帧期望虚警数约为 0.15 个。
    params.false_alarm_rate = 0.001;

    % 距离分辨率（km）
    % 由发射信号带宽决定: Δr = c/(2B)，10 km 分辨率对应约 15 kHz 带宽。
    % OTH-SWR 因电离层色散效应，实际分辨率通常劣于名义值。
    params.range_resolution_km = 10.0;

    % 方位分辨率（度）
    % 由接收阵列孔径和波束形成算法决定。1° 分辨率对应约 57 倍波长的
    % 有效孔径（常规波束形成），或更小孔径+超分辨算法。
    params.azimuth_resolution_deg = 1.0;

    % 总分辨单元数
    % 覆盖区的距离-方位二维分辨单元总数：
    %   距离方向: (2000-1000)/10 = 100 个单元
    %   方位方向: 15°/1° = 15 个单元
    %   总计 = 100×15 = 1500 个单元
    % 每帧期望虚警数 = n_resolution_cells × P_fa = 1500×0.001 = 1.5 个
    % 该值用于 PDA 模块的杂波密度计算和蒙特卡洛虚警生成。
    params.n_resolution_cells = ...
        ((params.range_max_km - params.range_min_km) / params.range_resolution_km) * ...
        (params.beam_width_deg / params.azimuth_resolution_deg);

    % =====================================================================
    % 模块10: PDA（概率数据关联）网格加权参数
    % =====================================================================
    % 说明：
    %   PDA（Probabilistic Data Association）处理关联模糊问题：当波门内
    %   存在多个候选量测时，不确定哪个量测来自真实目标，哪个是虚警。
    %
    %   PDA 的核心思想是不做硬关联（选择"最佳"量测），而是用所有门内
    %   量测的加权和作为等效量测，权重 = 该量测来自目标的概率。
    %
    %   网格加权是 PDA 的一种离散化实现：将距离-方位覆盖区划分为网格，
    %   根据每个网格单元的检测情况（有/无量测）计算关联概率，再按概率
    %   加权更新 UKF 状态。
    %
    %   PDA 的贝叶斯框架（参数化形式）：
    %     β_0 = 杂波后验概率（无量测来自目标的概率）
    %     β_i = 第 i 个量测来自目标的概率，正比于:
    %           N(ν_i; 0, S) * P_d / (λ * (1-P_d*P_G))
    %     其中 λ 为杂波空间密度，P_G 为门内概率。

    % 是否启用 PDA 网格加权
    % true: 使用 PDA 处理多量测→单航迹的关联
    % false: 使用最近邻（NN）硬关联（波门内取马氏距离最小的量测）
    params.use_pda_weighting = true;

    % 门内检测概率 P_G（Gate Probability）
    % 真实量测落在 gate_sigma=2 的椭球波门内的概率 ≈ 0.8647（2D 卡方）
    params.pda_pd_gate = 0.8647;

    % 杂波空间密度 λ（单位面积内的期望虚警数）
    % λ = 1.5 虚警 / (2000 km × 15°) = 1.5 / 30000 ≈ 5e-5 虚警/(km·°)
    % 用于计算 PDA 中"虚警量测数"的泊松分布先验 P(m_F = k) =
    %   Poisson(k; λ·V)，其中 V 为波门体积（距离×方位面积）。
    params.pda_clutter_intensity = 1.5 / (2000e3 * 15);

    % =====================================================================
    % 模块11: 模糊自适应过程噪声 Q
    % =====================================================================
    % 说明：
    %   标准 UKF 使用固定的过程噪声协方差 Q，无法应对目标机动（转弯、
    %   加速）导致的模型失配。模糊自适应 UKF 通过在线监测新息序列的
    %   统计特性来动态调整 Q：
    %
    %   核心思想：当目标平稳飞行时，Q 应当小（信任运动模型）；
    %             当目标机动时，Q 应当大（更多地依赖量测来纠正状态）。
    %
    %   自适应流程：
    %   1. 计算最近 W 帧的新息归一化平方和（Normalized Innovation
    %      Squared, NIS）的滑动均值
    %   2. 将 NIS 均值输入模糊推理系统（FIS），得到 Q 的缩放因子 μ
    %      - NIS 接近期望值（n_z 维量测 = 3）→ μ ≈ 1（维持）
    %      - NIS 显著大于期望值 → μ > 1（放大 Q，跟踪机动）
    %      - NIS 显著小于期望值 → μ < 1（缩小 Q，平稳滤波）
    %   3. Q_adaptive = μ * Q_nominal
    %
    %   模糊逻辑的优势：无需精确的机动模型（如 Singer 模型需要机动时间
    %   常数和加速度方差），通过启发式规则即可实现对各种机动的鲁棒响应。

    % 是否启用模糊自适应 Q
    % true: 启动模糊逻辑在线调整过程噪声，适应目标机动
    % false: 使用固定 Q（由 ukf_Q_scale 参数确定）
    params.use_fuzzy_adaptive = true;

    % 模糊逻辑滑动窗口大小（帧）
    % 用最近 8 帧的新息进行统计，8 帧对应 240 秒（4 分钟）。窗口太小则
    % 统计不稳定（方差大），窗口太大则对机动检测的响应延迟长。
    params.fuzzy_window_size = 8;

    % Q 调整因子范围
    % Q_min_factor = 0.6: 平稳（非机动）时 Q 最多可缩小到名义值的 60%，
    %   使滤波器更信任运动模型，获得更平滑的航迹
    % Q_max_factor = 1.5: 机动时 Q 最多可放大到名义值的 150%，使滤波器
    %   更信任新量测，快速跟上目标的运动变化
    % 范围 [0.6, 1.5] 较为保守（仅 ±50% 的调整范围），避免 Q 过度变化
    % 导致滤波发散
    params.fuzzy_Q_min_factor = 0.6;
    params.fuzzy_Q_max_factor = 1.5;

    % =====================================================================
    % 模块12: ADS-B 标定数据路径
    % =====================================================================
    % 说明：
    %   ADS-B（广播式自动相关监视）数据提供目标的真实经纬度轨迹，用作
    %   跟踪结果的"地面真值"（ground truth），用于事后性能评估：
    %   - 位置 RMSE（均方根误差）
    %   - 速度 RMSE
    %   - 航迹连续性、起始延迟、终止延迟等指标
    %
    %   CSV 文件格式：时间戳 (UTC), 经度 (°), 纬度 (°), 高度 (m), ...
    %   文件命名规则: 'yyyy-MM-dd HH-mm-ss.csv'（与 ref_start_time 对应）

    % ADS-B CSV 数据文件路径（相对于 MATLAB 当前工作目录）
    params.adsb_csv_path = '2026-04-27 09-30-00.csv';

    % =====================================================================
    % 模块13: 随机种子
    % =====================================================================
    % 说明：
    %   固定随机种子确保每次运行仿真产生完全相同的随机数序列，实现仿真
    %   的完全可重复性。这在算法调试、参数敏感性分析和性能对比中至关重要。
    %
    %   随机数用途（均使用 rng(seed) 初始化后的同一全局流）：
    %   - 量测噪声生成（randn: 高斯噪声）
    %   - 检测/漏检判定（rand: 均匀分布与 P_d 比较）
    %   - 虚警生成（rand: 泊松过程建模）
    %
    %   如果需要进行蒙特卡洛仿真（统计平均性能），可在 run_simulation.m
    %   中将 seed 设为 run_index（每次运行不同种子），此处定义的默认值
    %   仅用于单次确定性仿真。

    % 随机数生成器种子
    % 42 是一个经典的"计算机科学梗"（《银河系漫游指南》中"生命、宇宙
    % 及一切的答案"），没有特殊的数学意义，任何固定整数均可。
    params.random_seed = 42;
end
