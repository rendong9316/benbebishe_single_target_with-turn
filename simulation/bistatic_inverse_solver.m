% =========================================================================
% bistatic_inverse_solver.m — 天波双基地群距离反解经纬度
% =========================================================================
% 【功能】
%   从天波双基地雷达的量测值（群距离 Rg 和方位角 az）反推出目标的
%   经纬度坐标 (lon, lat) 和目标到接收站的地表大圆距离 r1。
%
%   天波超视距雷达（OTH-SWR）通过电离层反射传播，其群距离 Rg 不等于
%   简单的几何距离和，而是满足天波传播模型：
%     Rg = sqrt(D_tx^2 + 4H^2) + sqrt(D_rx^2 + 4H^2)
%   其中 H 为电离层反射高度（约 250-350 km），D_tx/D_rx 为地表大圆距离。
%
%   本函数采用"初值 + 迭代精化"的策略：
%     第1步：用经典双基地反解公式（假设 Rg = r0 + r1 为大圆距离和）给出 r1 初值
%     第2步：用天波模型预测 Rg_pred，按比例修正 r1，迭代至收敛
%     第3步：用最终的 r1 和方位角 az，通过球面正算得到目标经纬度
%
% 【输入参数】
%   Rg       — 标量，天波群距离（米），即 Rg = r_tx + r_rx
%   az       — 标量，接收站观测目标的方位角（度，0=正北，顺时针）
%   tx_lon   — 标量，发射站经度（度）
%   tx_lat   — 标量，发射站纬度（度）
%   rx_lon   — 标量，接收站经度（度）
%   rx_lat   — 标量，接收站纬度（度）
%
% 【输出】
%   r1       — 标量，目标到接收站的地表大圆距离（米）
%   lat      — 标量，推算的目标纬度（度）
%   lon      — 标量，推算的目标经度（度）
%
% 【数学推导——第1步初值】
%   设 baseline = 发射站与接收站的地表大圆距离
%   设 tx_az = 从接收站看发射站的方位角
%   设 phi = az - tx_az = 目标方位与基线方位的夹角
%   由余弦定理：
%     baseline^2 = r0^2 + r1^2 - 2*r0*r1*cos(phi)
%   结合 Rg = r0 + r1（经典假设），消去 r0 得：
%     r1 = 0.5 * (Rg^2 - baseline^2) / (Rg - baseline*cos(phi))
%
% 【数学推导——第2步迭代精化】
%   用当前 r1 通过球面正算得到目标经纬度
%   用天波模型计算 Rg_pred(tx, rx, target_lon, target_lat)
%   若 |Rg - Rg_pred| < 1m 则收敛
%   否则按比例修正：r1_new = r1_old * (Rg / Rg_pred)
%   物理意义：如果预测群距离偏小，说明 r1 估计过小，按比例放大
%
% =========================================================================
function [r1, lat, lon] = bistatic_inverse_solver(Rg, az, tx_lon, tx_lat, rx_lon, rx_lat)
    % ---------------------------------------------------------------
    % 第1步：经典双基地反解初值（假设 Rg = r0 + r1 为大圆距离和）
    % 这是 OTH-SWR 文献中的经典反解公式，忽略天波效应，将群距离
    % 近似等于地表大圆距离之和。虽然不精确，但提供了良好的初值。
    % ---------------------------------------------------------------

    % 计算发射站与接收站之间的地表大圆距离（基线长度）
    % 使用 Haversine 公式，结果单位为米
    baseline = sphere_utils_haversine_distance(tx_lon, tx_lat, rx_lon, rx_lat);

    % 计算从接收站看发射站的方位角
    % 这个方位角是后续计算目标与基线夹角 phi 的参考方向
    tx_az = sphere_utils_azimuth(rx_lon, rx_lat, tx_lon, tx_lat);

    % phi = 目标方位角 - 发射站方位角
    % 即目标相对于基线的方位偏差角，用于余弦定理中的夹角
    phi = az - tx_az;

    % 经典双基地反解公式：
    % 由余弦定理 baseline^2 = r0^2 + r1^2 - 2*r0*r1*cos(phi)
    % 结合 Rg = r0 + r1 消去 r0 = Rg - r1，整理得：
    %   r1 = 0.5 * (Rg^2 - baseline^2) / (Rg - baseline*cos(phi))
    % 注意：此处的 cosd 表示角度制的余弦
    r1 = 0.5 * (Rg^2 - baseline^2) / (Rg - baseline * cosd(phi));

    % 将 r1 钳制到合理范围 [1km, 5000km]
    % 防止初值过小（目标太近）或过大（目标太远）导致后续迭代发散
    r1 = max(1e3, min(r1, 5e6));

    % ---------------------------------------------------------------
    % 第2步：迭代精化——用天波模型修正 r1
    % 天波群距离 Rg 不等于地表大圆距离和，因为电离层反射增加了
    % 传播路径。需要通过天波几何模型反复校正。
    % 最多迭代 30 次，收敛阈值 1 米。
    % ---------------------------------------------------------------
    for iter = 1:30
        % 用当前 r1 和方位角 az，从接收站球面正算得到目标经纬度
        % sphere_utils_destination_point: 给定起点、距离、方位角，求终点经纬度
        [tgt_lon, tgt_lat] = sphere_utils_destination_point(rx_lon, rx_lat, r1, az);

        % 用天波几何模型计算当前目标位置的预测群距离
        % skywave_geometry('group_range', ...) 根据电离层反射模型
        % 计算真实的群距离 Rg_pred = r_tx + r_rx（考虑电离层高度 H）
        Rg_pred = skywave_geometry('group_range', tx_lon, tx_lat, rx_lon, rx_lat, tgt_lon, tgt_lat);

        % 计算当前预测群距离与实测群距离的误差
        err = Rg - Rg_pred;

        % 如果误差小于 1 米，认为已收敛，退出迭代
        if abs(err) < 1.0
            break;
        end

        % 按比例修正 r1：
        % 如果 Rg_pred < Rg（预测群距离偏小），说明 r1 估计过小，放大
        % 如果 Rg_pred > Rg（预测群距离偏大），说明 r1 估计过大，缩小
        % 比例因子 Rg/Rg_pred 体现了天波效应带来的距离修正
        r1 = r1 * Rg / Rg_pred;

        % 再次钳制 r1 到合理范围，防止迭代过程中 r1 溢出
        r1 = max(1e3, min(r1, 5e6));
    end

    % ---------------------------------------------------------------
    % 第3步：用最终收敛的 r1 正算目标经纬度
    % 此时 r1 已经过天波模型精化，对应的目标位置最准确
    % ---------------------------------------------------------------
    % 从接收站出发，沿方位角 az 走距离 r1，得到目标经纬度
    [lon, lat] = sphere_utils_destination_point(rx_lon, rx_lat, r1, az);
end
