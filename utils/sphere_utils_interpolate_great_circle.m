% =========================================================================
% sphere_utils_interpolate_great_circle.m
% =========================================================================
% 功能概要：
%   大圆航线插值函数。
%   在球面两点之间的大圆航线上，按给定的比例 fraction 插值计算
%   中间点的经纬度坐标。
%
% 数学原理：
%   大圆航线是球面上两点之间的最短路径（测地线）。
%   在球面上，两点之间的大圆航线沿着通过球心的大圆（即球面上
%   半径最大的圆）延伸。
%
%   本函数采用"先求距离和方位角，再正向推算"的二步法：
%
%   第1步：利用 sphere_utils_haversine_distance 计算从起点到终点的
%          总大圆距离 dist（米）。
%   第2步：利用 sphere_utils_azimuth 计算从起点指向终点的初始方位角
%          az（度）。
%   第3步：计算插值点到起点的距离 travel_dist = dist × fraction（米）。
%   第4步：利用 sphere_utils_destination_point，以起点为基准，
%          沿方位角 az 行进 travel_dist 米，得到插值点的经纬度。
%
%   当 fraction = 0 时，返回起点坐标。
%   当 fraction = 1 时，返回终点坐标。
%   当 fraction = 0.5 时，返回大圆航线的中点坐标。
%
%   为什么这种方法正确？
%   大圆航线从起点到终点沿固定"大圆"延伸。sphere_utils_destination_point
%   也是沿大圆移动的——它从起点沿给定的初始方位角出发，
%   沿大圆航线行走指定的弧长，结果恰好落在同一大圆上。
%   因此，这种方法保证了插值点精确位于起点和终点之间的大圆航线上。
%
% 在项目中的角色：
%   本函数用于在仿真中生成目标航迹的中间采样点。
%   给定飞机的起点和终点（或相邻航路点），需要在时间步长 dt 内
%   计算飞机在每个采样时刻的位置。本函数配合 fraction 参数，
%   可以均匀地在大圆航线上散布采样点。
%   在"straight"轨迹模式中，飞机从第一个航路点沿大圆直飞最后一个
%   航路点，所有中间采样点都通过本函数生成。
%
% 输入：
%   lon1, lat1 - 大圆航线起点的经纬度（单位：度）
%   lon2, lat2 - 大圆航线终点的经纬度（单位：度）
%   fraction   - 插值比例（标量，取值范围 0 到 1）
%                0 = 起点，1 = 终点，0.5 = 中点
% 输出：
%   lon - 插值点的经度（单位：度）
%   lat - 插值点的纬度（单位：度）
% =========================================================================

function [lon, lat] = sphere_utils_interpolate_great_circle(lon1, lat1, lon2, lat2, fraction)
    % -----------------------------------------------------------------
    % 第1步：计算起点到终点的总大圆距离（米）
    % -----------------------------------------------------------------
    % 使用 Haversine 公式计算球面两点间的最短距离。
    % 返回值 dist 的单位为米。
    % -----------------------------------------------------------------
    dist = sphere_utils_haversine_distance(lon1, lat1, lon2, lat2);

    % -----------------------------------------------------------------
    % 第2步：计算从起点指向终点的初始方位角（度）
    % -----------------------------------------------------------------
    % 使用球面方位角公式计算大圆航线的初始方向。
    % 返回值 az 的单位为度，范围 [0, 360)，从北顺时针计量。
    % -----------------------------------------------------------------
    az = sphere_utils_azimuth(lon1, lat1, lon2, lat2);

    % -----------------------------------------------------------------
    % 第3步：计算从起点到插值点的实际距离（米）
    % -----------------------------------------------------------------
    % travel_dist = dist × fraction
    % 例如：dist = 1000 km, fraction = 0.3 → travel_dist = 300 km
    % 即插值点在距起点 300 km 处
    % -----------------------------------------------------------------
    travel_dist = dist * fraction;

    % -----------------------------------------------------------------
    % 第4步：从起点沿方位角 az 行进 travel_dist 米，得到插值点坐标
    % -----------------------------------------------------------------
    % 使用大圆目的地点公式（正算问题）计算目的地经纬度。
    % 输入：起点经度 lon1、纬度 lat1、距离 travel_dist、方位角 az
    % 输出：插值点的经度 lon 和纬度 lat（均为度）
    % -----------------------------------------------------------------
    [lon, lat] = sphere_utils_destination_point(lon1, lat1, travel_dist, az);
end