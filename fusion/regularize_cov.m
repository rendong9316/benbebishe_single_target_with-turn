% =========================================================================
% regularize_cov.m
% =========================================================================
% 功能概要：
%   协方差矩阵正则化函数。通过特征值分解 + 裁剪负/过小特征值，确保输出
%   的协方差矩阵对称正定（symmetric positive definite），防止因浮点数
%   舍入误差导致的数值退化。
%
% =========================================================================
% 【为什么需要协方差正则化？】
% =========================================================================
%
% 1. 协方差矩阵的理论约束
%    在卡尔曼滤波的理论框架中，协方差矩阵 P 必须是对称正定的：
%      - 对称（P = P'）：因为协方差 Cov(a,b) = Cov(b,a)
%      - 正定（x'Px > 0 对所有非零向量 x）：因为方差必为正值
%    任何理论上的卡尔曼递推都保证 P 满足此约束。
%
% 2. 数值计算中的退化
%    但在实际的浮点运算中，由于舍入误差的累积，P 可能逐渐偏离此约束：
%      (a) 不对称：P ≈ P' 但不完全相等（加减法舍入）
%      (b) 非正定：某些特征值变成负的或接近零（减法消去、除法截断）
%
%    这些退化在以下情况下尤其严重：
%      - 滤波器长时间运行后
%      - 连续多帧漏检（协方差持续膨胀，元素幅值差异变大）
%      - 卡尔曼增益计算中的矩阵除法（inv 或 / 操作放大舍入误差）
%      - 状态各分量量级差异大（如经度 ~100 度，速率 ~0.001 度/秒）
%
% 3. 非正定协方差会引发什么问题？
%    如果 P 非正定：
%      - Cholesky 分解（Sigma 点生成的关键步骤）会直接报错失败，
%        因为 chol() 要求输入矩阵严格正定
%      - 即便 Cholesky 侥幸成功，负的特征值意味着"负的方差"，
%        这在物理上无意义，会使得 Sigma 点散布错误
%      - 卡尔曼增益的计算可能产生无意义的修正量，导致滤波发散
%
% 4. 正则化的策略：特征值裁剪法
%
%    本函数采用的策略是：
%      (1) 强制对称化：P_sym = (P + P')/2
%          （取矩阵与其转置的平均值，消除了因舍入产生的不对称）
%      (2) 特征值分解：P_sym = V * D * V'，其中 D 的对角元是特征值
%          （将 P 旋转到其主轴坐标系，D 的对角元就是各主轴上的方差）
%      (3) 裁剪特征值：把过小或负的特征值替换为一个最小安全阈值
%          （在主轴坐标系中，方差不能为负也不能太小）
%      (4) 重构矩阵：P_reg = V * D_clipped * V'
%          （用裁剪后的特征值，在原始坐标系中重构一个正定的协方差）
%
%    这个方法的优点是：只修改了 P 的"坏"部分（过小的特征值），保留了
%    协方差矩阵的主体结构（主轴方向和较大的方差分量）。
%
% 5. 双阈值策略（绝对阈值 + 相对阈值）
%
%    绝对阈值 min_eig（默认 1e-12）：
%      用于处理那些数值上就是零的特征值（如滤波初始阶段，某些方向的
%      不确定性确实可能是零或接近零）。
%
%    相对阈值 1e-6 * max_d：
%      用于处理协方差矩阵整体膨胀时的情况。比如当 P 的所有特征值都
%      膨胀到 1e3 量级时，1e-12 的绝对阈值就太小了。相对阈值保证了
%      不会出现某些特征值比其他特征值小 6 个数量级以上（条件数过大）
%      的病态情况。
%
%    最终阈值 = max(绝对阈值, 相对阈值)
%    这种双阈值策略兼顾了小尺度和大尺度的数值安全。
%
% 输入：
%   P       — 待正则化的协方差矩阵（n×n），可能不对称或非正定
%   min_eig — 可选，绝对特征值最小阈值（默认 1e-12）
%
% 输出：
%   P_reg   — 正则化后的协方差矩阵（n×n），保证对称正定
%
% 调用位置：
%   - ukf_filter_update.m   : 每帧滤波更新后正则化 P
%   - scc_fusion.m           : 融合前正则化两个输入的 P
%   - bsc_fusion.m           : 融合前正则化
%   - ci_fusion.m            : 融合前正则化
% =========================================================================

function P_reg = regularize_cov(P, min_eig)
    % 如果没有传入第二个参数（特征值最小阈值），则使用默认值 1e-12
    if nargin < 2
        min_eig = 1e-12;             % 绝对阈值：防止特征值为精确零
    end

    % NaN守卫：若协方差已被NaN污染，返回保守的单位矩阵
    if any(isnan(P(:))) || any(isinf(P(:)))
        P_reg = eye(size(P, 1));
        return;
    end

    % =====================================================================
    % 步骤1：强制对称化
    % =====================================================================
    % (P + P')/2.0 将矩阵强制变为对称矩阵。
    % 如果 P 本身已经对称，此操作是恒等变换（无损）。
    % 如果 P 因舍入误差轻微不对称，此操作将非对称分量消除（损失极小）。
    P_sym = (P + P') / 2.0;

    % =====================================================================
    % 步骤2：特征值分解
    % =====================================================================
    % [V, D] = eig(P_sym) 返回：
    %   V — 特征向量矩阵（n×n），每列是一个特征向量（主轴方向）
    %   D — 特征值对角矩阵（n×n），对角元是各主轴对应的方差
    %
    % 特征值分解的几何解释：
    %   把协方差矩阵想象成一个 n 维的"不确定椭球"：
    %     V 的每一列是椭球的一个主轴方向
    %     D 的每个对角元是该主轴方向的半轴长度平方（方差）
    %   特征值越大 → 椭球在该方向越"扁长"（越不确定）
    %   特征值为零 → 椭球在该方向退化成了一个点（完全确定，但这不可靠）
    %   特征值为负 → 数学上无意义（"负的方差"在实际中不存在）
    [V, D] = eig(P_sym);

    % 提取对角元为列向量 d（n×1）
    % diag(D) 从对角矩阵 D 中提取对角元素
    d = diag(D);

    % =====================================================================
    % 步骤3：计算裁剪阈值（双阈值策略）
    % =====================================================================
    % max_d = 最大的特征值
    % 用于计算相对阈值：不允许其他特征值比最大特征值小超过 6 个数量级
    max_d = max(d);

    % min_allowed = max(绝对阈值, 相对阈值)
    %   绝对阈值 min_eig = 1e-12：防止特征值为零
    %   相对阈值 1e-6*max_d：防止特征值之间差距过大（条件数过高）
    min_allowed = max(min_eig, 1e-6 * max_d);

    % =====================================================================
    % 步骤4：裁剪特征值
    % =====================================================================
    % max(d, min_allowed) 逐元素比较：
    %   如果 d(i) >= min_allowed → 保持原值
    %   如果 d(i) <  min_allowed → 提升到 min_allowed
    % 注意：这包括负特征值（会被提升到正的 min_allowed）
    d_clip = max(d, min_allowed);

    % =====================================================================
    % 步骤5：用裁剪后的特征值重构协方差矩阵
    % =====================================================================
    % V * diag(d_clip) * V'
    %   diag(d_clip)：将裁剪后的特征值向量变回对角矩阵
    %   V * (...) * V'：在原始坐标系中重构矩阵（将主轴坐标系"旋转"回原始坐标系）
    %
    % 这个重构矩阵保证：
    %   - 对称：因为 (V*D*V')' = V*D*V'（D 是对角的）
    %   - 正定：所有特征值 >= min_allowed > 0
    %   - 与原始 P 的差异最小：只修改了有问题的特征值
    P_reg = V * diag(d_clip) * V';
end
