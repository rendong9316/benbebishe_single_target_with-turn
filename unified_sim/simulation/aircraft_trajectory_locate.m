% ========================================================================
% aircraft_trajectory_locate.m
% ========================================================================
%
% 【功能概述】
% 航迹时间段定位函数。给定一个时间 t（单位：秒），在飞机航迹的
% 各个航段（segments）中查找该时间点属于哪一个航段，并返回该
% 航段的索引以及在该航段内的时间偏移量。
%
% 【数学原理】
% 航迹由 n 个航路点（waypoints）定义了 (n-1) 个航段（segments）。
% 每个航段 i 有：
%   - t_start(i) : 该航段的起始绝对时间（秒），等于前 i-1 个航段
%                  时长的累加和
%   - dur(i)      : 该航段的持续时间（秒）
% 对于给定时间 t，本函数查找满足以下条件的航段索引 idx：
%   t_start(idx) <= t < t_start(idx) + dur(idx)
% 如果 t 超出最后一个航段，则将其限制在最后一个航段内。
%
% 【在项目流水线中的位置】
% 本函数是航迹插值的辅助函数，被 aircraft_trajectory_interpolate
% 调用。流水线中的位置：
%   航迹创建 → locate（时间定位）→ interpolate（位置插值）→ 量测仿真
%
% 【输入参数】
%   traj - 航迹结构体（由 aircraft_trajectory_create 创建），
%          包含以下关键字段：
%          .segments     : cell 数组，每个元素是一个航段结构体
%          .n_segments   : 航段总数
%          .duration_sec : 航迹总时长（秒）
%   t    - 标量双精度浮点数，需要定位的时间（单位：秒），
%          相对于航迹起点（即第一个航路点对应 t=0）
%
% 【返回值】
%   idx   - 整数，时间 t 所在的航段在 traj.segments 中的索引
%           （MATLAB 下标从 1 开始）
%   t_seg - 标量双精度浮点数，时间 t 在所属航段内的时间偏移量
%           （单位：秒），即 t_seg = t - segments{idx}.t_start
%
% 【使用方法】
%   [idx, t_seg] = aircraft_trajectory_locate(traj, 150.0);
%
% 【注意事项】
%   - 输入时间 t 会被自动限制在 [0, duration_sec] 范围内
%   - 如果 t 正好等于最后一个航段的结束时间，函数仍然返回
%     最后一个航段的索引
% ========================================================================

function [idx, t_seg] = aircraft_trajectory_locate(traj, t)
    % ----------------------------------------------------------------
    % aircraft_trajectory_locate - 将时间 t 映射到航迹的航段
    % ----------------------------------------------------------------
    % 该函数是航迹插值的前置步骤。航迹由多个首尾相连的航段组成，
    % 每个航段有其起始时间和持续时间。本函数负责回答"在时间 t，
    % 飞机处于哪个航段中"以及"在该航段中已经飞行了多久"。
    %
    % 算法：线性扫描法 —— 从第一个航段开始依次遍历，比较当前
    % 时间 t 是否落在该航段的时间区间内。由于航段数量通常较少
    % （一般不超过 10 个），线性扫描的效率完全足够，无需使用
    % 二分查找等复杂算法。
    %
    % 边界处理：t 会被钳制在 [0, duration_sec] 区间内，防止
    % 因浮点误差或调用方传入非法值导致的数组越界。
    % ----------------------------------------------------------------

    % ---- 时间钳制：将 t 限制在 [0, 总时长] 范围内 ----
    % max(0.0, ...): 确保 t 不小于 0（防止负数时间）
    % min(t, traj.duration_sec): 确保 t 不超过航迹总时长
    % 该操作防止因数值误差导致访问不存在的航段
    t = max(0.0, min(t, traj.duration_sec));

    % ---- 线性扫描：遍历所有航段，寻找 t 所属的航段 ----
    % 循环变量 idx 从 1 遍历到 n_segments（MATLAB 下标从 1 开始）
    for idx = 1:traj.n_segments
        % 取出当前第 idx 个航段的结构体
        % traj.segments{idx}: 使用花括号 {} 访问 cell 数组的元素
        seg = traj.segments{idx};

        % ---- 判断时间 t 是否属于当前航段 ----
        % 条件 1: t < seg.t_start + seg.dur
        %   即时间 t 在当前航段结束之前（含该航段内）
        % 条件 2: idx == traj.n_segments
        %   如果是最后一个航段，即使 t 等于结束时间也算作属于该段
        %   两个条件用逻辑或 || 连接：满足任一条件即认为找到
        if t < seg.t_start + seg.dur || idx == traj.n_segments
            % 计算在该航段内的时间偏移量
            % t_seg = 当前绝对时间 - 该航段的起始绝对时间
            % 例如：航段从 t=100s 开始，当前 t=130s，则 t_seg=30s
            t_seg = t - seg.t_start;

            % 找到后立即返回，不再继续查找
            return;
        end
    end

    % ---- 保险措施：如果循环未找到（理论上不会发生），默认返回最后一个航段 ----
    % 这种情况仅当 duration_sec=0 且 n_segments=0 时可能触发
    % 此时强制返回最后一个航段索引和 0 偏移量
    idx = traj.n_segments;
    t_seg = 0.0;
end
