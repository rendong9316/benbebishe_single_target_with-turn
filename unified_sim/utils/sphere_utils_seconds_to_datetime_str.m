% =========================================================================
% sphere_utils_seconds_to_datetime_str.m
% =========================================================================
% 功能概要：
%   秒数转日期时间字符串函数。
%   将仿真内部的相对时间（从参考时间起算的秒数）转换为人类可读的
%   日期时间格式字符串。用于在输出报告、图表标注和调试日志中
%   显示绝对时间。
%
% 数学原理：
%   本函数不涉及复杂的数学计算，仅进行时间格式转换。
%
%   仿真内部使用"相对时间"（单位：秒）来驱动所有时间相关计算。
%   t = 0 代表仿真开始时刻。但输出时往往需要显示绝对时间，
%   例如 "2001-09-11 08:15:30" 这样的字符串。
%
%   转换过程：
%     1. 相对秒数 + 参考绝对时间 = 绝对时间（datetime 类型）
%     2. 绝对时间 → 格式化字符串（yyyy-mm-dd HH:MM:SS）
%
%   在 MATLAB 中，seconds(secs) 将数值表达为"秒"的持续时间对象，
%   然后可以直接与 datetime 对象相加。
%
% 在项目中的角色：
%   主要用于在绘图中生成时间轴标签（如 X 轴显示时刻）、
%   在输出日志中标注事件发生的绝对时间。
%   由于仿真可能覆盖数小时的数据，以秒为单位的时间标签不够直观，
%   使用日期时间字符串可以直观地理解数据对应的真实时刻。
%
% 输入：
%   secs     - 相对于参考时间的秒数（标量，单位：秒）
%              例如 secs = 930 表示从参考时间起过了 15分30秒
%   ref_time - 参考时间（MATLAB datetime 类型）
%              通常是 params.ref_start_time
%              即仿真中 t=0 对应的绝对时间
% 输出：
%   time_str - 日期时间格式字符串（字符串类型）
%              格式为 'yyyy-mm-dd HH:MM:SS'
%              例如 '2001-09-11 08:15:30'
% =========================================================================

function time_str = sphere_utils_seconds_to_datetime_str(secs, ref_time)
    % -----------------------------------------------------------------
    % 第1步：将相对秒数加到参考时间上，得到绝对时间
    % -----------------------------------------------------------------
    % seconds(secs) 是 MATLAB 内置函数，将数值型秒数转换为
    % duration（持续时间）数据类型。
    % duration 数据类型可以直接与 datetime 类型相加，结果仍为 datetime。
    %
    % 例如：
    %   ref_time = 2001-09-11 08:00:00
    %   secs = 930（即 15分钟30秒 = 930秒）
    %   abs_time = 2001-09-11 08:15:30
    % -----------------------------------------------------------------
    abs_time = ref_time + seconds(secs);

    % -----------------------------------------------------------------
    % 第2步：将绝对时间格式化为指定的日期时间字符串
    % -----------------------------------------------------------------
    % datestr 是 MATLAB 内置函数，将 datetime（或 datenum）转换为字符串。
    %
    % 格式字符串 'yyyy-mm-dd HH:MM:SS' 的含义：
    %   yyyy = 四位年份，如 2001
    %   mm   = 两位月份，如 09（9月）
    %   dd   = 两位日期，如 11
    %   HH   = 两位小时（24小时制），如 08
    %   MM   = 两位分钟，如 15
    %   SS   = 两位秒数，如 30
    %
    % 最终输出示例：'2001-09-11 08:15:30'
    % -----------------------------------------------------------------
    time_str = datestr(abs_time, 'yyyy-mm-dd HH:MM:SS');
end