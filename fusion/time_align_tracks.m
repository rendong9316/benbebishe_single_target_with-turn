% =========================================================================
% time_align_tracks.m — 雷达航迹时间对齐（外推/回退至统一时间基准）
% =========================================================================
%
% 【功能概述】
%   将两雷达异步采样的航迹结果对齐到统一的时间网格上。
%   在仿真场景中，R1 采样于整分钟时刻 (0s/30s/60s/...)，R2 采样延迟
%   13 秒 (13s/43s/73s/...)。本函数以 R1 的时间网格为基准，将 R2 的
%   UKF 状态用匀速直线 (CV) 模型"回退"到 R1 的采样时刻。
%
% 【为什么要时间对齐？】
%   航迹融合的前提是两个传感器的估计指向"同一时刻"的目标状态。
%   如果 R1 给出 t=30s 的位置估计而 R2 给出 t=43s 的位置估计，
%   直接融合相当于把目标在 30s 和 43s 的两个不同位置混在一起算平均，
%   这会产生物理上无意义的结果（目标已经移动了 13 秒的距离）。
%   因此必须先将两者的估计拉到同一时刻。
%
% 【数学原理：CV 模型逆向预测】
%   已知 R2 在 t 时刻的 UKF 状态 x(t)，需要推断其在 t-Δt 时刻
%   的状态 x(t-Δt)，其中 Δt = time_offset_radar2_sec（13秒）。
%
%   CV（Constant Velocity，匀速直线）模型假设目标在短时间窗口内
%   速度恒定，状态转移方程为：
%
%     [lon_t]   [1  Δt  0   0 ] [lon_{t-Δt}]
%     [v_lon] = [0   1  0   0 ] [v_lon_{t-Δt}]
%     [lat_t]   [0   0  1  Δt ] [lat_{t-Δt}]
%     [v_lat]   [0   0  0   1 ] [v_lat_{t-Δt}]
%
%   写成矩阵形式：x_t = F(Δt) * x_{t-Δt}
%   因此逆向回退：x_{t-Δt} = F(-Δt) * x_t
%
%   协方差传播（逆向预测，不确定度增加）：
%     P(t-Δt) = F(-Δt) * P(t) * F(-Δt)' + Q(|Δt|)
%
%   其中 Q(|Δt|) = Q_base * (|Δt| / dt_sec) 为按比例缩放的过程噪声。
%   注意：回退操作不会"改善"精度——P 在传播后只会增大或保持不变，
%   这是因为我们丢失了 |Δt| 时间内的量测信息。
%
% 【输入参数】
%   trackSnapshots_R2 — [n_frames x 1] cell，R2 各帧的原始航迹快照
%                       每帧 snap 包含 trackList cell 数组
%   params            — 仿真参数结构体，所需字段：
%                       .time_offset_radar2_sec — R2 采样延迟（秒），默认 13
%                       .dt_sec                 — 仿真时间步长（秒），默认 30
%
% 【输出】
%   aligned_R2 — [n_frames x 1] cell，时间对齐后的 R2 航迹快照
%                结构与输入相同，但每个航迹的状态和协方差已回退 Δt
%                每个处理过的航迹增加字段 .time_aligned = true
%
% 【航迹类型过滤逻辑】
%   trk.type = 7 → 已终止航迹（无 UKF 状态，跳过处理）
%   trk.type = 6 → 临时/未起始航迹（无可靠 UKF 状态，跳过处理）
%   其他 type 值 → 正常航迹，执行时间对齐
%
% 【调用关系】
%   被上层仿真脚本调用，在 run_track_fusion 之前执行
%   内部调用: regularize_cov() 对传播后的协方差进行正则化
% =========================================================================

function aligned_R2 = time_align_tracks(trackSnapshots_R2, params)
    % ---------------------------------------------------------------
    % 获取 R2 相对于 R1 的采样延迟时间（秒）
    % 正值表示 R2 采样晚于 R1，因此需要将 R2 的状态回退到更早的时刻
    % 例如：dt_offset = 13 表示 R2 在 t=13s 采样，需要回退到 t=0s
    % ---------------------------------------------------------------
    dt_offset = params.time_offset_radar2_sec;  % 默认 13 秒

    n_frames = length(trackSnapshots_R2);
    aligned_R2 = cell(n_frames, 1);  % 预分配输出 cell 数组

    % =================================================================
    % 主循环：逐帧对齐 R2 航迹
    % 每一帧对应 R2 的一个采样时刻，将该帧中所有航迹的状态回退到
    % R1 的对应采样时刻
    % =================================================================
    for k = 1:n_frames
        snap = trackSnapshots_R2{k};
        aligned_snap = snap;  % 浅拷贝快照结构体，后续仅修改 trackList 中的元素

        % -------------------------------------------------------------
        % 遍历本帧中每条航迹，逐一执行时间对齐
        % trackList 是一个 cell 数组，每个元素是一条航迹的结构体
        % -------------------------------------------------------------
        for t = 1:length(snap.trackList)
            trk = snap.trackList{t};

            % ---------------------------------------------------------
            % 跳过不需要对齐的航迹类型
            % type=7: 已终止航迹（跟踪结束），没有有效的 UKF 状态
            % type=6: 临时/未起始航迹（尚未通过 M/N 起始确认），
            %         状态不可靠，不应参与时间对齐
            % ---------------------------------------------------------
            if trk.type == 7, continue; end
            if trk.type == 6, continue; end

            % ---------------------------------------------------------
            % 跳过 UKF 状态为空的航迹
            % 即使航迹类型不是 6 或 7，也可能因为各种原因（起始失败、
            % 数据损坏等）导致 ukf 字段为空或没有 x 状态向量。
            % 这些航迹无法执行时间对齐，直接跳过
            % ---------------------------------------------------------
            if isempty(trk.ukf) || ~isfield(trk.ukf, 'x') || isempty(trk.ukf.x)
                continue;
            end

            % ---------------------------------------------------------
            % 构造 CV 模型状态转移矩阵（Δt 为负，表示时间回退）
            %
            % 状态向量顺序：[lon; v_lon; lat; v_lat]
            %   lon/lat — 经纬度位置（单位：度）
            %   v_lon/v_lat — 经纬度方向速度（单位：度/秒）
            %
            % CV 模型的运动学方程：
            %   新位置 = 旧位置 + Δt * 速度
            %   速度保持不变（匀速直线假设）
            %
            % 矩阵 F 的物理含义：
            %   F(1,2) = dt：经度位移 = 经度速度 × 时间偏移
            %   F(3,4) = dt：纬度位移 = 纬度速度 × 时间偏移
            %   F(2,2) = 1：  经度速度不变
            %   F(4,4) = 1：  纬度速度不变
            % ---------------------------------------------------------
            dt = -dt_offset;  % 回退，故 Δt 取负值（从 t+13 回到 t）
            F = [1, dt, 0, 0;
                 0,  1, 0, 0;
                 0,  0, 1, dt;
                 0,  0, 0,  1];

            % ---------------------------------------------------------
            % 步骤1：状态回退
            % x(t-Δt) = F(Δt) * x(t)
            %
            % 利用 CV 模型将当前时刻的状态"倒推"到目标时刻。
            % 经度方向：lon_new = lon_old + (-dt_offset) * v_lon
            % 纬度方向：lat_new = lat_old + (-dt_offset) * v_lat
            % 速度方向：v_lon 和 v_lat 保持不变（CV 假设）
            %
            % 注意：这是一个确定性预测，不引入额外的状态噪声
            % ---------------------------------------------------------
            trk.ukf.x = F * trk.ukf.x;

            % ---------------------------------------------------------
            % 步骤2：协方差传播（逆向预测，不确定度增加）
            % P(t-Δt) = F * P(t) * F' + Q(|Δt|)
            %
            % 协方差传播分为两部分：
            %
            %   (a) F * P * F'：确定性传播
            %       将当前协方差矩阵通过状态转移矩阵 F 传播到目标时刻。
            %       这部分描述了状态转移过程中不确定度的几何变换。
            %       例如，位置的方差会随着 Δt 增大而增大（因为速度
            %       的不确定性被"放大"到了位置维度）。
            %
            %   (b) Q(|Δt|)：过程噪声
            %       由于 CV 模型是简化的（实际目标可能有加速度），
            %       模型本身存在不确定性。这部分用过程噪声协方差 Q
            %       来建模。
            %       缩放规则：Q(|Δt|) = Q_base * (|Δt| / dt_sec)
            %       回退时间越长，缺失的信息越多，不确定度增加越大。
            %       当 |Δt| = dt_sec 时，Q 等于一个完整时间步的噪声。
            %
            % 重要原则：回退操作绝不会"改善"精度。
            %   因为我们丢失了 |Δt| 时间内的量测信息，
            %   所以 P 在传播后只会增大或保持不变。
            %   这是信息单向损失的不可逆过程。
            % ---------------------------------------------------------
            dt_abs = abs(dt);
            Q_dt = trk.ukf.Q * (dt_abs / params.dt_sec);
            trk.ukf.P = F * trk.ukf.P * F' + Q_dt;

            % ---------------------------------------------------------
            % 协方差正则化
            %   协方差传播后可能因浮点误差出现轻微不对称或特征值退化，
            %   需要再次正则化以确保后续融合算法的安全性
            % ---------------------------------------------------------
            trk.ukf.P = regularize_cov(trk.ukf.P);

            % ---------------------------------------------------------
            % 更新经纬度字段
            %   状态回退后，ukf.x 中的位置已经改变，需要同步更新
            %   外层可直接读取的 lat/lon 字段，保持一致性
            % ---------------------------------------------------------
            trk.lat = trk.ukf.x(3);  % 纬度在状态向量第 3 个位置
            trk.lon = trk.ukf.x(1);  % 经度在状态向量第 1 个位置

            % ---------------------------------------------------------
            % 标记已执行时间对齐
            %   设置标志位，方便下游代码判断该航迹是否经过时间对齐
            %   也用于调试和日志追踪
            % ---------------------------------------------------------
            trk.time_aligned = true;

            % 将修改后的航迹写回对齐后的快照
            aligned_snap.trackList{t} = trk;
        end

        % 保存本帧对齐后的快照
        aligned_R2{k} = aligned_snap;
    end
end
