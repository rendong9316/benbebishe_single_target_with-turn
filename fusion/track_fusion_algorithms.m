% =========================================================================
% track_fusion_algorithms.m — 航迹融合算法合集
% =========================================================================
%
% 【功能概述】
%   本文件集中实现四种经典的双传感器单帧航迹融合算法：
%     - fuse_scc: 简单凸组合（SCC / Simple Convex Combination）
%     - fuse_bc:  Bar-Shalom-Campo 融合（BC, 维护互协方差 P12）
%     - fuse_ci:  协方差交叉（CI / Covariance Intersection, 优化 w）
%     - fuse_fci: 快速协方差交叉（FCI / Fast CI, 闭式解 w）
%
%   通过统一的调度函数 track_fusion_algorithms(method, ...) 调用，
%   各子算法作为局部函数实现，保持代码集中管理和复用。
%
% 【四种算法的适用场景对比】
%
%   | 算法 | 需要 P12? | 独立性假设 | 保守性 | 计算量 | 适用场景 |
%   |------|----------|-----------|--------|--------|---------|
%   | SCC  | 否       | 是        | 无     | 最低   | 传感器完全独立、实时性要求极高 |
%   | BC   | 是       | 否        | 中等   | 高     | 已知互协方差、追求最优融合 |
%   | CI   | 否       | 否        | 高     | 中高   | 互相关性未知、需要保守保证 |
%   | FCI  | 否       | 否        | 高     | 低     | 实时系统、CI 的加速近似 |
%
% 【调度函数用法】
%   [x_fused, P_fused] = track_fusion_algorithms('SCC', x1, P1, x2, P2);
%   [x_fused, P_fused] = track_fusion_algorithms('BC',  x1, P1, x2, P2, P12);
%   [x_fused, P_fused, w] = track_fusion_algorithms('CI', x1, P1, x2, P2);
%   [x_fused, P_fused, w] = track_fusion_algorithms('FCI', x1, P1, x2, P2);
%
% 【输入参数】（调度函数）
%   method - 字符串，算法选择: 'SCC' / 'BC' / 'CI' / 'FCI'
%   x1     - [Nx1] 传感器1 的状态估计向量（N=4: [lon; v_lon; lat; v_lat]）
%   P1     - [NxN] 传感器1 的误差协方差矩阵
%   x2     - [Nx1] 传感器2 的状态估计向量
%   P2     - [NxN] 传感器2 的误差协方差矩阵
%   后续可选参数按各算法要求传入（如 BC 的 P12）
%
% 【输出】（调度函数）
%   x_fused  - [Nx1] 融合后的状态估计向量
%   P_fused  - [NxN] 融合后的误差协方差矩阵
%   varargout{1} - 标量，CI/FCI 的权重系数 w
%
% 【调用关系】
%   被 run_track_fusion.m 通过调度函数调用
%   内部调用 regularize_cov() 对协方差进行正则化
% =========================================================================

function [x_fused, P_fused, varargout] = track_fusion_algorithms(method, x1, P1, x2, P2, varargin)
    % 调度函数：根据 method 字符串分发到对应的融合算法实现
    % upper(method) 确保大小写不敏感（'scc' 和 'SCC' 都能正确识别）
    switch upper(method)
        case 'SCC'
            % 简单凸组合：假设独立，直接信息融合
            % 信息矩阵求和：P_fused^{-1} = P1^{-1} + P2^{-1}
            [x_fused, P_fused] = fuse_scc(x1, P1, x2, P2);

        case 'BC'
            % Bar-Shalom-Campo：利用互协方差 P12
            % 如果调用了 fuse_bc(x1,P1,x2,P2,P12)，varargin{1} 就是 P12
            % 如果没传 P12（fuse_bc(x1,P1,x2,P2)），则 P12=0（退化为独立假设）
            if ~isempty(varargin)
                [x_fused, P_fused] = fuse_bc(x1, P1, x2, P2, varargin{1});
            else
                [x_fused, P_fused] = fuse_bc(x1, P1, x2, P2);
            end

        case 'CI'
            % 协方差交叉：通过 fminbnd 优化 w 最小化 det(P_fused)
            % 返回三个输出：融合状态、融合协方差、最优权重
            [x_fused, P_fused, w] = fuse_ci(x1, P1, x2, P2);
            varargout{1} = w;

        case 'FCI'
            % 快速协方差交叉：用迹的倒数之比解析计算 w
            % 返回三个输出：融合状态、融合协方差、FCI 权重
            [x_fused, P_fused, w] = fuse_fci(x1, P1, x2, P2);
            varargout{1} = w;

        otherwise
            error('Unknown fusion method: %s', method);
    end
end

% =========================================================================
% fuse_scc — Simple Convex Combination (SCC) 单帧融合算法
% =========================================================================
%
% 【功能概述】
%   实现简单凸组合（Simple Convex Combination / Naive Fusion）融合算法。
%   SCC 假设两个传感器的估计误差完全独立（互协方差为零），直接将两
%   个信息矩阵相加得到融合结果。这是最简单、计算量最小的融合策略。
%
% 【数学原理详解】
%   SCC 的信息形式融合公式：
%
%     步骤1：融合信息矩阵（精度矩阵）
%       P_fused^{-1} = P1^{-1} + P2^{-1}
%
%       物理意义：信息矩阵（精度矩阵）是可加的。两个独立的测量提供
%       的"信息量"可以直接相加，融合后的精度必然不低于任一单独精度。
%       这与卡尔曼滤波中多步量测更新的数学性质一致。
%
%     步骤2：融合状态估计
%       x_fused = P_fused * (P1^{-1} * x1 + P2^{-1} * x2)
%                = P_fused * (y1 + y2)
%       其中 y_i = P_i^{-1} * x_i 称为"信息状态"（information state）。
%
%       展开看：x_fused = (P1^{-1} + P2^{-1})^{-1} * (P1^{-1}*x1 + P2^{-1}*x2)
%       这正是加权最小二乘解的形式，权重由各传感器的精度矩阵给出。
%       精度越高（P 越小）的传感器，其估计在融合中占的比重越大。
%
%       等价地，当 P1 = P2 时，x_fused = (x1 + x2) / 2，即等权平均。
%
%   SCC 的隐含假设：
%     1. 两传感器估计误差相互独立
%        E[(x1 - x_true)(x2 - x_true)'] = 0
%     2. 两个估计均是真实值的无偏估计
%        E[x1] = E[x2] = x_true
%
%   风险警告：当独立性假设不成立时（如两个传感器受到相同的干扰、
%   或共享部分量测信息），SCC 会给出过度乐观（overconfident）的
%   融合协方差，即 P_fused 可能小于实际误差协方差。这意味着融合
%   后的系统"以为自己很准"，但实际上并没有那么准。
%
% 【与其他算法的关系】
%   - SCC 是 BC 在 P12=0 时的特例（BC 去掉互协方差就退化为 SCC）
%   - SCC 是 CI 在 w=0.5 时的直接信息融合特例（CI 的凸组合权重
%     退化为等权时就是 SCC）
%   - FCI 在 P1 和 P2 的迹相等时退化为 SCC（此时 tr1_inv = tr2_inv，
%     w_fci = 0.5）
%   - SCC 是最简单快速但可能不够保守的基准算法
%
% 【输入参数】
%   x1 - [Nx1] 传感器1 的状态估计向量
%   P1 - [NxN] 传感器1 的误差协方差矩阵
%   x2 - [Nx1] 传感器2 的状态估计向量
%   P2 - [NxN] 传感器2 的误差协方差矩阵
%
% 【输出】
%   x_fused - [Nx1] 融合后的状态估计向量
%   P_fused - [NxN] 融合后的误差协方差矩阵
% =========================================================================

function [x_fused, P_fused] = fuse_scc(x1, P1, x2, P2)
    % -----------------------------------------------------------------
    % 步骤1: 正则化两个输入协方差矩阵
    % 确保 P1 和 P2 是对称正定的，防止后续求逆时出现数值问题。
    % 如果输入协方差已经退化（如 Cholesky 分解失败），正则化会修复它们。
    % -----------------------------------------------------------------
    P1 = regularize_cov(P1);
    P2 = regularize_cov(P2);

    % -----------------------------------------------------------------
    % 步骤2: 独立假设下的信息矩阵加法
    %        P_fused^{-1} = P1^{-1} + P2^{-1}
    %
    % 先求逆得到信息矩阵（精度矩阵），然后相加。
    % 信息矩阵描述了该估计"有多确定"——协方差越小（不确定性越低），
    % 信息矩阵越大（精度越高）。两个独立估计的信息矩阵相加，
    % 融合后的精度必然高于任一单传感器。
    %
    % 然后对求和后的信息矩阵再求逆，得到融合协方差。
    % 两次求逆是必要的：先求逆得到信息域做加法，再加完求逆回到
    % 协方差域。
    % -----------------------------------------------------------------
    P_fused_inv = inv(P1) + inv(P2);
    P_fused = inv(P_fused_inv);

    % -----------------------------------------------------------------
    % 步骤3: 融合状态估计
    %        x_fused = P_fused * ( P1^{-1} * x1 + P2^{-1} * x2 )
    %
    % 这是加权最小二乘的形式。P1^{-1} * x1 是将状态估计转换到
    % 信息域（信息状态），两个信息状态相加后再乘以融合协方差，
    % 将结果投影回状态空间。
    %
    % 注意：这里使用反斜杠运算符 (P1 \ x1) 而不是显式求逆 (inv(P1)*x1)，
    %   因为反斜杠在数值计算上更稳定——它使用 LU/QR 分解求解线性方程组，
    %   而不是先求逆矩阵再做乘法。求逆矩阵会放大数值误差，特别是
    %   当矩阵接近奇异时。
    % -----------------------------------------------------------------
    x_fused = P_fused * (P1 \ x1 + P2 \ x2);

    % -----------------------------------------------------------------
    % 步骤4: 最终正则化，确保输出协方差矩阵的数值稳定性
    % 融合后的 P_fused 可能因浮点误差出现微小的非对称性或负特征值，
    % 再次正则化保证输出是严格对称正定的，可供下游使用
    % -----------------------------------------------------------------
    P_fused = regularize_cov(P_fused);
end

% =========================================================================
% fuse_bc — Bar-Shalom-Campo (BC) 单帧融合算法
% =========================================================================
%
% 【功能概述】
%   实现 Bar-Shalom-Campo 提出的考虑互协方差的线性最小方差融合。
%   与 SCC/CI 等算法不同，BC 融合显式地维护两个传感器估计之间的互协
%   方差矩阵 P12，从而实现理论上更精确的融合结果。适用于两个传感器对
%   同一目标的估计存在相关性的场景（如同一过程噪声驱动、公共先验等）。
%
% 【数学原理详解】
%   BC 融合公式（假设两个估计 x1, x2，协方差 P1, P2，互协方差 P12）：
%
%     步骤1：计算新息协方差 S
%       S = P1 + P2 - P12 - P12'
%
%       物理意义：如果两个估计完全独立 (P12=0)，S = P1 + P2，
%       即新息的协方差等于各自协方差之和。但如果存在正相关 (P12 > 0)，
%       新息 (x2 - x1) 的协方差会减小——因为两个估计朝同一方向漂移，
%       它们的差异反而变小了。
%
%     步骤2：计算 BC 融合增益 K_bc
%       K_bc = (P1 - P12) * inv(S)
%
%       这是 BC 方法的核心创新。标准卡尔曼增益是 K = P*H'*S^{-1}，
%       而 BC 增益用 (P1 - P12) 替代了 P*H'，体现了互协方差对
%       融合权重的影响。
%       - 如果 P12 = 0（独立），K_bc = P1 * S^{-1}，退化为标准形式
%       - 如果 P12 > 0（正相关），K_bc 减小，说明 R2 提供的
%         新信息减少了（因为它的估计已经部分包含了 R1 的信息）
%
%     步骤3：融合状态估计
%       x_fused = x1 + K_bc * (x2 - x1)
%
%       这是对 x1 的修正：修正量 = K_bc * 新息(x2 - x1)
%       新息越大（两个估计差异越大），修正量越大。
%       但 K_bc 越小（相关性越强），修正量越小——因为相关性强的
%       两个估计之间的差异可能只是巧合，不值得大幅修正。
%
%     步骤4：融合协方差
%       P_fused = P1 - K_bc * (P1 - P12')
%
%       融合后协方差 = 原始协方差 - 修正量
%       修正量 = K_bc * (P1 - P12')，体现了互协方差的影响。
%
%   互协方差 P12 的递推维护（在融合循环中由调用方 run_track_fusion 维护）：
%     预测步: P12_pred = F * P12 * F' + 0.5 * Q
%     更新步: P12_new ≈ alpha1 * alpha2 * P12_pred
%       其中 alpha_i 是第 i 个传感器的迹收缩比
%
%   两个传感器共享同一过程噪声源，因此互协方差中仅计入 Q 的 1/2
%   （各传感器单独的协方差 P1、P2 中计入完整的 Q）。
%
% 【输入参数】
%   x1  - [Nx1] 传感器1（参考源）的状态估计向量
%   P1  - [NxN] 传感器1 的误差协方差矩阵
%   x2  - [Nx1] 传感器2 的状态估计向量
%   P2  - [NxN] 传感器2 的误差协方差矩阵
%   P12 - [NxN] 互协方差矩阵（可选参数，默认为零矩阵）
%         首次融合或无互协方差信息时传入空矩阵 []
%
% 【输出】
%   x_fused - [Nx1] 融合后的状态估计向量
%   P_fused - [NxN] 融合后的误差协方差矩阵
%
% 【参考文献】
%   Bar-Shalom, Y. and Campo, L., "The Effect of the Common Process Noise
%   on the Two-Sensor Fused-Track Covariance", IEEE T-AES, 1986.
% =========================================================================

function [x_fused, P_fused] = fuse_bc(x1, P1, x2, P2, P12)
    % -----------------------------------------------------------------
    % 步骤1: 对两个输入协方差进行正则化
    % regularize_cov 将强制矩阵对称、正定、条件数可控，
    % 确保后续的求逆和矩阵运算数值稳定
    % -----------------------------------------------------------------
    P1 = regularize_cov(P1);
    P2 = regularize_cov(P2);

    % -----------------------------------------------------------------
    % 步骤2: 处理互协方差矩阵 P12
    % 如果调用者未传入 P12（nargin < 5）或传入空矩阵，则设为零矩阵。
    % 零互协方差等价于"假设两个传感器估计独立"，此时 BC 退化为 SCC。
    % 这是一种安全的降级策略：不知道相关性时，假设独立总比假设错误好
    % -----------------------------------------------------------------
    if nargin < 5 || isempty(P12)
        P12 = zeros(size(P1));
    end

    % -----------------------------------------------------------------
    % 步骤3: 计算新息协方差 S
    %        S = P1 + P2 - P12 - P12'
    %
    % 注意：P12' 是 P12 的转置。即使 P12 理论上应该对称（互协方差
    % 是对称矩阵），但数值误差可能导致轻微不对称，因此显式加上 P12'
    % 确保 S 是对称的。
    %
    % 正则化 S 确保它是正定的，这样下一步的右除运算 (/) 才能安全执行。
    % 如果 S 奇异，inv(S) 会返回 Inf 或 NaN
    % -----------------------------------------------------------------
    S = P1 + P2 - P12 - P12';
    S = regularize_cov(S);  % 正则化确保可逆

    % -----------------------------------------------------------------
    % 步骤4: 计算 BC 融合增益 K_bc = (P1 - P12) / S
    %        使用 MATLAB 的 / 运算符（右除），等价于 (P1-P12) * inv(S)
    %        但数值稳定性更好：/ 运算符内部使用 QR 或 LU 分解，
    %        避免了显式求逆矩阵带来的数值误差放大
    % -----------------------------------------------------------------
    K_bc = (P1 - P12) / S;

    % -----------------------------------------------------------------
    % 步骤5: 融合状态估计
    %        x_fused = x1 + K_bc * (x2 - x1)
    %
    %        x2 - x1 是"新息"（innovation），表示两个估计之间的差异。
    %        K_bc 是新息的增益矩阵，决定了如何根据这个差异来修正 x1。
    %        如果 K_bc = I（单位矩阵），则 x_fused = (x1+x2)/2，等权平均。
    %        如果 K_bc 较小，说明两个估计相关性较强，新息可信度较低，
    %        修正量也较小。
    % -----------------------------------------------------------------
    x_fused = x1 + K_bc * (x2 - x1);

    % -----------------------------------------------------------------
    % 步骤6: 融合协方差矩阵
    %        P_fused = P1 - K_bc * (P1 - P12')
    %
    %        注意这里是 P12' 而不是 P12。从数学推导来看：
    %        P_fused = P1 - K_bc * S * K_bc'
    %        代入 K_bc = (P1-P12)*S^{-1} 并化简可得上述形式。
    %        这是 BC 方法的一个经典结果，体现了互协方差对融合精度的
    %        影响。
    %
    %        最后对 P_fused 做一次正则化，确保输出的协方差矩阵
    %        严格对称正定
    % -----------------------------------------------------------------
    P_fused = P1 - K_bc * (P1 - P12');
    P_fused = regularize_cov(P_fused);  % 最终正则化
end

% =========================================================================
% fuse_ci — Covariance Intersection (CI) 单帧融合算法
% =========================================================================
%
% 【功能概述】
%   实现协方差交叉（Covariance Intersection）融合算法。CI 算法是一种
%   保守融合策略，不需要未知的互协方差信息，通过凸组合方式保证融合
%   协方差不小于真实误差——即不会过度自信（overconfident）。
%
%   CI 的核心优势：无论两个传感器估计之间的相关性如何（完全相关、
%   部分相关、还是独立），CI 都能给出一个"保守但正确"的融合结果。
%   这使得它特别适合多雷达、多传感器融合场景，其中互相关性往往未知。
%
% 【数学原理详解】
%   CI 的信息形式融合公式：
%
%     步骤1：信息矩阵的凸组合
%       P_fused^{-1} = w * P1^{-1} + (1-w) * P2^{-1}
%       其中 w ∈ [0,1] 是权重系数。
%
%       物理意义：在信息域（精度域）中做加权平均。
%       w=0 时完全信任 P2，w=1 时完全信任 P1，w=0.5 时等权。
%       凸组合保证了 P_fused^{-1} 仍然是正定的（正定矩阵的凸组合
%       仍是正定的），因此 P_fused 也是正定的。
%
%     步骤2：状态估计的对应组合
%       x_fused = P_fused * [w * P1^{-1} * x1 + (1-w) * P2^{-1} * x2]
%       即先用同样的权重 w 组合信息状态 (P_i^{-1} * x_i)，
%       再乘以融合协方差得到融合状态。
%
%     步骤3：权重优化
%       选择 w 最小化 det(P_fused)（融合协方差的行列式）。
%       det(P_fused) 是"不确定度的总体度量"——行列式越小，
%       椭球体积越小，融合越精确。
%
%       等价地，最小化 det(P_fused) = 最大化 det(P_fused^{-1})。
%       因为 det(P^{-1}) = 1/det(P)，两者单调相反。
%
%       优化方法：fminbnd 在一维区间 (0.01, 0.99) 上搜索最优 w。
%       选择开区间而不是 [0,1] 是为了避免 w=0 或 w=1 时
%       P_fused^{-1} 退化为单个传感器的信息矩阵，失去融合意义。
%
% 【输入参数】
%   x1 - [Nx1] 传感器1 的状态估计向量
%   P1 - [NxN] 传感器1 的误差协方差矩阵
%   x2 - [Nx1] 传感器2 的状态估计向量
%   P2 - [NxN] 传感器2 的误差协方差矩阵
%
% 【输出】
%   x_fused - [Nx1] 融合后的状态估计向量
%   P_fused - [NxN] 融合后的误差协方差矩阵
%   w_opt   - 标量，优化得到的最优权重系数 w
%
% 【参考文献】
%   Julier, S.J. and Uhlmann, J.K., "A Non-divergent Estimation Algorithm
%   in the Presence of Unknown Correlations", ACC, 1997.
% =========================================================================

function [x_fused, P_fused, w_opt] = fuse_ci(x1, P1, x2, P2)
    % -----------------------------------------------------------------
    % 步骤1: 对两个协方差矩阵进行正则化
    % 确保 P1 和 P2 对称正定，为后续求逆做准备
    % -----------------------------------------------------------------
    P1 = regularize_cov(P1);
    P2 = regularize_cov(P2);

    % -----------------------------------------------------------------
    % 步骤2: 预计算逆矩阵，避免优化循环中重复求逆
    %
    % CI 优化需要多次评估不同的 w 值（fminbnd 内部会调用目标函数
    % 数十次），每次都要计算 w*P1^{-1} + (1-w)*P2^{-1}。
    % 如果每次都先求逆再组合，计算量会很大。
    % 因此提前计算 P1_inv 和 P2_inv，优化循环中只需做矩阵加权和。
    %
    % 注意：这里仍然使用了 inv() 而不是反斜杠，因为我们需要的是
    % 完整的逆矩阵（用于后续的凸组合），而不是解线性方程组
    % -----------------------------------------------------------------
    P1_inv = inv(P1);
    P2_inv = inv(P2);

    % -----------------------------------------------------------------
    % 步骤3: 通过 fminbnd 优化 w，最小化 det(P_fused)
    %        w 的搜索区间设为 (0.01, 0.99)，避免数值退化
    %        TolX=1e-4 作为收敛精度（权重变化的容忍度）
    %
    %        fminbnd 是 MATLAB 的一维有界优化器，使用黄金分割搜索
    %        和抛物线插值相结合的方法，在给定区间内找到函数的最小值。
    %        它不需要导数信息，适用于目标函数连续但导数难以解析
    %        表达的场景。
    % -----------------------------------------------------------------
    % 构造优化目标函数句柄，将 P1_inv 和 P2_inv 作为参数绑定
    % ci_cost 函数接收 w，返回 det(P_fused)
    obj = @(w) ci_cost(w, P1_inv, P2_inv);

    % MATLAB 内置的一维有界优化器 fminbnd
    % Display='off' 关闭优化过程的终端输出
    % TolX=1e-4 控制收敛精度：当连续两次迭代的 w 变化小于 1e-4 时停止
    w_opt = fminbnd(obj, 0.01, 0.99, ...
                    optimset('Display', 'off', 'TolX', 1e-4));

    % -----------------------------------------------------------------
    % 步骤4: 用最优 w 执行 CI 信息形式融合
    %        先融合信息矩阵（加权求和），再求逆得到协方差
    %        P_fused^{-1} = w_opt * P1^{-1} + (1-w_opt) * P2^{-1}
    % -----------------------------------------------------------------
    P_fused_inv = w_opt * P1_inv + (1 - w_opt) * P2_inv;
    P_fused = inv(P_fused_inv);

    % -----------------------------------------------------------------
    % 步骤5: 融合状态估计
    %        x_fused = P_fused * [w_opt * P1^{-1} * x1 + (1-w_opt) * P2^{-1} * x2]
    %
    %        这等价于加权最小二乘解：
    %        x_fused = argmin_x [ w_opt * (x-x1)'*P1^{-1}*(x-x1)
    %                           + (1-w_opt) * (x-x2)'*P2^{-1}*(x-x2) ]
    %        即找到使加权误差最小的状态估计
    % -----------------------------------------------------------------
    x_fused = P_fused * (w_opt * P1_inv * x1 + (1 - w_opt) * P2_inv * x2);

    % -----------------------------------------------------------------
    % 步骤6: 最终协方差正则化
    % 融合后的 P_fused 可能因浮点误差出现微小退化，
    % 再次正则化确保输出严格对称正定
    % -----------------------------------------------------------------
    P_fused = regularize_cov(P_fused);
end

% =========================================================================
% ci_cost — CI 优化目标函数（局部函数，由 fuse_ci 内部调用）
% =========================================================================
% 计算给定 w 下的融合协方差行列式，行列式越小融合越紧凑（不确定度越低）。
%
% 为什么用 1/det(P_inv) 而不是直接求 det(P)？
%   因为 det(P_fused) = det(P_fused_inv^{-1}) = 1/det(P_fused_inv)。
%   直接计算 P_inv 的行列式比先求逆矩阵再算行列式更稳定、更高效。
%   P_inv 已经是凸组合形式（w*P1^{-1} + (1-w)*P2^{-1}），
%   不需要再做额外的矩阵求逆操作。
%
% 输入:
%   w     - 标量权重，w ∈ (0, 1)
%   P1_inv - 传感器1 协方差的逆（信息矩阵）
%   P2_inv - 传感器2 协方差的逆（信息矩阵）
%
% 输出:
%   cost  - 标量，即 det(P_fused)，值越小融合越优
% =========================================================================
function cost = ci_cost(w, P1_inv, P2_inv)
    % 信息矩阵的凸组合：P_fused^{-1} = w * P1^{-1} + (1-w) * P2^{-1}
    P_inv = w * P1_inv + (1 - w) * P2_inv;

    % 行列式反比：cost = 1/det(P_inv) = det(P_fused)
    % 优化的目标是使这个值最小，即让融合后的不确定度椭球体积最小
    cost = 1 / det(P_inv);
end

% =========================================================================
% fuse_fci — Fast Covariance Intersection (FCI) 单帧融合算法
% =========================================================================
%
% 【功能概述】
%   实现快速协方差交叉（Fast Covariance Intersection）融合算法。
%   FCI 是 CI 的简化版本，无需迭代优化 w，而是用两个协方差矩阵的
%   迹的倒数之比直接确定权重，计算效率远高于 CI。
%
%   CI 需要 fminbnd 迭代 20-50 次才能找到最优 w，每次迭代都要做
%   矩阵加权和和行列式计算。FCI 用闭式解析解一步到位，省去了迭代开销。
%
% 【数学原理详解】
%   FCI 权重公式（基于协方差迹的倒数）：
%
%     步骤1：计算迹的倒数
%       tr1_inv = 1 / trace(P1)
%       tr2_inv = 1 / trace(P2)
%
%       迹 trace(P) 是协方差矩阵对角元之和，等于所有特征值之和，
%       代表"总不确定度"。迹越小，估计越精确。
%       迹的倒数 tr_inv 就是"总精度"的度量。
%
%     步骤2：归一化权重
%       w_fci = tr1_inv / (tr1_inv + tr2_inv)
%
%       这相当于将两个传感器的"总精度"归一化为权重。
%       精度越高（迹越小 → tr_inv 越大）的传感器获得更高权重。
%       权重总是满足 w_fci ∈ (0, 1)，不需要边界约束。
%
%       举例：如果 P1 的迹是 P2 的一半（P1 更精确），
%       则 tr1_inv = 2 * tr2_inv，w_fci = 2/3 ≈ 0.67，
%       即 P1 获得约 67% 的权重。
%
%     步骤3：融合公式与 CI 完全相同
%       P_fused^{-1} = w * P1^{-1} + (1-w) * P2^{-1}
%       x_fused = P_fused * [w * P1^{-1} * x1 + (1-w) * P2^{-1} * x2]
%
% 【FCI 与 CI 的权衡】
%   CI 的权重 w_opt 是通过最小化 det(P_fused) 严格优化的，
%   但需要迭代搜索。FCI 的权重 w_fci 是用迹近似的，
%   一步解析计算。迹优化和行列式优化不完全等价（迹关注各维度的
%   平均不确定度，行列式关注不确定度椭球的总体体积），因此 FCI
%   的融合结果通常略逊于 CI，但计算速度快一个数量级。
%
% 【输入参数】
%   x1 - [Nx1] 传感器1 的状态估计向量
%   P1 - [NxN] 传感器1 的误差协方差矩阵
%   x2 - [Nx1] 传感器2 的状态估计向量
%   P2 - [NxN] 传感器2 的误差协方差矩阵
%
% 【输出】
%   x_fused - [Nx1] 融合后的状态估计向量
%   P_fused - [NxN] 融合后的误差协方差矩阵
%   w_fci   - 标量，FCI 确定的权重系数
%
% 【与 CI 的对比总结】
%   CI: 通过 fminbnd 迭代优化 w，每次融合需多次求逆，精度高但慢
%   FCI: 解析式计算 w，每次融合仅需求逆一次，速度快但非最优
%   在精度要求不极端的实时系统中，FCI 通常是更实用的选择
% =========================================================================

function [x_fused, P_fused, w_fci] = fuse_fci(x1, P1, x2, P2)
    % -----------------------------------------------------------------
    % 步骤1: 正则化协方差矩阵，确保 trace 计算的稳定性
    % 如果 P 有负特征值，trace 可能失真（负特征值会抵消正特征值），
    % 正则化保证 trace 反映的是真实的总不确定度
    % -----------------------------------------------------------------
    P1 = regularize_cov(P1);
    P2 = regularize_cov(P2);

    % -----------------------------------------------------------------
    % 步骤2: 解析式计算 FCI 权重
    %        精度更高的传感器（迹更小 → tr_inv 更大）获得更高权重
    %        权重总是满足 w_fci ∈ (0, 1)，不需要额外约束
    %
    %        权重计算的物理直觉：
    %        trace(P) = Σ λ_i = 不确定椭球的"总体体积"度量
    %        1/trace(P) = 总体精度的代理变量
    %        w_fci = P1 的精度 / (P1 的精度 + P2 的精度)
    %        这就是将两个传感器的精度按比例分配权重
    % -----------------------------------------------------------------
    tr1_inv = 1 / trace(P1);  % 传感器1 精度的代理度量（总不确定度的倒数）
    tr2_inv = 1 / trace(P2);  % 传感器2 精度的代理度量
    w_fci = tr1_inv / (tr1_inv + tr2_inv);  % 归一化权重（两精度之比）

    % -----------------------------------------------------------------
    % 步骤3: 预计算逆矩阵
    % 与 CI 相同，先求逆再用于后续的凸组合和状态融合
    % -----------------------------------------------------------------
    P1_inv = inv(P1);
    P2_inv = inv(P2);

    % -----------------------------------------------------------------
    % 步骤4: 信息矩阵的凸组合
    %        P_fused^{-1} = w_fci * P1^{-1} + (1-w_fci) * P2^{-1}
    %        与 CI 完全相同的融合公式，只是权重从优化值换成了解析值
    % -----------------------------------------------------------------
    P_fused_inv = w_fci * P1_inv + (1 - w_fci) * P2_inv;
    P_fused = inv(P_fused_inv);

    % -----------------------------------------------------------------
    % 步骤5: 融合状态估计
    %        x_fused = P_fused * [w_fci * P1^{-1} * x1 + (1-w_fci) * P2^{-1} * x2]
    %        同样与 CI 完全相同，只是权重来源不同
    % -----------------------------------------------------------------
    x_fused = P_fused * (w_fci * P1_inv * x1 + (1 - w_fci) * P2_inv * x2);

    % -----------------------------------------------------------------
    % 步骤6: 最终正则化
    % 确保输出的协方差矩阵严格对称正定
    % -----------------------------------------------------------------
    P_fused = regularize_cov(P_fused);
end
