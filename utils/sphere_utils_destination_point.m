% =========================================================================
% sphere_utils_destination_point.m
% =========================================================================
% 功能概要：
%   大圆目的地点计算函数（也称为"正算问题"或"第一大地主题问题"）。
%   已知起点的经纬度、移动距离和方位角，计算出目的地点的经纬度。
%   这个函数是 sphere_utils_azimuth 的逆运算。
%
% 数学原理 —— 大圆航行公式（由球面三角推导）：
%   给定起点 P1(lon1, lat1)、球面弧长 δ（由 distance/R 得出）
%   和初始方位角 α（从北顺时针计量），求终点 P2(lon2, lat2)：
%
%   终点纬度 lat2：
%       lat2 = asin( sin(lat1) * cos(δ)
%                  + cos(lat1) * sin(δ) * cos(α) )
%
%   终点经度 lon2：
%       lon2 = lon1 + atan2( sin(α) * sin(δ) * cos(lat1),
%                            cos(δ) - sin(lat1) * sin(lat2) )
%
%   推导说明：
%   这两个公式来自球面三角形的余弦定律和正弦定律。
%   将球面三角形 N-P1-P2（N=北极）的三个边和角代入球面三角公式：
%     - 边 NP1 = 90° - lat1（P1到北极的角距离）
%     - 边 NP2 = 90° - lat2（P2到北极的角距离）
%     - 边 P1P2 = δ（弧长对应的球心角）
%     - 角 NP1P2 = α（方位角）
%   利用球面余弦定律求得 lat2，再利用球面正弦-余弦定律求得 lon2。
%
%   数值注意事项：
%   1. 当 cos(lat1) = 0（即起点在极点，lat1 = ±90°）时，公式退化。
%      此时经度无定义，atan2 的参数中 (sin(α)*0, cos(δ)-sin(lat1)*sin(lat2))
%      可能出问题。但本仿真中起点不在极点，故安全。
%   2. 当 δ = 0（距离为0）时，lon2 = lon1，lat2 = lat1，符合预期。
%   3. 当 δ = 180°（半圈）时，atan2 参数的分子为 sin(α)*0*cos(lat1)=0，
%      分母可能也为0，导致公式不稳定。实际中半圈距离为 πR ≈ 20000 km，
%      远超本仿真的距离范围，因此不会遇到此问题。
%
% 在项目中的角色：
%   本函数是 sphere_utils_azimuth 的逆运算函数。
%   主要被以下模块使用：
%   1. 航迹生成：根据速度、时间间隔和飞行方向计算飞机的下一位置
%   2. 大圆插值：在两点之间的航线上按比例计算中间点位置
%   3. 虚假航迹生成：从随机起点和随机方向生成虚假航线
%
% 输入：
%   lon_start  - 起点的经度（单位：度，东经为正）
%   lat_start  - 起点的纬度（单位：度，北纬为正）
%   distance_m - 沿大圆移动的距离（单位：米）
%   az_deg     - 移动的方位角（单位：度，从北顺时针 0-360）
% 输出：
%   lon - 目的地点的经度（单位：度）
%   lat - 目的地点的纬度（单位：度）
% =========================================================================

function [lon, lat] = sphere_utils_destination_point(lon_start, lat_start, distance_m, az_deg)
    % -----------------------------------------------------------------
    % 第1步：获取地球半径并计算弧长（球心角 δ）
    % -----------------------------------------------------------------
    % R = 6371000.0 米（WGS84 平均球半径）
    % arc_len = distance_m / R = 弧度表示的球心角
    % 例如：100 km 的距离，arc_len = 100000 / 6371000 ≈ 0.0157 弧度 ≈ 0.9°
    % -----------------------------------------------------------------
    R = sphere_utils_get_earth_radius();  % 地球半径（米）
    arc_len = distance_m / R;              % 弧长 = 距离/半径（弧度）

    % -----------------------------------------------------------------
    % 第2步：将方位角和起点坐标从"度"转换为"弧度"
    % -----------------------------------------------------------------
    az_rad = deg2rad(az_deg);        % 方位角（弧度）
    lat1   = deg2rad(lat_start);     % 起点纬度（弧度）
    lon1   = deg2rad(lon_start);     % 起点经度（弧度）

    % -----------------------------------------------------------------
    % 第3步：计算目的地的纬度 lat2
    % -----------------------------------------------------------------
    % 公式：lat2 = asin( sin(lat1)*cos(arc_len) + cos(lat1)*sin(arc_len)*cos(az_rad) )
    %
    % 公式解读：
    %   第一项 sin(lat1)*cos(arc_len)：
    %     - 如果 arc_len=0（没有移动），则 cos(0)=1，lat2=lat1（位置不变）
    %     - 随着距离增加（arc_len 增大），cos(arc_len) 减小，此项贡献减小
    %
    %   第二项 cos(lat1)*sin(arc_len)*cos(az_rad)：
    %     - cos(az_rad) = 1 时（正北方向），此项最大，纬度增加最多
    %     - cos(az_rad) = -1 时（正南方向），此项最小（负值），纬度减少最多
    %     - cos(az_rad) = 0 时（正东/正西方向），此项为0，纬度不变
    %     - cos(lat1) 反映纬度越高经线越密集的事实
    %       （在极点附近沿东西方向走也会导致纬度变化）
    %
    % asin 返回值范围 [-π/2, π/2]，对应纬度 [-90°, 90°]，符合地理约束
    % -----------------------------------------------------------------
    lat2 = asin(sin(lat1) * cos(arc_len) + ...
                cos(lat1) * sin(arc_len) * cos(az_rad));

    % -----------------------------------------------------------------
    % 第4步：计算目的地的经度 lon2
    % -----------------------------------------------------------------
    % 公式：
    %   lon2 = lon1 + atan2( sin(az_rad) * sin(arc_len) * cos(lat1),
    %                         cos(arc_len) - sin(lat1) * sin(lat2) )
    %
    % 公式解读：
    %   分子 sin(az_rad) * sin(arc_len) * cos(lat1)：
    %     - sin(az_rad) = 1（正东）时此项正值最大，经度增加最多
    %     - sin(az_rad) = -1（正西）时此项负值，经度减少
    %     - sin(az_rad) = 0（正北/正南）时分子为0，经度不变
    %     - sin(arc_len) 随距离增加而增大
    %     - cos(lat1) 反映高纬度处经度变化效应被放大
    %       （因为高纬度处经线密集，相同的东向距离对应更大的经度变化）
    %
    %   分母 cos(arc_len) - sin(lat1) * sin(lat2)：
    %     - 当 arc_len → 0 时，cos(arc_len) → 1, sin(lat2) → sin(lat1)，
    %       分母 → 1 - sin^2(lat1) = cos^2(lat1)，公式有稳定极限
    %
    % atan2 自动处理正负号，返回值范围 (-π, π]
    % -----------------------------------------------------------------
    lon2 = lon1 + atan2(sin(az_rad) * sin(arc_len) * cos(lat1), ...
                        cos(arc_len) - sin(lat1) * sin(lat2));

    % -----------------------------------------------------------------
    % 第5步：将计算结果从弧度转换回度
    % -----------------------------------------------------------------
    lon = rad2deg(lon2);  % 经度：弧度 → 度
    lat = rad2deg(lat2);  % 纬度：弧度 → 度
end