% ========================================================================
% measurement_simulator_create.m
% ========================================================================
%
% 【功能概述】
% 量测仿真器创建函数。本函数负责创建一个"量测仿真器"结构体，
% 该结构体封装了雷达传感器参数、噪声参数、系统偏置和随机数
% 种子，供后续的 measurement_simulator_measure 函数使用，
% 以模拟真实雷达对目标的探测过程。
%
% 【数学原理】
% 量测仿真器的核心是"真实值 + 噪声 = 量测值"的模型：
%
%   量测斜距    = 真实斜距 + 系统偏置_range + N(0, σ_range²)
%   量测方位角  = 真实方位角 + 系统偏置_azimuth + N(0, σ_azimuth²)
%   量测径向速度 = 真实径向速度 + N(0, σ_radial_vel²)
%
% 其中：
%   - N(μ, σ²) 表示均值为 μ、方差为 σ² 的高斯（正态）分布噪声
%   - 系统偏置是常数偏移量，模拟雷达的系统性误差
%   - 各噪声分量相互独立（即噪声协方差矩阵为对角矩阵）
%
% 另外，本仿真器还模拟了雷达的"检测概率"（Detection Probability）：
%   如果检测概率 < 1.0，则每次探测有 (1-p) 的概率"漏检"，
%   即目标存在但雷达没有产生量测报告。
%
% 【在项目流水线中的位置】
% 本函数连接了传感器定义和量测执行两个环节：
%
%   radar_station_create（定义雷达站位置）
%         +
%   simulation_params（定义噪声参数）
%         ↓
%   measurement_simulator_create（创建仿真器）★本函数★
%         ↓
%   measurement_simulator_measure（执行单次量测）
%
% 【输入参数】
%   radar        - 雷达站结构体（由 radar_station_create 创建），
%                  包含 .lon（经度）和 .lat（纬度）
%   params       - 仿真参数结构体（由 simulation_params 创建），
%                  包含以下关键字段：
%                  .detection_probability    : 检测概率（0~1）
%                  .range_noise_std_m        : 距离噪声标准差（米）
%                  .azimuth_noise_std_deg    : 方位噪声标准差（度）
%                  .radial_vel_noise_std_ms  : 径向速度噪声标准差（m/s）
%                  .ref_start_time           : 参考起始时间（datetime）
%   rng_seed     - 整数，MATLAB 随机数生成器的种子值，
%                  用于保证仿真结果的可重复性
%   range_bias   - 标量双精度浮点数（可选，默认 0），
%                  距离系统偏置（米），模拟雷达的系统性测距误差
%   azimuth_bias - 标量双精度浮点数（可选，默认 0），
%                  方位系统偏置（度），模拟雷达的系统性测角误差
%   tx_lon       - 标量双精度浮点数，照射源（发射站）经度（度）
%   tx_lat       - 标量双精度浮点数，照射源（发射站）纬度（度）
%
% 【返回值】
%   sim - 结构体（struct），包含以下字段：
%     .radar         : 雷达站结构体（输入参数原样保存）
%     .tx_lon        : 照射源经度（度）
%     .tx_lat        : 照射源纬度（度）
%     .params        : 仿真参数结构体（输入参数原样保存）
%     .range_bias    : 距离系统偏置（米）
%     .azimuth_bias  : 方位系统偏置（度）
%     .rng_seed      : 随机数种子（整数，原样保存）
%     .rng_stream    : 与 rng_seed 相同的值（用于状态回溯）
%
% 【使用方法】
%   radar = radar_station_create('南阳', 112.50, 33.00);
%   params = simulation_params;  % 加载默认仿真参数
%   sim = measurement_simulator_create(radar, params, 42, 50, 0.5);
%   % 创建一个仿真器：距离偏置50m，方位偏置0.5°
%
% 【注意事项】
%   - rng_seed 的设置会立即生效（调用 rng(rng_seed)），
%     影响后续所有随机数生成
%   - 如果需要在同一次运行中创建多个仿真器（对应多个雷达），
%     应使用不同的 rng_seed 值以避免随机数序列冲突
%   - range_bias 和 azimuth_bias 模拟的是系统误差（如天线校准误差），
%     与随机噪声不同，系统偏置对所有量测的影响相同
% ========================================================================

function sim = measurement_simulator_create(radar, params, rng_seed, ...
                                            range_bias, azimuth_bias, tx_lon, tx_lat)
    % ----------------------------------------------------------------
    % measurement_simulator_create - 创建单雷达量测仿真器
    % ----------------------------------------------------------------
    % 本函数将雷达定义、仿真参数和随机性控制封装到一个结构体中，
    % 形成完整的"量测仿真器"对象。调用 measurement_simulator_measure
    % 时只需要传递该结构体和目标状态即可完成一次仿真量测。
    %
    % 设计思想：
    %   将传感器物理特性（位置、噪声水平、检测概率）与
    %   仿真控制（随机种子、偏置）集中在仿真器中，
    %   使得量测函数 measurement_simulator_measure 保持简洁。
    % ----------------------------------------------------------------

    % ---- 保存雷达站信息 ----
    % 将雷达站结构体存入 sim.radar，后续量测计算需要用到雷达位置
    sim.radar = radar;

    % ---- 保存仿真参数 ----
    % 将仿真参数结构体存入 sim.params，包含了所有噪声标准差和检测概率
    sim.params = params;

    % ---- 处理可选输入参数：距离系统偏置 ----
    % nargin：MATLAB 内置变量，表示函数调用时实际传入的参数个数
    % if nargin < 4：如果调用者没有提供第4个参数（range_bias）
    %   则将其设为默认值 0.0（无系统偏置）
    if nargin < 4, range_bias = 0.0; end

    % ---- 处理可选输入参数：方位系统偏置 ----
    % if nargin < 5：如果调用者没有提供第5个参数（azimuth_bias）
    %   则将其设为默认值 0.0（无系统偏置）
    if nargin < 5, azimuth_bias = 0.0; end

    % ---- 保存系统偏置值 ----
    % 距离偏置（米）：正偏置会导致量测距离系统性地偏大
    sim.range_bias = range_bias;

    % 方位偏置（度）：正偏置会导致量测方位角系统性地偏大（顺时针方向）
    sim.azimuth_bias = azimuth_bias;

    % ---- 保存照射源（发射站）位置 ----
    sim.tx_lon = tx_lon;
    sim.tx_lat = tx_lat;

    % ---- 设置并保存随机数种子 ----
    % 保存种子值到结构体字段，方便后续查阅
    sim.rng_seed = rng_seed;

    % rng(rng_seed)：立即设置 MATLAB 全局随机数生成器的状态
    % 这确保了从此刻开始，rand() 和 randn() 等函数产生的随机数序列
    % 完全由该种子决定，从而实现仿真的可重复性
    % 注意：这会改变全局随机状态，可能影响同一次运行中的其他模块
    rng(rng_seed);

    % 额外保存一份种子值（与 rng_seed 相同），
    % 用于在某些需要重置随机状态的场景中回溯
    sim.rng_stream = rng_seed;
end
