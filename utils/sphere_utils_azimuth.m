% =========================================================================
% sphere_utils_azimuth.m
% =========================================================================
% 功能概要：
%   球面方位角（大圆初始方位角）计算函数。
%   计算从地球表面一点（起点）到另一点（终点）的大圆航线的
%   初始方位角（initial bearing / forward azimuth）。
%
% 数学原理 —— 大圆初始方位角公式：
%   对于球面上的两点 P1(lon_from, lat_from) 和 P2(lon_to, lat_to)，
%   大圆初始方位角 α 由以下球面三角公式给出：
%
%       α = atan2( sin(Δlon) * cos(lat_to),
%                  cos(lat_from) * sin(lat_to)
%                  - sin(lat_from) * cos(lat_to) * cos(Δlon) )
%
%   其中 Δlon = lon_to - lon_from。
%
%   推导思路（来自球面余弦定律和球面正弦定律）：
%   在大圆航线所在的球面三角形中，将北极 N 作为第三点，
%   形成球面三角形 N-P1-P2。利用球面三角形的正弦/余弦定律
%   即可推导出方位角公式。
%
%   方位角定义：
%   - 0°   = 正北方向
%   - 90°  = 正东方向
%   - 180° = 正南方向
%   - 270° = 正西方向
%   (顺时针从北方向计量)
%
%   注意事项（大圆航线的特性）：
%   1. 此公式计算的是"初始"方位角，即从起点刚出发时的方向。
%      沿大圆航线飞行过程中，方位角会持续变化（除非沿赤道或经线飞行）。
%   2. 起点的反方位角（从终点回到起点的方位角）不等于初始方位角+180°，
%      因为球面上的大圆路径不是"直线"（在球面意义下）。
%   3. 当两点非常接近时（距离趋于0），方位角在数值上趋于不稳定。
%
% 在项目中的角色：
%   被航迹生成（计算飞机飞行方向）、量测生成（计算雷达测量方位角）、
%   大圆插值等模块调用。是雷达极坐标系与地理经纬度之间的桥梁函数。
%
% 输入：
%   lon_from - 起点的经度（单位：度，东经为正）
%   lat_from - 起点的纬度（单位：度，北纬为正）
%   lon_to   - 终点的经度（单位：度，东经为正）
%   lat_to   - 终点的纬度（单位：度，北纬为正）
% 输出：
%   az - 从起点指向终点的初始方位角（单位：度，范围 0 到 360，
%        从正北方向顺时针计量）
% =========================================================================

function az = sphere_utils_azimuth(lon_from, lat_from, lon_to, lat_to)
    % -----------------------------------------------------------------
    % 第1步：将经度差从"度"转换为"弧度"
    % Δlon = lon_to - lon_from
    % 正值表示终点在起点以东，负值表示终点在起点以西
    % -----------------------------------------------------------------
    dlon = deg2rad(lon_to - lon_from);

    % -----------------------------------------------------------------
    % 第2步：将起点和终点的纬度从"度"转换为"弧度"
    % 后续三角运算的参数必须为弧度
    % -----------------------------------------------------------------
    lat_from_rad = deg2rad(lat_from);  % 起点纬度（弧度）
    lat_to_rad   = deg2rad(lat_to);    % 终点纬度（弧度）

    % -----------------------------------------------------------------
    % 第3步：计算方位角的两个三角分量 y 和 x
    % -----------------------------------------------------------------
    % 方位角 α = atan2(y, x)，其中：
    %
    % y = sin(Δlon) * cos(lat_to)
    %   ← y 分量反映了东西方向的贡献：
    %     sin(Δlon) 反映经度差的方向（东正西负）
    %     cos(lat_to) 反映终点纬度对投影的修正
    %
    % x = cos(lat_from) * sin(lat_to) - sin(lat_from) * cos(lat_to) * cos(Δlon)
    %   ← x 分量反映了南北方向的贡献：
    %     第一项 cos(lat_from)*sin(lat_to) 是纬度的直接贡献
    %     第二项 sin(lat_from)*cos(lat_to)*cos(Δlon) 是经度差对南北方向的修正
    % -----------------------------------------------------------------
    y = sin(dlon) * cos(lat_to_rad);

    x = cos(lat_from_rad) * sin(lat_to_rad) - ...
        sin(lat_from_rad) * cos(lat_to_rad) * cos(dlon);
    % 注意：... 是 MATLAB 的行续接符，表示表达式在下一行继续

    % -----------------------------------------------------------------
    % 第4步：通过 atan2 计算弧度方位角，并转换回度
    % -----------------------------------------------------------------
    % atan2(y, x) 返回弧度值，范围 (-π, π]
    %   - atan2 的两个参数：y（纵坐标/南北分量），x（横坐标/东西分量）
    %   - atan2 自动根据 y 和 x 的符号确定正确象限
    %
    % rad2deg() 将弧度转换为度：度 = 弧度 × 180 / π
    % -----------------------------------------------------------------

    % -----------------------------------------------------------------
    % 第5步：使用 mod(..., 360) 将方位角归一化到 [0, 360) 范围
    % -----------------------------------------------------------------
    % mod(val, 360) 确保结果在 0 ≤ az < 360 范围内：
    %   - atan2 可能返回负值（如 -45° 表示北偏西45°）
    %   - mod(-45, 360) = 315°，即北偏西45° = 西北方向 315°
    %   - atan2 返回 90° 保持不变，是正东方向
    % -----------------------------------------------------------------
    az = mod(rad2deg(atan2(y, x)), 360.0);
end