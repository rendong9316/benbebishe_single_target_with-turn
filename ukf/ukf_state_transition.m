% =========================================================================
% ukf_state_transition.m
% =========================================================================
% 功能概要：
%   执行 CV（匀速）运动模型的状态转移，即 x_next = F(dt) * x。
%   接收当前状态向量 x 和时间步长 dt，返回预测的下一时刻状态 x_next。
%
%   与 ukf_F_matrix.m 的关系：
%     ukf_F_matrix.m      → 仅返回 F 矩阵本身（4×4），供协方差传播等需要
%                            单独使用 F 的场景
%     ukf_state_transition.m → 执行完整的 F*x 运算，直接返回下一时刻状态
%                             x_next（4×1），省去调用方手动乘矩阵
%
% =========================================================================
% 【状态转移在 UKF 中的角色】
% =========================================================================
%
% 1. 预测步中状态转移的二重角色
%
%   在 UKF 的预测步骤（ukf_predict_step.m）中，每个 Sigma 点 X(:,i) 都
%   被本函数传播 dt 秒：
%
%     X_pred(:, i) = ukf_state_transition(X(:, i), dt)
%
%   传播后的 Sigma 点集合 X_pred 随后被用于两个目的：
%     (a) 加权求和得到预测状态均值：x_pred = Σ Wm_i * X_pred_i
%         （这些传播点告诉我们"根据运动模型，目标最可能在哪个区域"）
%     (b) 加权散布得到预测状态协方差：P_pred = Q + Σ Wc_i * (X_pred_i-x_pred)*(.)^T
%         （这些传播点的散布程度告诉我们"预测的不确定性有多大"）
%
%   为什么要通过 Sigma 点来传播，而不是直接 F*x？
%     因为 UKF 的设计原理要求所有概率分布的描述都通过 Sigma 点的统计
%     来完成。即便 CV 模型本身是线性的（F*x 就够了），UKF 框架仍然
%     统一使用 Sigma 点 + 加权统计的流程。这样做的好处是：
%       - 框架统一：不需要区分"线性预测"和"非线性预测"
%       - 可扩展：如果将来换成非线性运动模型（如 CTRV），只需改本函数，
%         预测步的其他逻辑无需修改
%
% 2. CV 模型的数值特性
%
%   由于 CV 模型是线性的，F*x 的结果与 UT（无迹变换）传播后加权求均值
%   的结果完全等价（这是因为线性变换下 UT 不引入额外误差）。
%   但代码仍走 UT 流程，保持框架的通用性。
%
%   在 ukf_filter_predict_only.m 中（漏检时的纯预测），由于不需要保留
%   Sigma 点给更新步使用，所以直接调用本函数和 F 矩阵做简化线性预测，
%   节省了 Sigma 点生成和加权的计算开销。
%
% 3. 状态向量的分量含义回顾
%
%   x(1) = lon       ：目标经度（度），东经为正
%   x(2) = lon_dot   ：经度变化率（度/秒），向东为正
%   x(3) = lat       ：目标纬度（度），北纬为正
%   x(4) = lat_dot   ：纬度变化率（度/秒），向北为正
%
%   注意：这里的"速度"单位为度/秒，与米/秒的换算依赖于纬度：
%     东向速度(m/s) = lon_dot * (π/180) * R_EARTH * cos(lat)
%     北向速度(m/s) = lat_dot * (π/180) * R_EARTH
%   这个转换发生在量测模型（ukf_measurement_model.m）的径向速度计算中，
%   不在本函数处理。
%
% 输入：
%   x  — 当前状态向量，4×1 列向量 [lon; lon_dot; lat; lat_dot]
%   dt — 采样时间间隔（秒），状态向前传播的时间步长
%
% 输出：
%   x_next — 预测的下一时刻状态向量，4×1 列向量，各分量含义同输入
% =========================================================================

function x_next = ukf_state_transition(x, dt)
    % =====================================================================
    % 步骤1：构造 CV 模型状态转移矩阵 F(4×4)
    % =====================================================================
    % 此处与 ukf_F_matrix.m 的功能重复，是出于性能考虑的有意重复：
    %   - 本函数在 UKF 预测步中被调用的次数 = 每帧 Sigma 点数量 = 9 次
    %   - ukf_F_matrix.m 每帧被调用 1 次（协方差传播那里需要单独的 F）
    %   内联构造 F 避免了 9 次函数调用开销（MATLAB 中函数调用有固定开销）。
    %
    % F 矩阵的结构化说明：
    %   ┌──────────┬──────────┐
    %   │ 1    dt  │ 0    0   │  ← 经度组（lon, lon_dot）
    %   │ 0    1   │ 0    0   │
    %   ├──────────┼──────────┤
    %   │ 0    0   │ 1    dt  │  ← 纬度组（lat, lat_dot）
    %   │ 0    0   │ 0    1   │
    %   └──────────┴──────────┘
    %
    % 矩阵中 dt 的位置表明：速度分量（第2、4个状态）通过乘以 dt 来驱动
    % 位置分量（第1、3个状态）的变化。
    F = [1.0, dt,  0.0, 0.0; ...   % 经度更新: lon + lon_dot*dt
         0.0, 1.0, 0.0, 0.0; ...   % 经度速率: 保持不变（CV 核心）
         0.0, 0.0, 1.0, dt;  ...   % 纬度更新: lat + lat_dot*dt
         0.0, 0.0, 0.0, 1.0];      % 纬度速率: 保持不变（CV 核心）

    % =====================================================================
    % 步骤2：执行矩阵-向量乘法 x_next = F * x
    % =====================================================================
    % 4×4 矩阵 × 4×1 向量 = 4×1 向量
    % 展开计算（等价于）：
    %   x_next(1) = 1.0*x(1) + dt*x(2)         = 新经度
    %   x_next(2) =         1.0*x(2)            = 新经度速率
    %   x_next(3) =                   1.0*x(3) + dt*x(4) = 新纬度
    %   x_next(4) =                              1.0*x(4) = 新纬度速率
    x_next = F * x;
end
