% =========================================================================
% radar_coverage_check.m — 雷达威力覆盖判定
% =========================================================================
%
% 【功能概述】
%   判断给定目标是否位于指定雷达的地理探测范围内。检查两个条件：
%   1. 斜距条件：目标与接收站的距离在 [range_min_m, range_max_m] 内
%   2. 波束条件：目标相对于接收站的方位角落在波束中心 +/- 半波束宽度内
%
%   用于仿真中确定每个雷达在每帧是否能"看到"目标，从而驱动检测概率
%   模型和量测生成流程。
%
% 【数学原理】
%   距离计算: 使用球面 Haversine 公式计算地球表面两点间的大圆距离
%     r1 = 2 * R_earth * arcsin( sqrt( sin^2(Δlat/2)
%                                     + cos(lat1)*cos(lat2)*sin^2(Δlon/2) ) )
%   其中 R_earth 为地球半径（通常取 6371 km）
%
%   方位角计算: 从接收站指向目标的大圆初始方位角
%     az = atan2( sin(Δlon)*cos(lat2),
%                 cos(lat1)*sin(lat2) - sin(lat1)*cos(lat2)*cos(Δlon) )
%     结果 ∈ [0°, 360°)，0° 为正北，顺时针增加
%
%   角度差处理: 考虑方位角在 0°/360° 附近的跳跃（wrap-around）
%     若 |az - beam_center| > 180° 则取 360° - |az - beam_center|
%
% 【输入参数】
%   rx_lon      - 标量，接收站经度 (度)
%   rx_lat      - 标量，接收站纬度 (度)
%   tgt_lon     - 标量，目标经度 (度)
%   tgt_lat     - 标量，目标纬度 (度)
%   beam_center - 标量，波束中心方位角 (度，0=正北，顺时针)
%   params      - 仿真参数结构体，所需字段：
%                 .beam_width_deg  — 波束宽度 (度)
%                 .range_min_m     — 最小探测距离 (米)
%                 .range_max_m     — 最大探测距离 (米)
%
% 【输出】
%   in_coverage - logical 标量，true = 目标在威力覆盖内
%   r1          - 标量，接收站到目标的地表大圆距离 (米)
%   az          - 标量，接收站到目标的方位角 (度)
%
% 【调用关系】
%   被仿真主程序（run_simulation.m）在量测生成前调用
%   被检测概率模型调用，用于判断是否对目标产生检测
%
%   内部调用:
%     sphere_utils_haversine_distance() — 计算球面距离
%     sphere_utils_azimuth()            — 计算方位角
%
% 【使用注意事项】
%   - 所有角度单位为度（degree），不是弧度
%   - 方位角 0° = 正北，90° = 正东，180° = 正南，270° = 正西
%   - 距离为地表大圆距离，不包含电离层传播路径的额外弯曲
%   - 波束宽度为全宽度（双边），半侧覆盖角 = beam_width_deg/2
% =========================================================================

function [in_coverage, r1, az] = radar_coverage_check(rx_lon, rx_lat, tgt_lon, ...
        tgt_lat, beam_center, params)

    % -----------------------------------------------------------------
    % 步骤1: 计算接收站到目标的地表大圆距离
    %        使用 Haversine 公式，输入输出均为国际单位 (度 → 米)
    % -----------------------------------------------------------------
    r1 = sphere_utils_haversine_distance(rx_lon, rx_lat, tgt_lon, tgt_lat);

    % -----------------------------------------------------------------
    % 步骤2: 计算从接收站指向目标的方位角
    %        返回值为 [0°, 360°)，0° 为正北
    % -----------------------------------------------------------------
    az = sphere_utils_azimuth(rx_lon, rx_lat, tgt_lon, tgt_lat);

    % -----------------------------------------------------------------
    % 步骤3: 判断波束角度条件
    %        half_beam: 半波束宽度（单侧覆盖角）
    %        az_diff: 目标方位角与波束中心的偏差
    % -----------------------------------------------------------------
    half_beam = params.beam_width_deg / 2;
    az_diff = abs(az - beam_center);

    % 处理 0°/360° 附近的方位角跳跃
    % 例如: az=350°, center=10° → |350-10|=340° → 应修正为 20°
    if az_diff > 180
        az_diff = 360 - az_diff;
    end

    % -----------------------------------------------------------------
    % 步骤4: 综合判定覆盖条件
    %        (1) 距离在 [range_min, range_max] 内
    %        (2) 方位角偏差在 [-half_beam, +half_beam] 内
    %        两个条件必须同时满足
    % -----------------------------------------------------------------
    in_coverage = (r1 >= params.range_min_m) && ...
                  (r1 <= params.range_max_m) && ...
                  (az_diff <= half_beam);
end
