% =========================================================================
% ukf_filter_update.m
% =========================================================================
% 功能说明：
%   本函数是UKF滤波器最核心的函数，实现完整的预测-更新循环（实时滤波）。
%   每一帧收到雷达量测后，调用本函数执行一步完整的UKF递推，输出滤波后
%   的目标经纬度。这是整个UKF模块中逻辑最复杂、子步骤最多的函数。
%
%   UKF算法中的角色：
%     本函数串联了预测步和更新步，构成了UKF滤波的完整单次迭代：
%       预测步：Sigma点生成 → 状态转移传播 → 计算预测均值和协方差
%       更新步：Sigma点通过量测模型 → 计算预测量测 → 新息计算
%              → 卡尔曼增益 → 状态更新 → 协方差更新
%     最后对协方差矩阵进行正则化以保证数值稳定性。
%
%   完整的UKF滤波循环（本函数实现的内容）：
%     第1段（预测步）：
%       (a) 从后验 x_{k-1|k-1} 和 P_{k-1|k-1} 生成Sigma点 X_{k-1}^{(i)}
%       (b) 将每个Sigma点通过状态转移函数：X_k^{(i)} = f(X_{k-1}^{(i)})
%       (c) 加权求和得预测均值：x_{k|k-1} = Σ Wm_i * X_k^{(i)}
%       (d) 加权散布得预测协方差：
%           P_{k|k-1} = Q + Σ Wc_i * [X_k^{(i)}-x_{k|k-1}][X_k^{(i)}-x_{k|k-1}]^T
%
%     第2段（更新步）：
%       (e) 将传播后的Sigma点通过量测模型：Z_k^{(i)} = h(X_k^{(i)})
%       (f) 加权求和得预测测量：z_{k|k-1} = Σ Wm_i * Z_k^{(i)}
%       (g) 计算量测协方差：P_zz = R + Σ Wc_i * [Z_k^{(i)}-z_{k|k-1}][.]^T
%       (h) 计算互协方差：P_xz = Σ Wc_i * [X_k^{(i)}-x_{k|k-1}][Z_k^{(i)}-z_{k|k-1}]^T
%       (i) 计算卡尔曼增益：K = P_xz * inv(P_zz)  或  K = P_xz / P_zz
%       (j) 状态更新：x_{k|k} = x_{k|k-1} + K * (z_k - z_{k|k-1})
%       (k) 协方差更新：P_{k|k} = P_{k|k-1} - K * P_zz * K^T
%
%     第3段（后处理）：
%       (l) 协方差正则化：确保 P 对称正定，防止数值退化
%
%   输入参数：
%     ukf  : UKF滤波器结构体，包含所有滤波器状态和参数
%     meas : 量测结构体（struct），含以下字段：
%            - meas.range_meas     : 雷达测量的斜距（米）
%            - meas.azimuth_meas   : 雷达测量的方位角（度）
%            - meas.radial_vel_meas: 雷达测量的径向速度（米/秒）
%
%   输出参数：
%     lon  : 滤波后的目标经度（度）
%     lat  : 滤波后的目标纬度（度）
%     ukf  : 更新后的UKF结构体（含新的x和P）
%
%   注意事项：
%     - 方位角量测存在360°包裹问题（0°和359.9°实际上是相邻的），
%       新息计算时必须做包裹处理以避免错误的大角度跳跃
%     - 量测协方差 P_zz 有时会接近奇异（尤其在滤波器刚初始化后），
%       代码通过 try-catch 使用伪逆 (pinv) 作为备选方案
%     - 每个Sigma点都被分别用于状态传播和量测传播，保证了非线性变换
%       的精度，但计算量是 2n+1=9 次函数调用
% =========================================================================

function [lon, lat, ukf] = ukf_filter_update(ukf, meas)
    % =====================================================================
    % 前置检查：滤波器是否已初始化？
    % 如果尚未初始化（第一帧量测），则先调用初始化函数建立初始状态
    % =====================================================================
    if ~ukf.initialized
        % 滤波器未初始化，使用第一帧量测进行单点初始化
        ukf = ukf_filter_init(ukf, meas);

        % 初始化后直接返回状态向量的位置分量作为滤波输出
        lon = ukf.x(1);   % 状态向量的第1个元素是经度
        lat = ukf.x(3);   % 状态向量的第3个元素是纬度

        % 提前返回，不执行后续的预测-更新循环
        return;
    end

    % =====================================================================
    % 第1段：预测步骤（时间更新，先验估计）
    % 目标：从后验 p(x_{k-1}|z_{1:k-1}) 计算先验 p(x_k|z_{1:k-1})
    % =====================================================================

    % ---- 1a：调用 ukf_predict_step 执行完整预测 ----
    % 该函数内部完成：
    %   (1) 从 ukf.x, ukf.P 生成 2n+1 个Sigma点
    %   (2) 每个Sigma点通过CV状态转移函数传播 dt 秒
    %   (3) 加权求和得到预测均值 x_pred 和预测协方差 P_pred
    % 返回的参数：
    %   x_pred : 先验状态均值，4×1向量 [lon_pred; lonDot_pred; lat_pred; latDot_pred]
    %   P_pred : 先验协方差矩阵，4×4矩阵
    %   X_pred : 传播后的Sigma点矩阵，4×9，在后续更新步中会复用
    [x_pred, P_pred, X_pred, ukf] = ukf_predict_step(ukf);

    % =====================================================================
    % 第2段：更新步骤（量测更新，后验估计）
    % 目标：从先验 p(x_k|z_{1:k-1}) 利用量测 z_k 计算后验 p(x_k|z_{1:k})
    % =====================================================================

    % ---- 2a：将传播后的Sigma点通过量测模型 ----
    % 预分配预测量测矩阵 Z_pred：尺寸 ukf.m × (2*ukf.n+1) = 3×9
    % 每一列存储一个Sigma点经过量测模型得到的预测量测向量（3维）
    Z_pred = zeros(ukf.m, 2 * ukf.n + 1);

    % 对每一个传播后的Sigma点 X_pred(:,i)，调用非线性量测函数
    % 将其映射到量测空间，结果存入 Z_pred(:,i)
    for i = 1:(2 * ukf.n + 1)
        % ukf_measurement_model 内部：
        %   计算Haversine球面距离 → range
        %   计算球面方位角 → azimuth
        %   计算径向速度（速度投影） → radial_vel
        Z_pred(:, i) = ukf_measurement_model(ukf, X_pred(:, i));
    end

    % ---- 2b：计算预测量测均值 z_pred ----
    % 对所有Sigma点的预测量测进行加权平均
    % z_pred = Σ Wm_i * Z_pred_i
    % Z_pred 为 3×9，ukf.Wm 为 9×1，结果为 3×1 预测量测向量
    % 这是量测预测的中心值，即"如果没有量测噪声，预计会看到什么"
    z_pred = Z_pred * ukf.Wm;

    % ---- 2c：计算量测协方差矩阵 P_zz 和互协方差矩阵 P_xz ----
    % 量测协方差 P_zz (3×3)：
    %   P_zz = R + Σ Wc_i * (Z_pred_i - z_pred) * (Z_pred_i - z_pred)'
    %   其中 R 是量测噪声协方差（传感器本身的噪声水平）
    %   后半部分是Sigma点散布引入的不确定性

    % 初始化 P_zz 为量测噪声协方差 R（3×3矩阵）
    P_zz = ukf.R;

    % 初始化互协方差 P_xz 为零矩阵：尺寸 ukf.n × ukf.m = 4×3
    % P_xz 描述状态与量测之间的相关性
    %   P_xz = Σ Wc_i * (X_pred_i - x_pred) * (Z_pred_i - z_pred)'
    % 互协方差是计算卡尔曼增益的关键，它告诉滤波器"量测偏差中有多少
    % 可以归因于状态估计偏差"
    P_xz = zeros(ukf.n, ukf.m);

    % 对每个Sigma点累加协方差贡献
    for i = 1:(2 * ukf.n + 1)
        % dz_i: 第i个Sigma点的预测量测与预测量测均值的偏差（3×1向量）
        % 反映该Sigma点在量测空间中的散布
        dz = Z_pred(:, i) - z_pred;

        % dx_i: 第i个Sigma点与状态预测均值的偏差（4×1向量）
        % 反映该Sigma点在状态空间中的散布
        dx = X_pred(:, i) - x_pred;

        % 量测协方差累加：Wc_i * dz * dz'（3×3矩阵）
        % dz * dz' 是量测偏差的外积，代表该Sigma点对量测协方差的贡献
        P_zz = P_zz + ukf.Wc(i) * (dz * dz');

        % 互协方差累加：Wc_i * dx * dz'（4×3矩阵）
        % dx * dz' 是状态偏差与量测偏差的外积，描述两者的线性相关
        % 状态分量和量测分量变化方向一致 → 正值，相反 → 负值
        P_xz = P_xz + ukf.Wc(i) * (dx * dz');
    end

    % ---- 2d：获取实际量测向量 z_actual ----
    % 从雷达量测结构体中组装实际量测向量（3×1）
    % z_actual(1): 斜距量测值（米）
    % z_actual(2): 方位角量测值（度）
    % z_actual(3): 径向速度量测值（米/秒）
    z_actual = [meas.range_meas; meas.azimuth_meas; meas.radial_vel_meas];

    % ---- 2e：计算新息（Innovation）向量 ----
    % 新息 = 实际量测 - 预测量测
    % 物理含义：滤波器"预期"看到的值与"实际"看到的值之间的差异
    % 该差异被卡尔曼增益加权后用于修正状态估计
    innovation = z_actual - z_pred;

    % ---- 2f：方位角新息的包裹处理（Angle Wrapping） ----
    % 方位角具有360°周期性：0°和359.9°实际上只差0.1°
    % 如果不做包裹处理，算出的新息会是359.8°，导致滤波发散
    %
    % 例如：预测量测方位角=359°，实际量测方位角=1°，
    %       实际偏差应为 +2°（或 -358°），而非 naive 的 1-359=-358°
    %
    % 包裹策略：将新息约束到 [-180°, +180°] 区间内
    if innovation(2) > 180.0
        % 新息 > 180°：减去360°使其落入合法范围
        % 例如：350° 新息 → 350-360 = -10°（表示实际比预测偏西10°）
        innovation(2) = innovation(2) - 360.0;
    elseif innovation(2) < -180.0
        % 新息 < -180°：加上360°使其落入合法范围
        % 例如：-350° 新息 → -350+360 = +10°（表示实际比预测偏东10°）
        innovation(2) = innovation(2) + 360.0;
    end
    % 包裹处理后 innovation(2) ∈ [-180, +180]

    % ---- 2g：计算卡尔曼增益矩阵 K ----
    % 卡尔曼增益 K (4×3) 决定了量测信息对状态修正的"力度"和"方向"
    % 标准公式：K = P_xz * inv(P_zz)
    %   = P_xz / P_zz  （MATLAB中 A/B = A*inv(B)）
    %
    % 卡尔曼增益的直观含义：
    %   K 的每一行表示"单位量测偏差"应对状态各分量做多大修正
    %   - 量测噪声大（P_zz大）→ K 小 → 信任预测，不信任量测
    %   - 状态不确定性大（P_xz大）→ K 大 → 信任量测，大幅修正状态
    %   - 量测和状态相关性弱（P_xz小）→ K 小 → 量测信息不相关

    try
        % 首选路径：使用矩阵右除（等价于 P_xz * inv(P_zz)）
        % MATLAB 的 / 运算符使用LU分解等数值稳定方法求解线性方程组
        % K = P_xz / P_zz 实际上解的是 K * P_zz = P_xz
        K = P_xz / P_zz;
    catch
        % 备选路径（数值稳定性保障）：
        % 如果 P_zz 奇异或接近奇异，用伪逆 pinv 代替矩阵求逆
        % pinv 基于SVD分解，对奇异矩阵也能给出最小二乘意义下的解
        % 这是一种鲁棒的 fallback 机制，防止因数值问题导致滤波器崩溃
        K = P_xz * pinv(P_zz);
    end

    % ---- 2h：状态更新（后验均值） ----
    % 卡尔曼滤波的核心更新方程：
    %   x_{k|k} = x_{k|k-1} + K * innovation
    %
    % 物理含义：
    %   预测状态 x_pred 是仅基于运动模型的猜测
    %   K * innovation 是量测带来的修正量
    %   更新后的状态 = 预测 + 修正
    %
    % 修正量由两个因素决定：
    %   1) 新息 innovation 的大小（实际与预测的偏差）
    %   2) 卡尔曼增益 K 的权重（决定"信任"量测的程度）
    %
    % 维度验证：x_pred(4×1) + K(4×3) * innovation(3×1) = 4×1
    ukf.x = x_pred + K * innovation;

    % 数值稳定性守卫：若状态更新产生NaN/Inf，回退为纯预测
    if any(isnan(ukf.x)) || any(isinf(ukf.x))
        ukf.x = x_pred;
        ukf.P = P_pred;
        lon = ukf.x(1);
        lat = ukf.x(3);
        return;
    end

    % ---- 2i：协方差更新（后验协方差） ----
    % 卡尔曼滤波的协方差更新方程（Joseph形式）：
    %   P_{k|k} = P_{k|k-1} - K * P_zz * K'
    %
    % 物理含义：
    %   P_pred 是预测的不确定性
    %   减去 K * P_zz * K' 是因为量测带来了新的信息，降低了不确定性
    %
    % 公式推导直觉：
    %   新息的协方差为 P_zz
    %   卡尔曼增益 K 将量测空间的不确定性"映射"回状态空间
    %   所以 K * P_zz * K' 就是从预测协方差中"扣除"的不确定性量
    %
    % 注意：此处使用 Joseph 形式而非简化形式 P = (I-KH)P
    %       因为 UKF 没有显式的 H 矩阵（观测矩阵），所以使用
    %       K*P_zz*K' 的形式，该形式等价且对数值误差更鲁棒
    %
    % 维度验证：P_pred(4×4) - K(4×3)*P_zz(3×3)*K'(3×4) = 4×4
    ukf.P = P_pred - K * P_zz * K';

    % ---- 2j：协方差正则化（数值稳定性维护） ----
    % 由于浮点运算的舍入误差，ukf.P 可能逐渐失去对称性或正定性
    % 调用 regularize_cov 对协方差矩阵进行修复：
    %   1) 强制对称化：P_sym = (P + P')/2
    %   2) 特征值分解后，将负的或过小的特征值裁剪到合理阈值
    %   3) 用裁剪后的特征值重构矩阵
    % 这一步是保障滤波器长期运行稳定的重要措施
    ukf.P = regularize_cov(ukf.P);

    % =====================================================================
    % 第3段：提取滤波输出
    % 从更新后的状态向量中提取目标经纬度坐标并返回
    % =====================================================================

    % 输出滤波后的经度（状态向量第1个元素）
    lon = ukf.x(1);

    % 输出滤波后的纬度（状态向量第3个元素）
    lat = ukf.x(3);
end
