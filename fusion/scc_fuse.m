% =========================================================================
% scc_fuse.m — Simple Convex Combination (SCC) 单帧融合算法
% =========================================================================
%
% 【功能概述】
%   实现简单凸组合（Simple Convex Combination / Naive Fusion）融合算法。
%   SCC 假设两个传感器的估计误差完全独立（互协方差为零），直接将两
%   个信息矩阵相加得到融合结果。这是最简单、计算量最小的融合策略。
%
% 【数学原理】
%   SCC 信息形式融合公式（隐含权 w = 0.5，即等权假设）：
%
%     P_fused^{-1} = P1^{-1} + P2^{-1}
%
%     x_fused = P_fused * ( P1^{-1} * x1 + P2^{-1} * x2 )
%
%   等价于标准卡尔曼滤波中两个独立观测的组合，其中每个传感器的估计
%   被视为一次"虚拟观测"。
%
%   SCC 的隐含假设：
%     1. 两传感器估计误差相互独立（E[(x1-x_true)(x2-x_true)'] = 0）
%     2. 两个估计均是真实值的无偏估计
%
%   注意：当存在未知互相关性时，SCC 可能给出过度乐观（overconfident）
%   的融合协方差，即 P_fused 可能小于实际误差协方差。
%
% 【输入参数】
%   x1 - [Nx1] 传感器1 的状态估计向量
%   P1 - [NxN] 传感器1 的误差协方差矩阵
%   x2 - [Nx1] 传感器2 的状态估计向量
%   P2 - [NxN] 传感器2 的误差协方差矩阵
%
% 【输出】
%   x_fused - [Nx1] 融合后的状态估计向量
%   P_fused - [NxN] 融合后的误差协方差矩阵
%
% 【调用关系】
%   被 run_track_fusion.m 调用（method='SCC' 时）
%   内部调用 regularize_cov() 对协方差进行正则化
%
% 【与其他算法的关系】
%   - SCC  是 BC 在 P12=0 时的特例
%   - SCC  是 CI 在 w=0.5 时的直接信息融合特例
%   - FCI 在 P1 和 P2 的迹相等时退化为 SCC（此时 w=0.5）
%   - SCC  是最简单快速但可能不够保守的基准算法
% =========================================================================

function [x_fused, P_fused] = scc_fuse(x1, P1, x2, P2)
    % -----------------------------------------------------------------
    % 步骤1: 正则化两个协方差矩阵
    % -----------------------------------------------------------------
    P1 = regularize_cov(P1);
    P2 = regularize_cov(P2);

    % -----------------------------------------------------------------
    % 步骤2: 独立假设下的信息矩阵加法
    %        P_fused^{-1} = P1^{-1} + P2^{-1}
    %        两个精度矩阵直接求和，融合后精度必然高于任一单传感器
    % -----------------------------------------------------------------
    P_fused_inv = inv(P1) + inv(P2);
    P_fused = inv(P_fused_inv);

    % -----------------------------------------------------------------
    % 步骤3: 融合状态估计
    %        x_fused = P_fused * ( P1^{-1} * x1 + P2^{-1} * x2 )
    %        等价于加权最小二乘，权重由各自的精度矩阵给出
    %        此处使用反斜杠运算符 (P1\x1) 代替显式求逆以提高数值精度
    % -----------------------------------------------------------------
    x_fused = P_fused * (P1 \ x1 + P2 \ x2);

    % -----------------------------------------------------------------
    % 步骤4: 最终正则化，确保输出协方差矩阵的数值稳定性
    % -----------------------------------------------------------------
    P_fused = regularize_cov(P_fused);
end
