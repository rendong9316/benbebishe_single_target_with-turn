% ========================================================================
% radar_station_create.m
% ========================================================================
%
% 【功能概述】
% 雷达站创建模块。本函数负责创建一个包含雷达站基本地理信息的
% 结构体（struct），用于后续的目标量测仿真和定位计算。
%
% 【数学原理】
% 雷达站在本系统中直接使用 WGS84 经纬度坐标系定义，不经过
% ENU（East-North-Up）等局部切平面坐标系转换。所有距离、方位
% 等计算均基于球面几何公式（如 Haversine 公式）。
%
% 【在项目流水线中的位置】
% 本模块是整个仿真系统的"传感器定义起点"：
%   第1步: radar_station_create     → 创建雷达站结构体
%   第2步: aircraft_trajectory_create → 创建飞机航迹结构体
%   第3步: measurement_simulator_create → 创建量测仿真器
%   第4步: measurement_simulator_measure → 执行量测仿真
%
% 【输入参数】
%   name  - 字符串，雷达站名称（如 '南阳'）
%   lon   - 标量双精度浮点数，雷达站经度（单位：度）
%   lat   - 标量双精度浮点数，雷达站纬度（单位：度）
%
% 【返回值】
%   radar - 结构体（struct），包含以下字段：
%           .name : 雷达站名称（字符串）
%           .lon  : 雷达站经度（度）
%           .lat  : 雷达站纬度（度）
%
% 【使用方法】
%   radar = radar_station_create('南阳', 112.50, 33.00);
%
% 【注意事项】
%   - 经纬度使用 WGS84 坐标系，东经为正、北纬为正
%   - 该结构体将被传递给 measurement_simulator_create 和
%     radar_station_true_polar 等函数使用
% ========================================================================

function radar = radar_station_create(name, lon, lat)
    % ----------------------------------------------------------------
    % radar_station_create - 创建一个雷达站结构体
    % ----------------------------------------------------------------
    % 本函数接收雷达站的名称和地理坐标（经纬度），返回一个
    % 包含了这些信息的 MATLAB 结构体。这个结构体将作为整个
    % 仿真系统中"雷达站"的数据载体，在各个函数间传递。
    %
    % 设计思路：采用"纯函数+结构体"的方式，而非面向对象的
    % classdef，使得代码更易于理解和修改，也避免了 MATLAB
    % 中 handle 类和 value 类混用可能带来的问题。
    % ----------------------------------------------------------------

    % ---- 为结构体字段赋值 ----
    % 将输入参数依次存入结构体的对应字段中
    radar.name = name;   % 雷达站名称，如 '南阳'，用于标识和显示
    radar.lon  = lon;    % 雷达站经度（单位：度），符合 WGS84 标准
    radar.lat  = lat;    % 雷达站纬度（单位：度），符合 WGS84 标准
end
% ========================================================================
% 文件结束
% ========================================================================
