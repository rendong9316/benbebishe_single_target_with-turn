% =========================================================================
% coord_systems_get_A.m
% =========================================================================
% 功能概要：
%   获取 WGS84 椭球模型的长半轴参数 A。
%   它是 WGS84 参考椭球体在赤道方向的半轴长度（即赤道半径）。
%   整个 coord_systems_* 系列函数的基准常数之一。
%
% 数学原理：
%   WGS84 (World Geodetic System 1984) 是 GPS 系统采用的参考椭球模型，
%   也是世界上使用最广泛的大地测量参考系统之一。
%
%   WGS84 椭球体的定义参数：
%     长半轴（赤道半径）  A = 6378137.0 米
%     扁率的倒数          1/f = 298.257223563
%
%   由此可以推导出：
%     扁率      f = 1 / 298.257223563 ≈ 0.003352810664747
%     短半轴    B = A × (1 - f) ≈ 6356752.314245 米
%
%   地球椭球的两个半轴长度相差约 21.4 公里（扁率约 1/298），
%   因此将地球近似为球体时精度有限。但在本项目中，
%   球面近似模型（6371000.0 米）主要用于距离和方位角计算，
%   椭球参数 A 和 E2 则用于将经纬度转换到 ECEF 地心地固坐标系。
%
%   为什么需要椭球参数？
%   球面模型虽然简单，但在将经纬度映射为三维笛卡尔坐标时，
%   如果不考虑椭球的扁平效应，会导致高程方向几公里到十几公里的误差。
%   对于本仿真中的雷达数据融合任务，使用 WGS84 椭球参数进行
%   LLA（经纬度高度）到 ECEF（地心地固）的转换可以获得更好的精度。
%
% 在项目中的角色：
%   被 coord_systems_lla_to_ecef 调用，用于计算卯酉曲率半径 N。
%   也常与 coord_systems_get_E2 搭配使用。
%
% 输入：无（本函数不接受任何输入参数）
% 输出：
%   A - WGS84 椭球模型的长半轴（赤道半径），单位：米
%       数值为 6378137.0
% =========================================================================

function A = coord_systems_get_A()
    % 返回 WGS84 椭球长半轴（赤道半径）
    % 6378137.0 米 ≈ 6378.137 公里
    % 这是 WGS84 标准的官方定义值
    A = 6378137.0;
end