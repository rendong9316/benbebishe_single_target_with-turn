% =========================================================================
% time_align_tracks.m — 雷达航迹时间对齐（外推/回退至统一时间基准）
% =========================================================================
%
% 【功能概述】
%   将两雷达异步采样的航迹结果对齐到统一的时间网格上。在当前场景中，
%   R1 采样于整分钟时刻 (0s/30s/60s/...)，R2 采样延迟 13 秒
%   (13s/43s/73s/...)。本函数以 R1 的时间网格为基准，将 R2 的 UKF
%   状态用匀速直线 (CV) 模型回退至 R1 的采样时刻。
%
% 【数学原理】
%   问题：已知 R2 在 t 时刻的 UKF 状态 x(t)，需要推断其在 t-Δt 时刻
%         的状态 x(t-Δt)，其中 Δt = time_offset_radar2_sec（13秒）。
%
%   CV 模型的状态转移矩阵 (Δt 为负值，表示回退)：
%              [1   Δt   0    0 ]
%       F(Δt)= [0    1   0    0 ]
%              [0    0   1   Δt ]
%              [0    0   0    1 ]
%     状态: [lon; v_lon; lat; v_lat]（经度、经度速度、纬度、纬度速度）
%
%   状态回退（逆向预测）：
%       x(t-Δt) = F(Δt) * x(t)
%
%   协方差传播（考虑逆向带来的额外不确定度）：
%       P(t-Δt) = F(Δt) * P(t) * F(Δt)' + Q(|Δt|)
%     其中 Q(|Δt|) = Q_base * (|Δt| / dt_sec) 为按比例缩放的过程噪声
%
%   注意：回退操作不会"改善"精度，P 在传播后只会增大或保持不变。
%   这是信息单向损失的不可逆过程。
%
% 【输入参数】
%   trackSnapshots_R2 - [n_frames x 1] cell，R2 各帧的原始航迹快照
%                       每帧 snap 包含 trackList cell 数组
%   params            - 仿真参数结构体，所需字段：
%                       .time_offset_radar2_sec - R2 采样延迟（秒），默认 13
%                       .dt_sec                - 仿真时间步长（秒），默认 30
%
% 【输出】
%   aligned_R2 - [n_frames x 1] cell，时间对齐后的 R2 航迹快照
%                结构与输入相同，但每个航迹的状态和协方差已回退 Δt
%                每个处理过的航迹增加字段 .time_aligned = true
%
% 【调用关系】
%   被 run_track_fusion.m 的上层调用者调用（通常在主仿真脚本中）
%   内部调用: regularize_cov() 对协方差进行正则化
%
% 【航迹类型说明】
%   trk.type = 7 → 已终止航迹（无 UKF 状态，跳过处理）
%   trk.type = 6 → 临时/未起始航迹（无可靠 UKF 状态，跳过处理）
%   其他 type 值 → 正常航迹，执行时间对齐
% =========================================================================

function aligned_R2 = time_align_tracks(trackSnapshots_R2, params)
    % -----------------------------------------------------------------
    % R2 相对于 R1 的采样延迟时间（秒）
    % 正值表示 R2 采样晚于 R1，因此需要回退（Δt < 0）
    % -----------------------------------------------------------------
    dt_offset = params.time_offset_radar2_sec;  % 默认 13 秒

    n_frames = length(trackSnapshots_R2);
    aligned_R2 = cell(n_frames, 1);  % 预分配输出 cell

    % =================================================================
    % 主循环：逐帧对齐 R2 航迹
    % =================================================================
    for k = 1:n_frames
        snap = trackSnapshots_R2{k};
        aligned_snap = snap;  % 浅拷贝，后续修改 trackList 中的元素

        % 遍历本帧中每条航迹
        for t = 1:length(snap.trackList)
            trk = snap.trackList{t};

            % ---------------------------------------------------------
            % 跳过不需要对齐的航迹类型
            % type=7: 已终止航迹，无有效 UKF 状态
            % type=6: 临时航迹，尚未完成 M/N 起始确认
            % ---------------------------------------------------------
            if trk.type == 7, continue; end
            if trk.type == 6, continue; end

            % 跳过 UKF 状态为空的航迹
            if isempty(trk.ukf) || ~isfield(trk.ukf, 'x') || isempty(trk.ukf.x)
                continue;
            end

            % ---------------------------------------------------------
            % 构造 CV 模型状态转移矩阵（Δt 为负，表示时间回退）
            %
            % 状态顺序：[lon; v_lon; lat; v_lat]
            %   新位置 = 旧位置 + Δt * 速度
            %   速度不变（CV 假设）
            % ---------------------------------------------------------
            dt = -dt_offset;  % 回退，故 Δt 取负
            F = [1, dt, 0, 0;
                 0,  1, 0, 0;
                 0,  0, 1, dt;
                 0,  0, 0,  1];

            % ---------------------------------------------------------
            % 状态回退: x(t-Δt) = F(Δt) * x(t)
            % ---------------------------------------------------------
            trk.ukf.x = F * trk.ukf.x;

            % ---------------------------------------------------------
            % 协方差传播（逆向预测，增加不确定度）
            % P(t-Δt) = F * P * F' + Q(|Δt|)
            %
            % 过程噪声按回退时间长度比例缩放：
            % Q(|Δt|) = Q_base * (|Δt| / dt_sec)
            % 时间越长，不确定度增加越多
            % ---------------------------------------------------------
            dt_abs = abs(dt);
            Q_dt = trk.ukf.Q * (dt_abs / params.dt_sec);
            trk.ukf.P = F * trk.ukf.P * F' + Q_dt;
            trk.ukf.P = regularize_cov(trk.ukf.P);  % 正则化确保正定性

            % ---------------------------------------------------------
            % 更新经纬度字段（从回退后的状态向量中提取）
            % ---------------------------------------------------------
            trk.lat = trk.ukf.x(3);
            trk.lon = trk.ukf.x(1);

            % ---------------------------------------------------------
            % 标记已执行时间对齐
            % ---------------------------------------------------------
            trk.time_aligned = true;

            % 写回修改后的航迹
            aligned_snap.trackList{t} = trk;
        end
        aligned_R2{k} = aligned_snap;
    end
end
